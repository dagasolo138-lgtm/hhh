import { 创建分析资料 } from './核心.js';

export function 构建分析请求(状态, 最近条数 = 80) {
  const 资料 = 创建分析资料(状态, 最近条数);

  return {
    角色说明: '你是一名模拟市场分析员。只依据提供的市场资料解释价格变化、风险状态和资金行为。不得预测必然涨跌，不得修改任何市场规则或账户数据。',
    输出要求: [
      '先用三句话概括当前市场状态。',
      '说明最近一段波动中最主要的价格推动因素。',
      '指出内在价值、趋势、波动率、流动性之间是否出现背离。',
      '分开写明事实、推断与无法判断的部分。',
    ],
    市场资料: 资料,
  };
}

export async function 请求市场分析(状态, 发送器) {
  if (typeof 发送器 !== 'function') {
    throw new Error('尚未配置智能分析发送器。');
  }

  return 发送器(构建分析请求(状态));
}
