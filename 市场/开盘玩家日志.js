function 写入日志(状态, 内容) {
  状态.历史.日志.push({
    时间: 状态.系统.虚拟时间,
    类型: '玩家委托',
    序号: 状态.系统.演算序号,
    ...内容,
  });
  const 上限 = Number(状态.结构?.日志上限 || 4000);
  if (状态.历史.日志.length > 上限) {
    状态.历史.日志.splice(0, 状态.历史.日志.length - 上限);
  }
}

/** 为自动开盘竞价执行器补写玩家可见的成交与转入连续记录。 */
export function 写入开盘玩家委托日志(状态, 开盘结果) {
  if (!开盘结果?.已执行 || !Array.isArray(开盘结果.玩家订单结果)) return 0;
  const 容器 = 状态.结构?.集合竞价;
  if (!容器 || 容器.玩家日志时间 === 状态.系统.虚拟时间) return 0;

  let 写入数量 = 0;
  for (const 结果 of 开盘结果.玩家订单结果) {
    if (结果.成交数量 > 0) {
      写入日志(状态, {
        动作: '集合竞价成交',
        编号: 结果.编号,
        数量: 结果.成交数量,
        成交价: 开盘结果.开盘价,
        成交金额: Number(结果.成交数量) * Number(开盘结果.开盘价),
        剩余数量: 结果.剩余数量,
        状态: 结果.剩余数量 > 0 ? '部分成交' : '全部成交',
        委托市场: '集合竞价',
        说明: `${结果.编号}在集合竞价以开盘价 ${开盘结果.开盘价} 成交 ${结果.成交数量}份。`,
      });
      写入数量 += 1;
    }
    if (结果.结果 === '转入连续竞价') {
      写入日志(状态, {
        动作: '转入连续竞价',
        编号: 结果.编号,
        数量: 0,
        剩余数量: 结果.剩余数量,
        状态: 结果.剩余数量 > 0 ? '部分成交' : '待撮合',
        委托市场: '连续竞价',
        说明: `${结果.编号}未在集合竞价完全成交，剩余 ${结果.剩余数量}份已转入连续竞价中心订单簿。`,
      });
      写入数量 += 1;
    }
  }
  容器.玩家日志时间 = 状态.系统.虚拟时间;
  return 写入数量;
}
