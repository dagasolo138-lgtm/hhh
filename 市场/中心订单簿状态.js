function 创建空盘口(状态, 版本) {
  const 价格 = Number(状态.变量?.基金交易价格 || 0);
  return {
    买方: [],
    卖方: [],
    中间价: 价格,
    上次成交价: 价格,
    版本,
  };
}

function 读取中心状态(状态) {
  状态.结构 ??= {};
  状态.结构.中心订单簿 ??= {
    下次委托序号: 1,
    本刻订单: [],
    本刻成交: [],
    订单历史: [],
    成交历史: [],
    盘口快照: 创建空盘口(状态, 0),
    版本: 0,
    最近更新: null,
  };
  return 状态.结构.中心订单簿;
}

export function 清空中心订单簿当刻记录(状态, 原因 = '非连续竞价') {
  const 中心 = 读取中心状态(状态);
  中心.本刻订单 = [];
  中心.本刻成交 = [];
  中心.盘口快照 = 创建空盘口(状态, Number(中心.版本 || 0) + 1);
  中心.版本 = Number(中心.版本 || 0) + 1;
  中心.最近更新 = null;
  中心.本刻原因 = 原因;
  return 中心;
}
