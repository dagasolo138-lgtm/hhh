export function 计算当日价格边界(上一交易日收盘价) {
  const 基准价 = Number(上一交易日收盘价);
  if (!Number.isFinite(基准价) || 基准价 <= 0) {
    throw new Error('上一交易日收盘价必须是大于零的有限数值。');
  }

  return Object.freeze({
    跌停价: Math.max(上交所黄金ETF交易制度.二级市场.价格最小变动, 基准价 * (1 - 上交所黄金ETF交易制度.二级市场.日涨跌幅限制)),
    涨停价: 基准价 * (1 + 上交所黄金ETF交易制度.二级市场.日涨跌幅限制),
  });
}

export function 验证二级市场数量(方向, 数量) {
  const 数值 = Math.floor(Number(数量));
  const 最小单位 = 上交所黄金ETF交易制度.二级市场.最小买入数量;

  if (!Number.isFinite(数值) || 数值 <= 0) {
    return Object.freeze({ 有效: false, 原因: '申报数量必须是大于零的整数。' });
  }

  if (方向 === '买入' && 数值 % 最小单位 !== 0) {
    return Object.freeze({ 有效: false, 原因: `买入申报数量必须为 ${最小单位} 份的整数倍。` });
  }

  return Object.freeze({ 有效: true, 原因: '' });
}

export function 是否处于集合竞价(时分) {
  const 时段 = 上交所黄金ETF交易制度.二级市场.集合竞价;
  return 时分 >= 时段.开始 && 时分 < 时段.结束;
}

export function 是否集合竞价允许撤单(时分) {
  const 时段 = 上交所黄金ETF交易制度.二级市场.集合竞价;
  return 时分 >= 时段.开始 && 时分 < 时段.撤单截止;
}

export function 是否处于连续竞价(时分) {
  return 上交所黄金ETF交易制度.二级市场.连续竞价时段.some(
    (时段) => 时分 >= 时段.开始 && 时分 < 时段.结束,
  );
}
