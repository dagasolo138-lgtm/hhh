import { 创建中国黄金市场状态 } from './状态工厂.js';
import { 初始化交易制度状态 } from './制度执行器.js';
import { 提交玩家集合竞价委托 } from './集合竞价.js';
import { 初始化开盘增强时序市场v2, 推进开盘增强时序市场v2 } from './时序演算器开盘增强v2.js';

function 断言(条件, 信息) {
  if (!条件) throw new Error(信息);
}

function 求和(状态, 字段) {
  return Object.values(状态.交易者).reduce((合计, 参与者) => 合计 + Number(参与者[字段] || 0), 0) + Number(状态.变量[`玩家${字段}`] || 0);
}

const 状态 = 创建中国黄金市场状态({
  随机种子: 2026062204,
  初始时间: '2026-06-22T01:15:00.000Z',
});
初始化交易制度状态(状态);
状态.交易者.价值套利.目标仓位 = 状态.交易者.价值套利.持仓 + 400;
状态.交易者.风险控制资金.目标仓位 = Math.max(0, 状态.交易者.风险控制资金.持仓 - 400);
初始化开盘增强时序市场v2(状态);

const 自动订单 = 状态.结构.集合竞价.自动订单;
断言(自动订单.length >= 2, '09:15必须生成至少两笔自动集合竞价订单。');
断言(自动订单.some((订单) => 订单.参与者标识 === '价值套利' && 订单.方向 === '买入'), '价值套利买入意图未进入集合竞价池。');
断言(自动订单.some((订单) => 订单.参与者标识 === '风险控制资金' && 订单.方向 === '卖出'), '风险控制卖出意图未进入集合竞价池。');

const 初始现金 = 求和(状态, '现金');
const 初始份额 = 求和(状态, '持仓');
const 涨停价 = 状态.结构.交易制度.当日涨停价;
const 玩家订单 = 提交玩家集合竞价委托(状态, '买入', 100, 涨停价);
断言(玩家订单.成功, '玩家集合竞价订单提交失败。');

推进开盘增强时序市场v2(状态);
推进开盘增强时序市场v2(状态);
const 开盘日志 = 推进开盘增强时序市场v2(状态);
const 开盘结果 = 开盘日志.集合竞价开盘;

断言(开盘结果?.已执行, '09:30必须执行自动开盘集合竞价。');
断言(开盘结果.开盘价 > 0, '开盘集合竞价必须产生单一开盘价。');
断言(开盘结果.自动订单结果.length >= 2, '开盘结果必须包含自动订单结算结果。');
断言(状态.结构.集合竞价.最近结果.自动订单数量 >= 2, '集合竞价摘要缺少自动订单数量。');
断言(Math.abs(求和(状态, '现金') - 初始现金) < 0.1, '开盘集合竞价后现金必须守恒。');
断言(Math.abs(求和(状态, '持仓') - 初始份额) < 0.0001, '开盘集合竞价后份额必须守恒。');

const 自动成交总量 = 开盘结果.自动订单结果.reduce((合计, 订单) => 合计 + Number(订单.成交数量 || 0), 0);
断言(自动成交总量 > 0 || 开盘结果.自动转入连续数量 > 0, '自动订单必须成交或转入连续竞价。');

推进开盘增强时序市场v2(状态);
const 中心转入 = 状态.结构.中心订单簿?.开盘转入订单 || [];
if (开盘结果.自动转入连续数量 > 0) {
  断言(中心转入.length > 0 || 状态.结构.集合竞价.自动转入连续.length === 0, '未成交自动订单必须被连续竞价接续处理。');
}

console.log(JSON.stringify({
  结果: '通过',
  自动订单数量: 自动订单.length,
  自动成交总量,
  自动转入连续数量: 开盘结果.自动转入连续数量,
  开盘价: 开盘结果.开盘价,
}, null, 2));
