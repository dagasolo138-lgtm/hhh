import { 上交所黄金ETF交易制度 } from './交易制度参数.js';
import { 初始化交易制度状态, 读取当日价格边界 } from './制度执行器.js';

function 是价格最小变动整数倍(价格) {
  const 最小变动 = 上交所黄金ETF交易制度.二级市场.价格最小变动;
  const 倍数 = Number(价格) / 最小变动;
  return Math.abs(倍数 - Math.round(倍数)) < 0.0000001;
}

function 接近(左, 右, 容差 = 0.01) {
  return Math.abs(Number(左 || 0) - Number(右 || 0)) <= 容差;
}

const 合法订单状态 = new Set(['待撮合', '部分成交', '全部成交', '已撤销', '已拒绝', '已过期']);
const 待处理订单状态 = new Set(['待撮合', '部分成交']);

function 检查订单列表(订单列表, 待处理列表, 错误) {
  for (const 订单 of 订单列表) {
    if (!合法订单状态.has(订单.状态)) 错误.push('玩家委托存在未知状态。');
    if (!['买入', '卖出'].includes(订单.方向)) 错误.push('玩家委托存在未知方向。');
    if (!(Number(订单.数量) > 0)) 错误.push('玩家委托数量不合法。');
    if (!(Number(订单.已成交数量 || 0) >= 0) || !(Number(订单.剩余数量 || 0) >= 0)) 错误.push('玩家委托成交数量不合法。');
    if (!接近(Number(订单.已成交数量 || 0) + Number(订单.剩余数量 || 0), Number(订单.数量 || 0), 0.0001)) {
      错误.push('玩家委托的已成交数量与剩余数量不守恒。');
    }
    if (待处理列表.some((项) => 项.编号 === 订单.编号) && !待处理订单状态.has(订单.状态)) {
      错误.push('待处理委托的状态不合法。');
    }
  }
}

function 检查玩家中心订单映射(状态, 连续待处理, 错误) {
  const 中心 = 状态.结构?.中心订单簿;
  // 初次加载旧存档时中心快照尚未构建；等同一虚拟时刻的中心撮合完成后再强制映射校验。
  if (!中心 || 中心.最近更新 !== 状态.系统?.虚拟时间) return;
  const 活跃玩家中心订单 = (中心.本刻订单 || []).filter((订单) => (
    订单.参与者标识 === '玩家'
    && !订单.市价单
    && ['待撮合', '部分成交'].includes(订单.状态)
  ));
  const 中心编号 = new Set(活跃玩家中心订单.map((订单) => 订单.编号));
  for (const 订单 of 连续待处理) {
    if (状态.系统.是否可交易 && !中心编号.has(订单.编号)) {
      错误.push('待处理连续竞价委托未进入中心订单簿。');
      break;
    }
  }
  for (const 中心订单项 of 活跃玩家中心订单) {
    if (!连续待处理.some((订单) => 订单.编号 === 中心订单项.编号)) {
      错误.push('中心订单簿存在脱离玩家委托容器的玩家限价单。');
      break;
    }
  }
}

export function 检查交易制度一致性(状态) {
  const 错误 = [];
  初始化交易制度状态(状态);

  const 变量 = 状态.变量;
  const { 前收盘价, 涨停价, 跌停价 } = 读取当日价格边界(状态);
  const 冻结 = 状态.结构.玩家冻结资产;
  const 连续委托 = 状态.结构.玩家委托;
  const 集合竞价 = 状态.结构.集合竞价;

  if (!(前收盘价 > 0 && 涨停价 >= 前收盘价 && 跌停价 <= 前收盘价 && 跌停价 > 0)) 错误.push('前收盘价或当日涨跌停边界不合法。');
  if (变量.基金交易价格 < 跌停价 || 变量.基金交易价格 > 涨停价) 错误.push('基金交易价格突破当日涨跌停边界。');
  if (!是价格最小变动整数倍(变量.基金交易价格)) 错误.push('基金交易价格未按最小报价单位对齐。');

  for (const 档位 of [...(状态.订单簿?.买方 || []), ...(状态.订单簿?.卖方 || [])]) {
    if (档位.价格 < 跌停价 || 档位.价格 > 涨停价) 错误.push('订单簿报价突破当日涨跌停边界。');
    if (!是价格最小变动整数倍(档位.价格)) 错误.push('订单簿报价未按最小报价单位对齐。');
  }

  if (冻结.现金 < -0.01 || 冻结.现金 > 变量.玩家现金 + 0.01) 错误.push('玩家冻结现金不合法。');
  if (冻结.份额 < 0 || 冻结.份额 > 变量.玩家持仓) 错误.push('玩家冻结份额不合法。');
  if (!Array.isArray(连续委托.待处理) || !Array.isArray(连续委托.历史)) 错误.push('连续竞价委托容器不完整。');
  if (!Array.isArray(集合竞价.待处理) || !Array.isArray(集合竞价.历史)) 错误.push('集合竞价委托容器不完整。');

  const 连续待处理 = 连续委托.待处理 || [];
  const 所有待处理 = [...连续待处理, ...(集合竞价.待处理 || [])];
  const 所有订单 = [...连续待处理, ...(连续委托.历史 || []), ...(集合竞价.待处理 || []), ...(集合竞价.历史 || [])];
  检查订单列表(所有订单, 所有待处理, 错误);
  检查玩家中心订单映射(状态, 连续待处理, 错误);

  const 编号集合 = new Set();
  for (const 订单 of 所有订单) {
    if (编号集合.has(订单.编号)) 错误.push('玩家委托编号重复。');
    编号集合.add(订单.编号);
  }

  const 待冻结现金 = 所有待处理.filter((订单) => 订单.方向 === '买入').reduce((合计, 订单) => 合计 + Number(订单.冻结现金 || 0), 0);
  const 待冻结份额 = 所有待处理.filter((订单) => 订单.方向 === '卖出').reduce((合计, 订单) => 合计 + Number(订单.冻结份额 || 0), 0);
  if (!接近(冻结.现金, 待冻结现金)) 错误.push('玩家冻结现金与全部待处理买单不一致。');
  if (!接近(冻结.份额, 待冻结份额, 0.0001)) 错误.push('玩家冻结份额与全部待处理卖单不一致。');

  return Object.freeze({ 有效: 错误.length === 0, 错误 });
}

export function 断言交易制度一致性(状态) {
  const 结果 = 检查交易制度一致性(状态);
  if (!结果.有效) throw new Error(`交易制度一致性检查失败：${结果.错误.join('；')}`);
  return 结果;
}
