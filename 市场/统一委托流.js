import { 保留数值 } from './状态工厂.js';
import { 读取中心订单簿 } from './中心订单簿.js';

function 数值(输入, 默认值 = 0) {
  const 结果 = Number(输入);
  return Number.isFinite(结果) ? 结果 : 默认值;
}

function 整数(输入) {
  return Math.max(0, Math.floor(数值(输入)));
}

function 初始化统一委托流(状态) {
  状态.结构 ??= {};
  状态.结构.统一委托流 ??= {
    本刻自动委托: [],
    自动委托历史: [],
    最近更新: null,
    版本: 0,
  };
  const 流 = 状态.结构.统一委托流;
  流.本刻自动委托 ??= [];
  流.自动委托历史 ??= [];
  流.版本 ??= 0;
  return 流;
}

function 基础盘口(状态) {
  const 订单簿 = 状态.订单簿 || { 买方: [], 卖方: [] };
  const 转换 = (方向) => (订单簿[方向] || []).map((档位, 索引) => ({
    档位: Number(档位.档位 || 索引 + 1),
    价格: 数值(档位.价格),
    做市数量: 整数(档位.数量),
    自动挂单数量: 0,
    玩家挂单数量: 0,
    来源: ['做市商'],
  }));
  return {
    买方: 转换('买方'),
    卖方: 转换('卖方'),
    中间价: 数值(订单簿.中间价, 状态.变量?.基金交易价格),
    上次成交价: 数值(订单簿.上次成交价, 状态.变量?.基金交易价格),
    版本: 0,
  };
}

function 叠加数量(档位列表, 价格, 字段, 数量, 来源) {
  if (!(价格 > 0) || !(数量 > 0)) return;
  let 档位 = 档位列表.find((项) => Math.abs(项.价格 - 价格) < 0.0000001);
  if (!档位) {
    档位 = {
      档位: 0,
      价格: 保留数值(价格, 3),
      做市数量: 0,
      自动挂单数量: 0,
      玩家挂单数量: 0,
      来源: [],
    };
    档位列表.push(档位);
  }
  档位[字段] += 整数(数量);
  if (!档位.来源.includes(来源)) 档位.来源.push(来源);
}

function 排序十档(档位列表, 方向) {
  return [...档位列表]
    .filter((档位) => 档位.做市数量 + 档位.自动挂单数量 + 档位.玩家挂单数量 > 0)
    .sort((左, 右) => 方向 === '买方' ? 右.价格 - 左.价格 : 左.价格 - 右.价格)
    .slice(0, 10)
    .map((档位, 索引) => ({
      ...档位,
      档位: 索引 + 1,
      数量: 档位.做市数量 + 档位.自动挂单数量 + 档位.玩家挂单数量,
      可成交数量: 档位.做市数量 + 档位.自动挂单数量,
    }));
}

function 读取玩家连续挂单(状态) {
  const 订单 = Array.isArray(状态.结构?.玩家委托?.待处理) ? 状态.结构.玩家委托.待处理 : [];
  return 订单.filter((项) => 项.当前委托市场 !== '集合竞价' && ['待撮合', '部分成交'].includes(项.状态));
}

function 叠加玩家挂单(状态, 盘口) {
  const 买方 = 盘口.买方.map((档位) => ({ ...档位, 来源: [...档位.来源] }));
  const 卖方 = 盘口.卖方.map((档位) => ({ ...档位, 来源: [...档位.来源] }));
  for (const 订单 of 读取玩家连续挂单(状态)) {
    const 目标 = 订单.方向 === '买入' ? 买方 : 卖方;
    叠加数量(目标, Number(订单.价格), '玩家挂单数量', Number(订单.剩余数量), '玩家');
  }
  const 可见买方 = 排序十档(买方, '买方');
  const 可见卖方 = 排序十档(卖方, '卖方');
  return {
    买方: 可见买方,
    卖方: 可见卖方,
    中间价: 盘口.中间价,
    上次成交价: 盘口.上次成交价,
    版本: 盘口.版本,
    汇总: {
      做市数量: 可见买方.concat(可见卖方).reduce((合计, 档位) => 合计 + 档位.做市数量, 0),
      自动挂单数量: 可见买方.concat(可见卖方).reduce((合计, 档位) => 合计 + 档位.自动挂单数量, 0),
      玩家挂单数量: 可见买方.concat(可见卖方).reduce((合计, 档位) => 合计 + 档位.玩家挂单数量, 0),
    },
  };
}

export function 同步统一委托流(状态) {
  const 流 = 初始化统一委托流(状态);
  const 中心 = 读取中心订单簿(状态);
  const 自动订单 = 中心.本刻订单.filter((订单) => Boolean(订单.自动生成));
  const 完结订单 = 自动订单.filter((订单) => !['待撮合', '部分成交'].includes(订单.状态));
  流.本刻自动委托 = 自动订单.map((订单) => ({ ...订单 }));
  流.自动委托历史.push(...完结订单.map((订单) => ({ ...订单 })));
  if (流.自动委托历史.length > 2400) {
    流.自动委托历史.splice(0, 流.自动委托历史.length - 2400);
  }
  流.最近更新 = 状态.系统?.虚拟时间 || null;
  流.版本 = Math.max(Number(流.版本 || 0) + 1, Number(中心.版本 || 0));
  return 流;
}

// 兼容旧调用名；当前不再从市场日志推测订单，而是直接同步中心订单簿真实记录。
export function 记录自动交易者委托流(状态) {
  return 同步统一委托流(状态).本刻自动委托.map((订单) => ({ ...订单 }));
}

export function 构建可见十档盘口(状态) {
  const 中心 = 读取中心订单簿(状态);
  const 原始盘口 = (中心.盘口快照?.买方?.length || 中心.盘口快照?.卖方?.length)
    ? 中心.盘口快照
    : 基础盘口(状态);
  return Object.freeze(叠加玩家挂单(状态, 原始盘口));
}

export function 读取统一委托流(状态) {
  const 流 = 同步统一委托流(状态);
  return Object.freeze({
    自动委托: 流.本刻自动委托.map((订单) => ({ ...订单 })),
    自动委托历史: 流.自动委托历史.slice(-160).map((订单) => ({ ...订单 })),
    可见盘口: 构建可见十档盘口(状态),
  });
}
