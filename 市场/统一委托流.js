import { 保留数值 } from './状态工厂.js';

const 自动参与者名称 = Object.freeze({
  价值套利: '价值套利',
  趋势资金: '趋势资金',
  散户群体: '散户群体',
  风险控制资金: '风险控制资金',
  信息优势资金: '先行信号资金',
});

function 数值(输入, 默认值 = 0) {
  const 结果 = Number(输入);
  return Number.isFinite(结果) ? 结果 : 默认值;
}

function 取整数量(输入) {
  return Math.max(0, Math.floor(数值(输入)));
}

function 取订单簿档位(订单簿, 方向) {
  const 原始 = Array.isArray(订单簿?.[方向]) ? 订单簿[方向] : [];
  return 原始.map((档位, 索引) => ({
    档位: Number(档位.档位 || 索引 + 1),
    价格: 数值(档位.价格),
    做市数量: 取整数量(档位.数量),
    自动挂单数量: 0,
    玩家挂单数量: 0,
    可成交数量: 取整数量(档位.数量),
    来源: ['做市商'],
  }));
}

function 初始化统一委托流(状态) {
  状态.结构 ??= {};
  状态.结构.统一委托流 ??= {
    下次自动编号: 1,
    本刻自动委托: [],
    自动委托历史: [],
    最近更新: null,
    版本: 0,
  };
  const 流 = 状态.结构.统一委托流;
  流.下次自动编号 ??= 1;
  流.本刻自动委托 ??= [];
  流.自动委托历史 ??= [];
  流.版本 ??= 0;
  return 流;
}

function 估算买卖成交量(日志) {
  const 总成交量 = Math.max(0, 数值(日志?.成交量));
  const 订单失衡 = Math.max(-1, Math.min(1, 数值(日志?.订单失衡)));
  const 买入量 = Math.round(总成交量 * (1 + 订单失衡) / 2);
  const 卖出量 = Math.max(0, 总成交量 - 买入量);
  return { 买入量, 卖出量 };
}

function 按方向分配成交(订单列表, 可成交量, 方向) {
  const 待分配 = [...订单列表]
    .filter((订单) => 订单.方向 === 方向 && 订单.申请数量 > 0)
    .sort((左, 右) => {
      if (左.限价 !== 右.限价) return 方向 === '买入' ? 右.限价 - 左.限价 : 左.限价 - 右.限价;
      return 左.委托序号 - 右.委托序号;
    });

  let 剩余可成交量 = Math.max(0, 可成交量);
  for (const 订单 of 待分配) {
    const 成交数量 = Math.min(订单.申请数量, 剩余可成交量);
    订单.成交数量 = 成交数量;
    订单.剩余数量 = 订单.申请数量 - 成交数量;
    订单.状态 = 订单.剩余数量 ? (成交数量 ? '部分成交' : '待撮合') : '全部成交';
    剩余可成交量 -= 成交数量;
  }
}

function 估算自动限价(状态, 方向, 成交均价) {
  const 当前价 = 数值(状态.变量?.基金交易价格, 1);
  const 买一 = 数值(状态.订单簿?.买方?.[0]?.价格, 当前价);
  const 卖一 = 数值(状态.订单簿?.卖方?.[0]?.价格, 当前价);
  if (成交均价 > 0) return 保留数值(成交均价, 3);
  return 保留数值(方向 === '买入' ? 卖一 : 买一, 3);
}

export function 记录自动交易者委托流(状态, 市场日志 = {}) {
  const 流 = 初始化统一委托流(状态);
  const 上一批 = 流.本刻自动委托;
  for (const 旧订单 of 上一批) {
    if (旧订单.状态 === '待撮合' || 旧订单.状态 === '部分成交') {
      旧订单.状态 = '已过期';
      旧订单.结束时间 = 状态.系统.虚拟时间;
      流.自动委托历史.push({ ...旧订单 });
    }
  }

  const 原始订单 = 市场日志?.订单 || {};
  const { 买入量, 卖出量 } = 估算买卖成交量(市场日志);
  const 买入均价 = 数值(市场日志?.交易价格, 状态.变量?.基金交易价格);
  const 卖出均价 = 买入均价;
  const 本刻订单 = [];

  for (const [标识, 原始数量] of Object.entries(原始订单)) {
    const 签名数量 = Math.trunc(数值(原始数量));
    if (!签名数量 || !自动参与者名称[标识]) continue;
    const 方向 = 签名数量 > 0 ? '买入' : '卖出';
    const 申请数量 = Math.abs(签名数量);
    本刻订单.push({
      编号: `自动委托-${流.下次自动编号}`,
      委托序号: 流.下次自动编号,
      来源: 自动参与者名称[标识],
      参与者标识: 标识,
      委托类型: '本刻即时限价单',
      方向,
      限价: 估算自动限价(状态, 方向, 方向 === '买入' ? 买入均价 : 卖出均价),
      申请数量,
      成交数量: 0,
      剩余数量: 申请数量,
      状态: '待撮合',
      委托时间: 状态.系统.虚拟时间,
      有效期: '本刻',
      自动生成: true,
    });
    流.下次自动编号 += 1;
  }

  按方向分配成交(本刻订单, 买入量, '买入');
  按方向分配成交(本刻订单, 卖出量, '卖出');
  for (const 订单 of 本刻订单) {
    if (订单.状态 === '全部成交') {
      订单.结束时间 = 状态.系统.虚拟时间;
      流.自动委托历史.push({ ...订单 });
    }
  }

  流.本刻自动委托 = 本刻订单;
  if (流.自动委托历史.length > 2400) {
    流.自动委托历史.splice(0, 流.自动委托历史.length - 2400);
  }
  流.最近更新 = 状态.系统.虚拟时间;
  流.版本 += 1;
  return 本刻订单.map((订单) => ({ ...订单 }));
}

function 将数量叠加到价格档位(档位列表, 价格, 字段, 数量, 来源) {
  if (!(价格 > 0) || !(数量 > 0)) return;
  let 档位 = 档位列表.find((项) => Math.abs(项.价格 - 价格) < 0.0000001);
  if (!档位) {
    档位 = {
      档位: 0,
      价格: 保留数值(价格, 3),
      做市数量: 0,
      自动挂单数量: 0,
      玩家挂单数量: 0,
      可成交数量: 0,
      来源: [],
    };
    档位列表.push(档位);
  }
  档位[字段] += 取整数量(数量);
  if (!档位.来源.includes(来源)) 档位.来源.push(来源);
}

function 排序十档(档位列表, 方向) {
  return [...档位列表]
    .filter((档位) => 档位.做市数量 + 档位.自动挂单数量 + 档位.玩家挂单数量 > 0)
    .sort((左, 右) => 方向 === '买方' ? 右.价格 - 左.价格 : 左.价格 - 右.价格)
    .slice(0, 10)
    .map((档位, 索引) => ({
      ...档位,
      档位: 索引 + 1,
      数量: 档位.做市数量 + 档位.自动挂单数量 + 档位.玩家挂单数量,
      可成交数量: 档位.做市数量 + 档位.自动挂单数量,
    }));
}

function 读取连续玩家挂单(状态) {
  const 列表 = Array.isArray(状态.结构?.玩家委托?.待处理) ? 状态.结构.玩家委托.待处理 : [];
  return 列表.filter((订单) => 订单.当前委托市场 !== '集合竞价' && ['待撮合', '部分成交'].includes(订单.状态));
}

export function 构建可见十档盘口(状态) {
  const 流 = 初始化统一委托流(状态);
  const 买方 = 取订单簿档位(状态.订单簿, '买方');
  const 卖方 = 取订单簿档位(状态.订单簿, '卖方');

  for (const 订单 of 流.本刻自动委托) {
    if (!['待撮合', '部分成交'].includes(订单.状态) || !订单.剩余数量) continue;
    const 目标 = 订单.方向 === '买入' ? 买方 : 卖方;
    将数量叠加到价格档位(目标, 订单.限价, '自动挂单数量', 订单.剩余数量, 订单.来源);
  }

  for (const 订单 of 读取连续玩家挂单(状态)) {
    const 目标 = 订单.方向 === '买入' ? 买方 : 卖方;
    将数量叠加到价格档位(目标, 订单.价格, '玩家挂单数量', 订单.剩余数量, '玩家');
  }

  const 可见买方 = 排序十档(买方, '买方');
  const 可见卖方 = 排序十档(卖方, '卖方');
  return Object.freeze({
    买方: 可见买方,
    卖方: 可见卖方,
    中间价: 数值(状态.订单簿?.中间价, 状态.变量?.基金交易价格),
    上次成交价: 数值(状态.订单簿?.上次成交价, 状态.变量?.基金交易价格),
    版本: 流.版本,
    汇总: Object.freeze({
      做市数量: 可见买方.concat(可见卖方).reduce((合计, 档位) => 合计 + 档位.做市数量, 0),
      自动挂单数量: 可见买方.concat(可见卖方).reduce((合计, 档位) => 合计 + 档位.自动挂单数量, 0),
      玩家挂单数量: 可见买方.concat(可见卖方).reduce((合计, 档位) => 合计 + 档位.玩家挂单数量, 0),
    }),
  });
}

export function 读取统一委托流(状态) {
  const 流 = 初始化统一委托流(状态);
  const 可见盘口 = 构建可见十档盘口(状态);
  return Object.freeze({
    自动委托: 流.本刻自动委托.map((订单) => ({ ...订单 })),
    自动委托历史: 流.自动委托历史.slice(-160).map((订单) => ({ ...订单 })),
    可见盘口,
  });
}
