import { 创建中国黄金市场状态, 同步系统变量 } from './状态工厂.js';
import { 初始化交易制度状态 } from './制度执行器.js';
import { 执行自动中心订单簿, 读取中心订单簿 } from './中心订单簿.js';

function 断言(条件, 信息) {
  if (!条件) throw new Error(信息);
}

function 总现金(状态) {
  return Object.values(状态.交易者).reduce((合计, 参与者) => 合计 + Number(参与者.现金 || 0), 0);
}

function 总份额(状态) {
  return Object.values(状态.交易者).reduce((合计, 参与者) => 合计 + Number(参与者.持仓 || 0), 0);
}

const 状态 = 创建中国黄金市场状态({
  随机种子: 2026062202,
  初始时间: '2026-06-22T01:30:00.000Z',
});
同步系统变量(状态);
初始化交易制度状态(状态);
状态.变量.基金交易价格 = 7;
状态.变量.基金理论净值 = 7;
状态.订单簿 = {
  买方: [
    { 档位: 1, 价格: 6.99, 数量: 100 },
    { 档位: 2, 价格: 6.98, 数量: 100 },
  ],
  卖方: [
    { 档位: 1, 价格: 7.01, 数量: 100 },
    { 档位: 2, 价格: 7.02, 数量: 100 },
  ],
  中间价: 7,
  上次成交价: 7,
  版本: 1,
};

状态.交易者.价值套利.目标仓位 = 状态.交易者.价值套利.持仓 + 200;
状态.交易者.趋势资金.目标仓位 = 状态.交易者.趋势资金.持仓;
状态.交易者.散户群体.目标仓位 = 状态.交易者.散户群体.持仓;
状态.交易者.风险控制资金.目标仓位 = 状态.交易者.风险控制资金.持仓 - 200;
状态.交易者.信息优势资金.目标仓位 = 状态.交易者.信息优势资金.持仓;

const 初始现金 = 总现金(状态);
const 初始份额 = 总份额(状态);
const 原始买一数量 = 状态.订单簿.买方[0].数量;
const 结果 = 执行自动中心订单簿(状态, {
  价值套利: 200,
  趋势资金: 0,
  散户群体: 0,
  风险控制资金: 200,
  信息优势资金: 0,
});
const 中心 = 读取中心订单簿(状态);

断言(状态.系统.是否可交易, '自检必须在连续竞价时段执行。');
断言(结果.本刻订单.length >= 4, '自动交易者应拆分为主动与被动中心订单。');
断言(结果.成交.length > 0, '中心订单簿应产生逐笔成交。');
断言(状态.变量.成交笔数 === 结果.成交.length, '成交笔数必须等于中心成交记录数量。');
断言(状态.变量.基金交易价格 === 结果.成交.at(-1).价格, '交易价格必须由最后一笔中心成交形成。');
断言(Math.abs(总现金(状态) - 初始现金) < 0.02, '中心成交后自动交易者现金必须守恒。');
断言(总份额(状态) === 初始份额, '中心成交后自动交易者份额必须守恒。');
断言(中心.本刻成交.every((成交) => 成交.买方标识 !== 成交.卖方标识), '中心订单簿不得发生自成交。');
断言(中心.盘口快照.买方[0].价格 < 中心.盘口快照.卖方[0].价格, '中心盘口买一必须低于卖一。');
断言(状态.订单簿.买方[0].数量 === 原始买一数量, '中心订单簿不得篡改玩家路径使用的基础做市盘口。');
assertRemaining(中心.本刻订单);

function assertRemaining(订单列表) {
  for (const 订单 of 订单列表) {
    断言(订单.已成交数量 + 订单.剩余数量 === 订单.数量, '中心订单成交数量与剩余数量必须守恒。');
  }
}

console.log(JSON.stringify({
  结果: '通过',
  成交笔数: 结果.成交.length,
  成交量: 状态.变量.成交量,
  最后成交价: 状态.变量.基金交易价格,
  剩余自动订单: 中心.本刻订单.filter((订单) => 订单.自动生成 && 订单.剩余数量 > 0).length,
}, null, 2));
