import { 初始化自动开盘竞价订单 } from './自动开盘竞价.js';

function 重置自动开盘订单(容器, 交易日) {
  容器.自动订单 = [];
  容器.自动历史 ??= [];
  容器.自动转入连续 ??= [];
  容器.自动订单已生成 = false;
  容器.自动订单锁定 = false;
  容器.自动订单交易日 = 交易日;
}

/**
 * 自动资金可在玩家撤单窗口内随新信息调整开盘意图；09:20 起锁定。
 * 这避免新世界在 09:15 目标仓位尚未形成时生成空订单后永久锁死。
 */
export function 刷新自动开盘竞价订单(状态) {
  const 制度 = 状态.结构?.交易制度;
  const 容器 = 状态.结构?.集合竞价;
  if (!制度 || !容器) return [];

  if (容器.自动订单交易日 !== 制度.当前交易日) {
    重置自动开盘订单(容器, 制度.当前交易日);
  }
  if (!制度.集合竞价中) return 容器.自动订单 || [];

  const 应锁定 = !制度.集合竞价可撤单;
  const 需要刷新 = !容器.自动订单已生成 || (!容器.自动订单锁定 && 容器.自动订单生成时分 !== 制度.当前时分);
  if (需要刷新) {
    容器.自动订单 = [];
    容器.自动订单已生成 = false;
    初始化自动开盘竞价订单(状态);
    容器.自动订单生成时分 = 制度.当前时分;
  }
  if (应锁定) 容器.自动订单锁定 = true;
  return 容器.自动订单 || [];
}
