function 数值(输入, 默认值 = 0) {
  const 结果 = Number(输入);
  return Number.isFinite(结果) ? 结果 : 默认值;
}

function 形成方向快照(订单列表, 方向) {
  const 层级 = new Map();
  for (const 订单 of (订单列表 || [])) {
    if (订单.方向 !== 方向 || !(订单.剩余数量 > 0) || !['待撮合', '部分成交'].includes(订单.状态)) continue;
    const 键 = 数值(订单.价格).toFixed(3);
    const 层 = 层级.get(键) || {
      价格: 数值(订单.价格),
      做市数量: 0,
      自动挂单数量: 0,
      玩家挂单数量: 0,
      来源: [],
    };
    if (订单.做市报价) 层.做市数量 += 数值(订单.剩余数量);
    else if (订单.玩家订单) 层.玩家挂单数量 += 数值(订单.剩余数量);
    else 层.自动挂单数量 += 数值(订单.剩余数量);
    if (!层.来源.includes(订单.来源)) 层.来源.push(订单.来源);
    层级.set(键, 层);
  }
  return [...层级.values()]
    .sort((左, 右) => 方向 === '买入' ? 右.价格 - 左.价格 : 左.价格 - 右.价格)
    .slice(0, 10)
    .map((层, 索引) => ({
      档位: 索引 + 1,
      ...层,
      数量: 层.做市数量 + 层.自动挂单数量 + 层.玩家挂单数量,
      可成交数量: 层.做市数量 + 层.自动挂单数量,
    }));
}

/** 订单删除、撤销或市价单过期后重建可见中心盘口。 */
export function 刷新中心盘口快照(状态) {
  const 中心 = 状态.结构?.中心订单簿;
  if (!中心) return null;
  const 上次成交价 = 数值(中心.本刻成交?.at(-1)?.价格, 状态.变量?.基金交易价格);
  中心.盘口快照 = {
    买方: 形成方向快照(中心.本刻订单, '买入'),
    卖方: 形成方向快照(中心.本刻订单, '卖出'),
    中间价: 上次成交价,
    上次成交价,
    版本: Number(中心.版本 || 0) + 1,
  };
  中心.版本 = Number(中心.版本 || 0) + 1;
  return 中心.盘口快照;
}
