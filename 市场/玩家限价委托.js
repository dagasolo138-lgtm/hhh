function 取整数量(数量) {
  return Math.max(0, Math.floor(Number(数量) || 0));
}

function 取北京时间日期键(时间) {
  const 日期 = new Date(new Date(时间).getTime() + 8 * 60 * 60 * 1000);
  return `${日期.getUTCFullYear()}-${String(日期.getUTCMonth() + 1).padStart(2, '0')}-${String(日期.getUTCDate()).padStart(2, '0')}`;
}

function 记录玩家委托日志(状态, 内容) {
  状态.历史.日志.push({
    时间: 状态.系统.虚拟时间,
    类型: '玩家委托',
    序号: 状态.系统.演算序号,
    ...内容,
  });
  if (状态.历史.日志.length > 状态.结构.日志上限) {
    状态.历史.日志.splice(0, 状态.历史.日志.length - 状态.结构.日志上限);
  }
}

function 刷新订单状态统计(状态) {
  const 统计 = { 待撮合: 0, 部分成交: 0, 全部成交: 0, 已撤销: 0, 已拒绝: 0, 已过期: 0 };
  const 容器 = 状态.结构.玩家委托;
  for (const 订单 of [...容器.待处理, ...容器.历史]) {
    if (订单.状态 in 统计) 统计[订单.状态] += 1;
  }
  容器.状态统计 = 统计;
}

function 移入历史(状态, 订单) {
  const 容器 = 状态.结构.玩家委托;
  const 索引 = 容器.待处理.findIndex((项) => 项.编号 === 订单.编号);
  if (索引 >= 0) 容器.待处理.splice(索引, 1);
  容器.历史.push({ ...订单 });
  if (容器.历史.length > 1200) 容器.历史.splice(0, 容器.历史.length - 1200);
}

function 释放剩余冻结资产(状态, 订单) {
  if (订单.冻结现金 > 0) {
    解冻玩家资产(状态, '现金', 订单.冻结现金);
    订单.冻结现金 = 0;
  }
  if (订单.冻结份额 > 0) {
    解冻玩家资产(状态, '份额', 订单.冻结份额);
    订单.冻结份额 = 0;
  }
}

function 完成订单(状态, 订单, 原因 = '全部成交') {
  释放剩余冻结资产(状态, 订单);
  订单.状态 = 原因;
  订单.完成时间 = 状态.系统.虚拟时间;
  订单.剩余数量 = Math.max(0, Number(订单.剩余数量 || 0));
  移入历史(状态, 订单);
}

function 读取订单中心成交(状态, 订单) {
  const 中心成交 = 读取中心订单簿(状态).本刻成交;
  return 中心成交.filter((成交) => 成交.买方订单 === 订单.编号 || 成交.卖方订单 === 订单.编号);
}

function 读取新增中心成交(订单, 中心成交) {
  const 已记录编号 = new Set(Array.isArray(订单.已记录中心成交编号) ? 订单.已记录中心成交编号 : []);
  const 新增成交 = 中心成交.filter((成交) => !已记录编号.has(成交.编号));
  if (新增成交.length) {
    订单.已记录中心成交编号 = [...已记录编号, ...新增成交.map((成交) => 成交.编号)].slice(-2400);
    订单.已记录中心成交数量 = Number(订单.已成交数量 || 0);
  }
  return 新增成交;
}

function 同步玩家订单结果(状态) {
  const 容器 = 状态.结构.玩家委托;
  const 订单结果 = [];
  for (const 订单 of [...容器.待处理]) {
    const 中心成交 = 读取订单中心成交(状态, 订单);
    const 新增成交 = 读取新增中心成交(订单, 中心成交);

    if (新增成交.length) {
      const 新增成交数量 = 新增成交.reduce((合计, 成交) => 合计 + 成交.数量, 0);
      const 新增成交金额 = 保留数值(新增成交.reduce((合计, 成交) => 合计 + 成交.金额, 0), 2);
      记录玩家委托日志(状态, {
        动作: 订单.剩余数量 > 0 ? '部分成交' : '全部成交',
        编号: 订单.编号,
        方向: 订单.方向,
        价格: 订单.价格,
        数量: 新增成交数量,
        成交价: 保留数值(新增成交金额 / Math.max(新增成交数量, 1), 6),
        成交金额: 新增成交金额,
        剩余数量: 订单.剩余数量,
        状态: 订单.状态,
        说明: `${订单.编号}已通过中心订单簿成交 ${新增成交数量}份，剩余 ${订单.剩余数量}份。`,
      });
    }

    if (订单.状态 === '全部成交' || 订单.状态 === '已拒绝') {
      const 原因 = 订单.状态;
      完成订单(状态, 订单, 原因);
    }

    const 成交数量 = Number(订单.已成交数量 || 0);
    const 成交金额 = 保留数值(Number(订单.成交金额 || 0), 2);
    订单结果.push({
      编号: 订单.编号,
      成交数量,
      成交金额,
      平均成交价: 成交数量 ? 保留数值(成交金额 / 成交数量, 6) : 0,
      剩余数量: 订单.剩余数量,
      状态: 订单.状态,
    });
  }
  刷新订单状态统计(状态);
  return 订单结果;
}

export function 提交玩家限价委托(状态, 方向, 请求数量, 请求价格) {
  初始化交易制度状态(状态);
  const 数量 = 取整数量(请求数量);
  const 校验 = 验证玩家二级市场订单(状态, 方向, 数量, Number(请求价格));
  if (!校验.有效) return Object.freeze({ 成功: false, 原因: 校验.原因 });

  const 买入 = 方向 === '买入';
  const 冻结金额 = 保留数值(校验.价格 * 数量, 2);
  if (买入 && 可用玩家现金(状态) + 0.0001 < 冻结金额) {
    return Object.freeze({ 成功: false, 原因: '可用现金不足，部分现金可能已被冻结在未成交买单中。' });
  }
  if (!买入 && 可用玩家持仓(状态) < 数量) {
    return Object.freeze({ 成功: false, 原因: '可卖持仓不足，部分份额可能已被冻结在未成交买单中。' });
  }

  const 容器 = 状态.结构.玩家委托;
  const 订单 = {
    编号: `玩家委托-${容器.下次编号}`,
    委托序号: 容器.下次编号,
    // 提交时即确定中心序号，保证同价时间优先不受后续做市/自动订单创建顺序影响。
    中心委托序号: 分配中心委托序号(状态),
    类型: '限价单',
    当前委托市场: '连续竞价',
    原委托市场: '连续竞价',
    方向,
    价格: 校验.价格,
    数量,
    已成交数量: 0,
    剩余数量: 数量,
    成交金额: 0,
    平均成交价: 0,
    已记录中心成交编号: [],
    状态: '待撮合',
    委托时间: 状态.系统.虚拟时间,
    交易日: 取北京时间日期键(状态.系统.虚拟时间),
    有效期: '当日有效',
    冻结现金: 买入 ? 冻结金额 : 0,
    冻结份额: 买入 ? 0 : 数量,
    上次成交前价格: 状态.变量.基金交易价格,
  };
  容器.下次编号 += 1;
  容器.待处理.push(订单);
  if (买入) 冻结玩家资产(状态, '现金', 冻结金额);
  else 冻结玩家资产(状态, '份额', 数量);

  记录玩家委托日志(状态, {
    动作: '提交', 编号: 订单.编号, 方向, 价格: 订单.价格, 数量, 剩余数量: 数量, 状态: 订单.状态,
    说明: `${方向}${数量}份中国黄金限价单已进入中心订单簿，委托价 ${订单.价格}。`,
  });

  const 撮合结果 = 撮合玩家限价委托(状态);
  const 最新订单 = 状态.结构.玩家委托.待处理.find((项) => 项.编号 === 订单.编号)
    || 状态.结构.玩家委托.历史.find((项) => 项.编号 === 订单.编号)
    || 订单;
  写入上一刻观测(状态);
  断言市场一致性(状态);
  断言交易制度一致性(状态);
  return Object.freeze({ 成功: true, 订单: { ...最新订单 }, 撮合结果 });
}

export function 撮合玩家限价委托(状态) {
  初始化交易制度状态(状态);
  const 容器 = 状态.结构.玩家委托;
  if (!状态.系统.是否可交易 || !容器.待处理.length) {
    刷新订单状态统计(状态);
    return Object.freeze({ 成交订单数: 0, 成交数量: 0, 成交金额: 0, 订单结果: [] });
  }

  const 中心结果 = 撮合中心玩家限价委托(状态);
  const 订单结果 = 同步玩家订单结果(状态);
  const 已成交订单 = 订单结果.filter((订单) => 订单.成交数量 > 0);
  return Object.freeze({
    成交订单数: 已成交订单.length,
    成交数量: 保留数值(中心结果.成交数量, 0),
    成交金额: 中心结果.成交金额,
    订单结果,
    中心新增成交: 中心结果.新增成交,
  });
}

export function 撤销玩家限价委托(状态, 编号) {
  初始化交易制度状态(状态);
  const 容器 = 状态.结构.玩家委托;
  const 订单 = 容器.待处理.find((项) => 项.编号 === 编号);
  if (!订单) return Object.freeze({ 成功: false, 原因: '未找到可撤销的待处理委托。' });

  从中心订单簿移除玩家订单(状态, 编号);
  const 原剩余数量 = 订单.剩余数量;
  完成订单(状态, 订单, '已撤销');
  记录玩家委托日志(状态, {
    动作: '撤销', 编号: 订单.编号, 方向: 订单.方向, 价格: 订单.价格, 数量: 原剩余数量,
    剩余数量: 0, 状态: '已撤销', 说明: `${订单.编号}已从中心订单簿撤销，冻结资产已释放。`,
  });
  刷新订单状态统计(状态);
  写入上一刻观测(状态);
  断言市场一致性(状态);
  断言交易制度一致性(状态);
  return Object.freeze({ 成功: true, 订单: { ...订单 } });
}

export function 使跨日玩家委托失效(状态, 当前日期键 = null) {
  初始化交易制度状态(状态);
  const 日期键 = 当前日期键 || 取北京时间日期键(状态.系统.虚拟时间);
  const 待失效 = [...状态.结构.玩家委托.待处理].filter((订单) => 订单.交易日 !== 日期键);
  for (const 订单 of 待失效) {
    从中心订单簿移除玩家订单(状态, 订单.编号);
    const 原剩余数量 = 订单.剩余数量;
    完成订单(状态, 订单, '已过期');
    记录玩家委托日志(状态, {
      动作: '过期', 编号: 订单.编号, 方向: 订单.方向, 价格: 订单.价格, 数量: 原剩余数量,
      剩余数量: 0, 状态: '已过期', 说明: `${订单.编号}因交易日结束从中心订单簿失效，冻结资产已释放。`,
    });
  }
  刷新订单状态统计(状态);
  写入上一刻观测(状态);
  断言交易制度一致性(状态);
  return Object.freeze({ 失效数量: 待失效.length, 编号列表: 待失效.map((订单) => 订单.编号) });
}

export function 读取玩家限价委托(状态) {
  初始化交易制度状态(状态);
  const 容器 = 状态.结构.玩家委托;
  return Object.freeze({
    待处理: 容器.待处理.map((订单) => ({ ...订单 })),
    历史: 容器.历史.slice(-100).map((订单) => ({ ...订单 })),
    状态统计: { ...容器.状态统计 },
  });
}
