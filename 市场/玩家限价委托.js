function 取整数量(数量) {
  return Math.max(0, Math.floor(Number(数量) || 0));
}

function 取北京时间日期键(时间) {
  const 日期 = new Date(new Date(时间).getTime() + 8 * 60 * 60 * 1000);
  return `${日期.getUTCFullYear()}-${String(日期.getUTCMonth() + 1).padStart(2, '0')}-${String(日期.getUTCDate()).padStart(2, '0')}`;
}

function 记录玩家委托日志(状态, 内容) {
  状态.历史.日志.push({
    时间: 状态.系统.虚拟时间,
    类型: '玩家委托',
    序号: 状态.系统.演算序号,
    ...内容,
  });
  if (状态.历史.日志.length > 状态.结构.日志上限) {
    状态.历史.日志.splice(0, 状态.历史.日志.length - 状态.结构.日志上限);
  }
}

function 刷新订单状态统计(状态) {
  const 统计 = {
    待撮合: 0,
    部分成交: 0,
    全部成交: 0,
    已撤销: 0,
    已拒绝: 0,
    已过期: 0,
  };
  const 容器 = 状态.结构.玩家委托;
  for (const 订单 of [...容器.待处理, ...容器.历史]) {
    if (订单.状态 in 统计) 统计[订单.状态] += 1;
  }
  容器.状态统计 = 统计;
}

function 移入历史(状态, 订单) {
  const 容器 = 状态.结构.玩家委托;
  const 索引 = 容器.待处理.findIndex((项) => 项.编号 === 订单.编号);
  if (索引 >= 0) 容器.待处理.splice(索引, 1);
  容器.历史.push({ ...订单 });
  if (容器.历史.length > 1200) 容器.历史.splice(0, 容器.历史.length - 1200);
}

function 按价格时间排序(订单列表, 方向) {
  return [...订单列表].sort((左, 右) => {
    if (左.价格 !== 右.价格) return 方向 === '买入' ? 右.价格 - 左.价格 : 左.价格 - 右.价格;
    return 左.委托序号 - 右.委托序号;
  });
}

function 可成交档位(状态, 方向, 限价) {
  const 档位列表 = 方向 === '买入' ? 状态.订单簿.卖方 : 状态.订单簿.买方;
  return 档位列表.filter((档位) => (方向 === '买入' ? 档位.价格 <= 限价 : 档位.价格 >= 限价));
}

function 吃限价单(状态, 订单) {
  const 档位列表 = 可成交档位(状态, 订单.方向, 订单.价格);
  let 剩余 = 订单.剩余数量;
  let 成交数量 = 0;
  let 成交金额 = 0;

  for (const 档位 of 档位列表) {
    if (剩余 <= 0) break;
    const 数量 = Math.min(剩余, Math.max(0, Number(档位.数量 || 0)));
    if (!数量) continue;
    档位.数量 -= 数量;
    剩余 -= 数量;
    成交数量 += 数量;
    成交金额 += 数量 * 档位.价格;
  }

  return {
    成交数量,
    成交金额: 保留数值(成交金额, 2),
    平均成交价: 成交数量 ? 保留数值(成交金额 / 成交数量, 6) : 0,
  };
}

function 更新玩家与做市商(状态, 订单, 成交) {
  const 变量 = 状态.变量;
  const 做市商 = 状态.交易者.做市商;
  const 买入 = 订单.方向 === '买入';
  const 原持仓 = 变量.玩家持仓;

  if (买入) {
    变量.玩家现金 = 保留数值(变量.玩家现金 - 成交.成交金额, 2);
    变量.玩家持仓 += 成交.成交数量;
    变量.玩家持仓成本 = 保留数值(
      (变量.玩家持仓成本 * 原持仓 + 成交.成交金额) / Math.max(变量.玩家持仓, 1),
      6,
    );
    做市商.现金 = 保留数值(做市商.现金 + 成交.成交金额, 2);
    做市商.持仓 -= 成交.成交数量;
    变量.主动买入量 += 成交.成交数量;
    const 释放冻结金额 = 保留数值(订单.价格 * 成交.成交数量, 2);
    订单.冻结现金 = 保留数值(Math.max(0, 订单.冻结现金 - 释放冻结金额), 2);
    解冻玩家资产(状态, '现金', 释放冻结金额);
  } else {
    变量.玩家现金 = 保留数值(变量.玩家现金 + 成交.成交金额, 2);
    变量.玩家持仓 -= 成交.成交数量;
    if (!变量.玩家持仓) 变量.玩家持仓成本 = 0;
    变量.玩家累计已实现盈亏 = 保留数值(
      Number(变量.玩家累计已实现盈亏 || 0) + (成交.平均成交价 - 变量.玩家持仓成本) * 成交.成交数量,
      2,
    );
    做市商.现金 = 保留数值(做市商.现金 - 成交.成交金额, 2);
    做市商.持仓 += 成交.成交数量;
    变量.主动卖出量 += 成交.成交数量;
    订单.冻结份额 = Math.max(0, 订单.冻结份额 - 成交.成交数量);
    解冻玩家资产(状态, '份额', 成交.成交数量);
  }

  变量.成交量 += 成交.成交数量;
  变量.成交额 = 保留数值(变量.成交额 + 成交.成交金额, 2);
  变量.成交笔数 += 1;
  变量.订单失衡 = 保留数值(
    (变量.主动买入量 - 变量.主动卖出量) / Math.max(变量.主动买入量 + 变量.主动卖出量, 1),
    8,
  );
  变量.成交价格冲击 = 保留数值(
    (买入 ? 1 : -1) * Math.min(0.08, 变量.价格冲击系数 * 成交.成交数量 / Math.max(1, 买入 ? 变量.卖方挂单深度 : 变量.买方挂单深度)),
    8,
  );
  变量.基金交易价格 = 限制在当日价格边界(状态, 成交.平均成交价);
  变量.基金交易价格收益 = 保留数值(
    Math.log(变量.基金交易价格 / Math.max(订单.上次成交前价格, Number.EPSILON)),
    8,
  );
  变量.基金交易溢价折价 = 保留数值(
    (变量.基金交易价格 - 变量.基金理论净值) / Math.max(变量.基金理论净值, Number.EPSILON),
    8,
  );
  变量.玩家持仓市值 = 保留数值(变量.玩家持仓 * 变量.基金交易价格, 2);
  变量.玩家浮动盈亏 = 保留数值((变量.基金交易价格 - 变量.玩家持仓成本) * 变量.玩家持仓, 2);
  变量.玩家总资产 = 保留数值(变量.玩家现金 + 变量.玩家持仓市值, 2);
  变量.做市库存 = 做市商.持仓 - 做市商.目标仓位;

  状态.订单簿.上次成交价 = 变量.基金交易价格;
  状态.订单簿.中间价 = 变量.基金交易价格;
  状态.变量.买方挂单深度 = 状态.订单簿.买方.reduce((合计, 档位) => 合计 + Math.max(0, Number(档位.数量 || 0)), 0);
  状态.变量.卖方挂单深度 = 状态.订单簿.卖方.reduce((合计, 档位) => 合计 + Math.max(0, Number(档位.数量 || 0)), 0);
  记录当日成交(状态, 变量.基金交易价格, 成交.成交数量, 成交.成交金额);
}

function 完成订单(状态, 订单, 原因 = '全部成交') {
  if (订单.方向 === '买入' && 订单.冻结现金 > 0) {
    解冻玩家资产(状态, '现金', 订单.冻结现金);
    订单.冻结现金 = 0;
  }
  if (订单.方向 === '卖出' && 订单.冻结份额 > 0) {
    解冻玩家资产(状态, '份额', 订单.冻结份额);
    订单.冻结份额 = 0;
  }
  订单.状态 = 原因 === '已撤销' ? '已撤销' : 原因 === '已过期' ? '已过期' : '全部成交';
  订单.完成时间 = 状态.系统.虚拟时间;
  移入历史(状态, 订单);
}

export function 提交玩家限价委托(状态, 方向, 请求数量, 请求价格) {
  初始化交易制度状态(状态);
  const 数量 = 取整数量(请求数量);
  const 价格 = Number(请求价格);
  const 校验 = 验证玩家二级市场订单(状态, 方向, 数量, 价格);
  if (!校验.有效) return Object.freeze({ 成功: false, 原因: 校验.原因 });

  const 买入 = 方向 === '买入';
  const 冻结金额 = 保留数值(校验.价格 * 数量, 2);
  if (买入 && 可用玩家现金(状态) + 0.0001 < 冻结金额) {
    return Object.freeze({ 成功: false, 原因: '可用现金不足，部分现金可能已被冻结在未成交买单中。' });
  }
  if (!买入 && 可用玩家持仓(状态) < 数量) {
    return Object.freeze({ 成功: false, 原因: '可卖持仓不足，部分份额可能已被冻结在未成交卖单中。' });
  }

  const 容器 = 状态.结构.玩家委托;
  const 订单 = {
    编号: `玩家委托-${容器.下次编号}`,
    委托序号: 容器.下次编号,
    类型: '限价单',
    方向,
    价格: 校验.价格,
    数量,
    已成交数量: 0,
    剩余数量: 数量,
    成交金额: 0,
    平均成交价: 0,
    状态: '待撮合',
    委托时间: 状态.系统.虚拟时间,
    交易日: 取北京时间日期键(状态.系统.虚拟时间),
    有效期: '当日有效',
    冻结现金: 买入 ? 冻结金额 : 0,
    冻结份额: 买入 ? 0 : 数量,
    上次成交前价格: 状态.变量.基金交易价格,
  };
  容器.下次编号 += 1;
  容器.待处理.push(订单);
  if (买入) 冻结玩家资产(状态, '现金', 冻结金额);
  else 冻结玩家资产(状态, '份额', 数量);

  记录玩家委托日志(状态, {
    动作: '提交',
    编号: 订单.编号,
    方向,
    价格: 订单.价格,
    数量,
    剩余数量: 数量,
    状态: 订单.状态,
    说明: `${方向}${数量}份中国黄金限价单已进入队列，委托价 ${订单.价格}。`,
  });

  const 撮合结果 = 撮合玩家限价委托(状态);
  刷新订单状态统计(状态);
  写入上一刻观测(状态);
  断言市场一致性(状态);
  断言交易制度一致性(状态);

  const 最新订单 = 状态.结构.玩家委托.待处理.find((项) => 项.编号 === 订单.编号)
    || 状态.结构.玩家委托.历史.find((项) => 项.编号 === 订单.编号)
    || 订单;

  return Object.freeze({
    成功: true,
    订单: { ...最新订单 },
    撮合结果,
  });
}

export function 撮合玩家限价委托(状态) {
  初始化交易制度状态(状态);
  const 容器 = 状态.结构.玩家委托;
  if (!状态.系统.是否可交易 || !容器.待处理.length) {
    刷新订单状态统计(状态);
    return Object.freeze({ 成交订单数: 0, 成交数量: 0, 成交金额: 0, 订单结果: [] });
  }

  const 订单结果 = [];
  const 方向顺序 = [
    ['买入', 按价格时间排序(容器.待处理.filter((订单) => 订单.方向 === '买入'), '买入')],
    ['卖出', 按价格时间排序(容器.待处理.filter((订单) => 订单.方向 === '卖出'), '卖出')],
  ];

  for (const [, 队列] of 方向顺序) {
    for (const 订单 of 队列) {
      if (!容器.待处理.some((项) => 项.编号 === 订单.编号)) continue;
      const 成交 = 吃限价单(状态, 订单);
      if (!成交.成交数量) continue;

      更新玩家与做市商(状态, 订单, 成交);
      订单.已成交数量 += 成交.成交数量;
      订单.剩余数量 -= 成交.成交数量;
      订单.成交金额 = 保留数值(订单.成交金额 + 成交.成交金额, 2);
      订单.平均成交价 = 保留数值(订单.成交金额 / Math.max(订单.已成交数量, 1), 6);
      订单.上次成交前价格 = 状态.变量.基金交易价格;

      if (订单.剩余数量 > 0) {
        订单.状态 = '部分成交';
      } else {
        完成订单(状态, 订单);
      }

      const 动作 = 订单.状态 === '全部成交' ? '全部成交' : '部分成交';
      记录玩家委托日志(状态, {
        动作,
        编号: 订单.编号,
        方向: 订单.方向,
        价格: 订单.价格,
        数量: 成交.成交数量,
        成交价: 成交.平均成交价,
        成交金额: 成交.成交金额,
        剩余数量: 订单.剩余数量,
        状态: 订单.状态,
        说明: `${订单.编号}${动作} ${成交.成交数量}份，成交均价 ${成交.平均成交价}，剩余 ${订单.剩余数量}份。`,
      });
      订单结果.push({
        编号: 订单.编号,
        成交数量: 成交.成交数量,
        成交金额: 成交.成交金额,
        平均成交价: 成交.平均成交价,
        剩余数量: 订单.剩余数量,
        状态: 订单.状态,
      });
    }
  }

  刷新订单状态统计(状态);
  return Object.freeze({
    成交订单数: 订单结果.length,
    成交数量: 订单结果.reduce((合计, 项) => 合计 + 项.成交数量, 0),
    成交金额: 保留数值(订单结果.reduce((合计, 项) => 合计 + 项.成交金额, 0), 2),
    订单结果,
  });
}

export function 撤销玩家限价委托(状态, 编号) {
  初始化交易制度状态(状态);
  const 容器 = 状态.结构.玩家委托;
  const 订单 = 容器.待处理.find((项) => 项.编号 === 编号);
  if (!订单) return Object.freeze({ 成功: false, 原因: '未找到可撤销的待处理委托。' });

  完成订单(状态, 订单, '已撤销');
  记录玩家委托日志(状态, {
    动作: '撤销',
    编号: 订单.编号,
    方向: 订单.方向,
    价格: 订单.价格,
    数量: 订单.剩余数量,
    剩余数量: 0,
    状态: '已撤销',
    说明: `${订单.编号}已撤销，冻结资产已释放。`,
  });
  刷新订单状态统计(状态);
  写入上一刻观测(状态);
  断言市场一致性(状态);
  断言交易制度一致性(状态);
  return Object.freeze({ 成功: true, 订单: { ...订单 } });
}

export function 使跨日玩家委托失效(状态, 当前日期键 = null) {
  初始化交易制度状态(状态);
  const 日期键 = 当前日期键 || 取北京时间日期键(状态.系统.虚拟时间);
  const 待失效 = 状态.结构.玩家委托.待处理.filter((订单) => 订单.交易日 !== 日期键);
  if (!待失效.length) return Object.freeze({ 失效数量: 0, 编号列表: [] });

  for (const 订单 of 待失效) {
    完成订单(状态, 订单, '已过期');
    记录玩家委托日志(状态, {
      动作: '过期',
      编号: 订单.编号,
      方向: 订单.方向,
      价格: 订单.价格,
      数量: 订单.剩余数量,
      剩余数量: 0,
      状态: '已过期',
      说明: `${订单.编号}因交易日结束自动失效，冻结资产已释放。`,
    });
  }

  刷新订单状态统计(状态);
  写入上一刻观测(状态);
  断言交易制度一致性(状态);
  return Object.freeze({ 失效数量: 待失效.length, 编号列表: 待失效.map((订单) => 订单.编号) });
}

export function 读取玩家限价委托(状态) {
  初始化交易制度状态(状态);
  const 容器 = 状态.结构.玩家委托;
  return Object.freeze({
    待处理: 容器.待处理.map((订单) => ({ ...订单 })),
    历史: 容器.历史.slice(-100).map((订单) => ({ ...订单 })),
    状态统计: { ...容器.状态统计 },
  });
}
