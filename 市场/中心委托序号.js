function 初始化中心序号状态(状态) {
  状态.结构 ??= {};
  状态.结构.中心订单簿 ??= {};
  const 中心 = 状态.结构.中心订单簿;
  中心.下次委托序号 ??= 1;
  return 中心;
}

/**
 * 在玩家限价单提交或由集合竞价转入连续竞价时分配序号。
 * 这样同价排序按真实进入连续簿的时间决定，不依赖后续渲染或撮合调用顺序。
 */
export function 分配中心委托序号(状态) {
  const 中心 = 初始化中心序号状态(状态);
  const 序号 = Number(中心.下次委托序号);
  中心.下次委托序号 = 序号 + 1;
  return 序号;
}

export function 确保连续玩家委托序号(状态) {
  const 待处理 = 状态.结构?.玩家委托?.待处理 || [];
  let 分配数量 = 0;
  for (const 订单 of 待处理) {
    if (订单.当前委托市场 !== '连续竞价' || !(订单.剩余数量 > 0) || 订单.中心委托序号) continue;
    订单.中心委托序号 = 分配中心委托序号(状态);
    分配数量 += 1;
  }
  return 分配数量;
}
