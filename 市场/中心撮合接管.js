import { 推进中国黄金市场 } from './演算器.js';
import { 保留数值, 限制数值 } from './状态工厂.js';
import { 执行自动中心订单簿 } from './中心订单簿.js';
import { 断言市场一致性 } from './一致性检查.js';

const 自动交易者标识 = Object.freeze([
  '价值套利',
  '趋势资金',
  '散户群体',
  '风险控制资金',
  '信息优势资金',
]);

function 保存最大调整(状态) {
  return Object.fromEntries(自动交易者标识.map((标识) => [标识, 状态.交易者[标识].最大单刻调整]));
}

function 暂停旧聚合成交(状态) {
  for (const 标识 of 自动交易者标识) {
    状态.交易者[标识].最大单刻调整 = 0;
  }
}

function 恢复最大调整(状态, 最大单刻调整) {
  for (const 标识 of 自动交易者标识) {
    状态.交易者[标识].最大单刻调整 = 最大单刻调整[标识];
  }
}

function 应让集合竞价优先(状态) {
  return 状态.系统.上一盘中阶段 === '集合竞价' && 状态.系统.盘中阶段 === '上午交易';
}

function 结束上一刻中心订单(状态) {
  const 中心 = 状态.结构?.中心订单簿;
  if (!中心?.本刻订单?.length) return;
  const 待过期 = 中心.本刻订单.filter((订单) => ['待撮合', '部分成交'].includes(订单.状态));
  for (const 订单 of 待过期) {
    订单.状态 = '已过期';
    订单.结束时间 = 状态.系统.虚拟时间;
    中心.订单历史.push({ ...订单 });
  }
  中心.本刻订单 = [];
  中心.本刻成交 = [];
  中心.盘口快照 = {
    买方: [],
    卖方: [],
    中间价: 状态.变量.基金交易价格,
    上次成交价: 状态.变量.基金交易价格,
    版本: Number(中心.版本 || 0) + 1,
  };
  中心.版本 = Number(中心.版本 || 0) + 1;
  if (中心.订单历史.length > 6000) {
    中心.订单历史.splice(0, 中心.订单历史.length - 6000);
  }
}

function 刷新中心盘口指标(状态) {
  const 快照 = 状态.结构.中心订单簿?.盘口快照;
  if (!快照) return;
  const 变量 = 状态.变量;
  const 买方深度 = (快照.买方 || []).reduce((合计, 档位) => 合计 + Number(档位.数量 || 0), 0);
  const 卖方深度 = (快照.卖方 || []).reduce((合计, 档位) => 合计 + Number(档位.数量 || 0), 0);
  if (买方深度 || 卖方深度) {
    变量.买方挂单深度 = 买方深度;
    变量.卖方挂单深度 = 卖方深度;
    状态.派生.开盘前流动性 = 限制数值((买方深度 + 卖方深度) / 160000, 0, 1);
    状态.派生.本地流动性状态 = 限制数值(
      (状态.派生.开盘前流动性 * 0.7 + 变量.做市报价意愿 * 0.3) * (1 - 变量.撤单率 * 0.35),
      0,
      1,
    );
    变量.订单簿脆弱度 = 限制数值(
      1 - 状态.派生.本地流动性状态 * 0.62 + 变量.撤单率 * 0.22 + 变量.买卖价差 * 18,
      0,
      1,
    );
  }
}

function 刷新中心撮合后的跨期状态(状态) {
  const 变量 = 状态.变量;
  刷新中心盘口指标(状态);
  状态.结构.最近收益.push(变量.基金交易价格收益);
  if (状态.结构.最近收益.length > 64) 状态.结构.最近收益.shift();

  const 总持仓 = Object.values(状态.交易者).reduce((合计, 参与者) => 合计 + Math.max(0, Number(参与者.持仓 || 0)), 0);
  const 加权体验 = 总持仓
    ? Object.values(状态.交易者).reduce((合计, 参与者) => {
      const 成本 = Number(参与者.平均成本 || 变量.基金交易价格);
      return 合计 + ((变量.基金交易价格 - 成本) / Math.max(成本, 0.001)) * Math.max(0, Number(参与者.持仓 || 0));
    }, 0) / 总持仓
    : 0;
  变量.最近盈利体验 = 限制数值(加权体验 * 8, -1, 1);
  变量.止损密度 = 限制数值(
    总持仓
      ? Object.values(状态.交易者).reduce((合计, 参与者) => {
        const 成本 = Number(参与者.平均成本 || 变量.基金交易价格);
        return 合计 + (成本 >= 变量.基金交易价格 && 成本 <= 变量.基金交易价格 * 1.035 ? Math.max(0, Number(参与者.持仓 || 0)) : 0);
      }, 0) / 总持仓
      : 0,
    0,
    1,
  );
  状态.派生.换手率 = 变量.成交量 / Math.max(状态.系统.守恒基线?.份额 || 1, 1);
  状态.派生.市场状态摘要 = {
    情绪: 保留数值(变量.国内市场情绪, 4),
    情绪不确定度: 保留数值(变量.情绪不确定度, 4),
    本地流动性: 保留数值(状态.派生.本地流动性状态, 4),
    订单簿脆弱度: 保留数值(变量.订单簿脆弱度, 4),
    波动率: 保留数值(变量.短期实现波动率, 6),
    溢价折价: 保留数值(变量.基金交易溢价折价, 6),
    中心撮合成交笔数: Number(状态.结构.中心订单簿?.本刻成交?.length || 0),
  };
}

export function 推进中心撮合市场(状态) {
  const 最大单刻调整 = 保存最大调整(状态);
  暂停旧聚合成交(状态);
  let 市场日志;
  try {
    市场日志 = 推进中国黄金市场(状态);
  } finally {
    恢复最大调整(状态, 最大单刻调整);
  }

  结束上一刻中心订单(状态);
  const 允许自动中心成交 = 状态.系统.是否可交易 && !应让集合竞价优先(状态);
  const 中心撮合 = 允许自动中心成交
    ? 执行自动中心订单簿(状态, 最大单刻调整, 市场日志)
    : { 本刻订单: [], 成交: [] };
  if (允许自动中心成交) 刷新中心撮合后的跨期状态(状态);

  市场日志.中心订单簿 = {
    已执行: 允许自动中心成交,
    订单数量: 中心撮合.本刻订单.length,
    成交笔数: 中心撮合.成交.length,
    成交量: 允许自动中心成交 ? 状态.变量.成交量 : 0,
    成交额: 允许自动中心成交 ? 状态.变量.成交额 : 0,
    最后成交价: 状态.变量.基金交易价格,
  };
  状态.系统.引擎状态 = '已完成';
  断言市场一致性(状态);
  return 市场日志;
}

export function 连续推进中心撮合市场(状态, 次数 = 1) {
  const 日志列表 = [];
  for (let 序号 = 0; 序号 < 次数; 序号 += 1) {
    日志列表.push(推进中心撮合市场(状态));
  }
  return 日志列表;
}
