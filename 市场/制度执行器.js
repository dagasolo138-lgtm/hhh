import {
  上交所黄金ETF交易制度,
  计算当日价格边界,
  是否处于集合竞价,
  是否集合竞价允许撤单,
  是否处于连续竞价,
  验证二级市场数量,
} from './交易制度参数.js';

function 保留(数值, 位数 = 6) {
  const 倍数 = 10 ** 位数;
  return Math.round(Number(数值 || 0) * 倍数) / 倍数;
}

function 取北京时间日期键(时间) {
  const 日期 = new Date(new Date(时间).getTime() + 8 * 60 * 60 * 1000);
  return `${日期.getUTCFullYear()}-${String(日期.getUTCMonth() + 1).padStart(2, '0')}-${String(日期.getUTCDate()).padStart(2, '0')}`;
}

function 取北京时分(时间) {
  const 日期 = new Date(new Date(时间).getTime() + 8 * 60 * 60 * 1000);
  return `${String(日期.getUTCHours()).padStart(2, '0')}:${String(日期.getUTCMinutes()).padStart(2, '0')}`;
}

function 对齐价格最小变动(价格) {
  const 最小变动 = 上交所黄金ETF交易制度.二级市场.价格最小变动;
  return 保留(Math.round(Number(价格) / 最小变动) * 最小变动, 3);
}

function 创建玩家委托容器() {
  return {
    下次编号: 1,
    待处理: [],
    历史: [],
    状态统计: {
      待撮合: 0,
      部分成交: 0,
      全部成交: 0,
      已撤销: 0,
      已拒绝: 0,
      已过期: 0,
    },
  };
}

function 创建集合竞价容器(日期键) {
  return {
    当前交易日: 日期键,
    待处理: [],
    历史: [],
    最近结果: null,
    最近开盘价: null,
    最近成交量: 0,
    最近成交额: 0,
  };
}

export function 初始化交易制度状态(状态) {
  状态.结构 ??= {};
  const 价格 = Math.max(0.001, Number(状态.变量?.基金交易价格 || 状态.变量?.基金理论净值 || 1));
  const 日期键 = 取北京时间日期键(状态.系统?.虚拟时间 || new Date().toISOString());

  状态.结构.交易制度 ??= {};
  const 制度 = 状态.结构.交易制度;
  制度.制度版本 = 上交所黄金ETF交易制度.制度版本;
  制度.当前交易日 ??= 日期键;
  制度.上一交易日收盘价 ??= 对齐价格最小变动(价格);

  const 边界 = 计算当日价格边界(制度.上一交易日收盘价);
  制度.当日涨停价 = 对齐价格最小变动(边界.涨停价);
  制度.当日跌停价 = 对齐价格最小变动(边界.跌停价);
  制度.当日开盘价 ??= 对齐价格最小变动(价格);
  制度.当日最高价 ??= 对齐价格最小变动(价格);
  制度.当日最低价 ??= 对齐价格最小变动(价格);
  制度.当日收盘候选价 ??= 对齐价格最小变动(价格);
  制度.当日成交量 ??= 0;
  制度.当日成交额 ??= 0;
  制度.当前时分 = 取北京时分(状态.系统?.虚拟时间 || new Date().toISOString());
  制度.集合竞价中 = 是否处于集合竞价(制度.当前时分);
  制度.集合竞价可撤单 = 是否集合竞价允许撤单(制度.当前时分);
  制度.连续竞价中 = 是否处于连续竞价(制度.当前时分);

  状态.结构.玩家冻结资产 ??= { 现金: 0, 份额: 0 };
  状态.结构.玩家委托 ??= 创建玩家委托容器();
  状态.结构.玩家委托.待处理 ??= [];
  状态.结构.玩家委托.历史 ??= [];
  状态.结构.玩家委托.状态统计 ??= 创建玩家委托容器().状态统计;
  状态.结构.玩家委托.下次编号 ??= 1;

  状态.结构.集合竞价 ??= 创建集合竞价容器(日期键);
  状态.结构.集合竞价.当前交易日 ??= 日期键;
  状态.结构.集合竞价.待处理 ??= [];
  状态.结构.集合竞价.历史 ??= [];
  状态.结构.集合竞价.最近结果 ??= null;
  状态.结构.集合竞价.最近开盘价 ??= null;
  状态.结构.集合竞价.最近成交量 ??= 0;
  状态.结构.集合竞价.最近成交额 ??= 0;

  return 制度;
}

export function 刷新交易制度时钟(状态, 原日期键 = null) {
  const 制度 = 初始化交易制度状态(状态);
  const 当前日期键 = 取北京时间日期键(状态.系统.虚拟时间);
  const 日期发生变化 = 原日期键 && 当前日期键 !== 原日期键;

  if (日期发生变化) {
    const 前收盘 = Math.max(0.001, Number(制度.当日收盘候选价 || 状态.变量.基金交易价格));
    制度.上一交易日收盘价 = 对齐价格最小变动(前收盘);
    const 边界 = 计算当日价格边界(制度.上一交易日收盘价);
    制度.当日涨停价 = 对齐价格最小变动(边界.涨停价);
    制度.当日跌停价 = 对齐价格最小变动(边界.跌停价);
    制度.当前交易日 = 当前日期键;
    制度.当日开盘价 = 限制在当日价格边界(状态, 状态.变量.基金交易价格);
    制度.当日最高价 = 制度.当日开盘价;
    制度.当日最低价 = 制度.当日开盘价;
    制度.当日收盘候选价 = 制度.当日开盘价;
    制度.当日成交量 = 0;
    制度.当日成交额 = 0;
    状态.结构.集合竞价 = 创建集合竞价容器(当前日期键);
  }

  制度.当前时分 = 取北京时分(状态.系统.虚拟时间);
  制度.集合竞价中 = 是否处于集合竞价(制度.当前时分);
  制度.集合竞价可撤单 = 是否集合竞价允许撤单(制度.当前时分);
  制度.连续竞价中 = 是否处于连续竞价(制度.当前时分);
  return 制度;
}

export function 读取当日价格边界(状态) {
  const 制度 = 初始化交易制度状态(状态);
  return Object.freeze({
    前收盘价: 制度.上一交易日收盘价,
    涨停价: 制度.当日涨停价,
    跌停价: 制度.当日跌停价,
  });
}

export function 读取竞价时段状态(状态) {
  const 制度 = 初始化交易制度状态(状态);
  return Object.freeze({
    当前时分: 制度.当前时分,
    集合竞价中: 制度.集合竞价中,
    集合竞价可撤单: 制度.集合竞价可撤单,
    连续竞价中: 制度.连续竞价中,
  });
}

export function 限制在当日价格边界(状态, 价格) {
  const 制度 = 初始化交易制度状态(状态);
  const 对齐后价格 = 对齐价格最小变动(价格);
  return 对齐价格最小变动(Math.max(制度.当日跌停价, Math.min(制度.当日涨停价, 对齐后价格)));
}

export function 验证价格符合当日边界(状态, 价格) {
  const 数值 = Number(价格);
  if (!Number.isFinite(数值) || 数值 <= 0) {
    return Object.freeze({ 有效: false, 原因: '委托价格必须是大于零的有限数值。' });
  }

  const 制度 = 初始化交易制度状态(状态);
  const 对齐后价格 = 对齐价格最小变动(数值);
  if (Math.abs(对齐后价格 - 数值) > 0.0000001) {
    return Object.freeze({ 有效: false, 原因: `委托价格必须为 ${上交所黄金ETF交易制度.二级市场.价格最小变动} 元的整数倍。` });
  }
  if (对齐后价格 < 制度.当日跌停价 || 对齐后价格 > 制度.当日涨停价) {
    return Object.freeze({ 有效: false, 原因: `委托价格必须处于当日 ${制度.当日跌停价} 元至 ${制度.当日涨停价} 元之间。` });
  }

  return Object.freeze({ 有效: true, 原因: '', 价格: 对齐后价格 });
}

export function 验证玩家二级市场订单(状态, 方向, 数量, 价格 = null, 选项 = {}) {
  if (!['买入', '卖出'].includes(方向)) {
    return Object.freeze({ 有效: false, 原因: '交易方向无效。' });
  }

  const 类型 = 选项.订单类型 || (价格 === null || 价格 === undefined ? '市价' : '限价');
  const 制度 = 初始化交易制度状态(状态);
  const 允许集合竞价 = 类型 === '限价' && 制度.集合竞价中;
  const 允许连续竞价 = 制度.连续竞价中;
  if (!允许连续竞价 && !允许集合竞价) {
    return Object.freeze({ 有效: false, 原因: '当前不处于允许提交该类委托的竞价时段。' });
  }
  if (类型 === '市价' && !允许连续竞价) {
    return Object.freeze({ 有效: false, 原因: '市价单只允许在连续竞价时段即时成交。' });
  }

  const 数量校验 = 验证二级市场数量(方向, 数量);
  if (!数量校验.有效) return 数量校验;

  if (价格 !== null && 价格 !== undefined) {
    const 价格校验 = 验证价格符合当日边界(状态, 价格);
    if (!价格校验.有效) return 价格校验;
  }

  return Object.freeze({ 有效: true, 原因: '', 委托市场: 允许集合竞价 && !允许连续竞价 ? '集合竞价' : '连续竞价' });
}

export function 是否允许撤销玩家委托(状态, 委托市场 = '连续竞价') {
  const 制度 = 初始化交易制度状态(状态);
  if (委托市场 === '集合竞价') {
    if (制度.集合竞价中 && 制度.集合竞价可撤单) return Object.freeze({ 有效: true, 原因: '' });
    return Object.freeze({ 有效: false, 原因: '集合竞价撤单窗口已关闭，09:20 至 09:25 的集合竞价委托不可撤销。' });
  }
  if (制度.连续竞价中) return Object.freeze({ 有效: true, 原因: '' });
  return Object.freeze({ 有效: false, 原因: '当前不处于可撤单的连续竞价时段。' });
}

export function 记录当日成交(状态, 成交价, 成交数量, 成交金额) {
  const 制度 = 初始化交易制度状态(状态);
  const 价格 = 限制在当日价格边界(状态, 成交价);
  制度.当日开盘价 ??= 价格;
  制度.当日最高价 = Math.max(制度.当日最高价, 价格);
  制度.当日最低价 = Math.min(制度.当日最低价, 价格);
  制度.当日收盘候选价 = 价格;
  制度.当日成交量 += Math.max(0, Number(成交数量 || 0));
  制度.当日成交额 = 保留(制度.当日成交额 + Math.max(0, Number(成交金额 || 0)), 2);
  return 制度;
}

export function 冻结玩家资产(状态, 类型, 数量) {
  初始化交易制度状态(状态);
  const 冻结 = 状态.结构.玩家冻结资产;
  const 数值 = Math.max(0, Number(数量 || 0));
  if (类型 === '现金') 冻结.现金 = 保留(冻结.现金 + 数值, 2);
  if (类型 === '份额') 冻结.份额 += Math.floor(数值);
  return 冻结;
}

export function 解冻玩家资产(状态, 类型, 数量) {
  初始化交易制度状态(状态);
  const 冻结 = 状态.结构.玩家冻结资产;
  const 数值 = Math.max(0, Number(数量 || 0));
  if (类型 === '现金') 冻结.现金 = 保留(Math.max(0, 冻结.现金 - 数值), 2);
  if (类型 === '份额') 冻结.份额 = Math.max(0, 冻结.份额 - Math.floor(数值));
  return 冻结;
}

export function 可用玩家现金(状态) {
  初始化交易制度状态(状态);
  return Math.max(0, Number(状态.变量.玩家现金 || 0) - Number(状态.结构.玩家冻结资产.现金 || 0));
}

export function 可用玩家持仓(状态) {
  初始化交易制度状态(状态);
  return Math.max(0, Number(状态.变量.玩家持仓 || 0) - Number(状态.结构.玩家冻结资产.份额 || 0));
}

export function 读取玩家委托状态(状态) {
  初始化交易制度状态(状态);
  return 状态.结构.玩家委托;
}
