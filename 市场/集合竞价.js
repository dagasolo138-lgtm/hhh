function 取整数量(数量) {
  return Math.max(0, Math.floor(Number(数量) || 0));
}

function 取北京时间日期键(时间) {
  const 日期 = new Date(new Date(时间).getTime() + 8 * 60 * 60 * 1000);
  return `${日期.getUTCFullYear()}-${String(日期.getUTCMonth() + 1).padStart(2, '0')}-${String(日期.getUTCDate()).padStart(2, '0')}`;
}

function 价格取整(价格) {
  const 最小变动 = 上交所黄金ETF交易制度.二级市场.价格最小变动;
  return 保留数值(Math.round(Number(价格) / 最小变动) * 最小变动, 3);
}

function 写入日志(状态, 内容) {
  状态.历史.日志.push({
    时间: 状态.系统.虚拟时间,
    类型: '玩家委托',
    序号: 状态.系统.演算序号,
    ...内容,
  });
  if (状态.历史.日志.length > 状态.结构.日志上限) {
    状态.历史.日志.splice(0, 状态.历史.日志.length - 状态.结构.日志上限);
  }
}

function 读取集合容器(状态) {
  初始化交易制度状态(状态);
  return 状态.结构.集合竞价;
}

function 从集合待处理移除(状态, 订单) {
  const 容器 = 读取集合容器(状态);
  const 索引 = 容器.待处理.findIndex((项) => 项.编号 === 订单.编号);
  if (索引 >= 0) 容器.待处理.splice(索引, 1);
}

function 转入集合历史(状态, 订单) {
  const 容器 = 读取集合容器(状态);
  从集合待处理移除(状态, 订单);
  容器.历史.push({ ...订单 });
  if (容器.历史.length > 1200) 容器.历史.splice(0, 容器.历史.length - 1200);
}

function 转入连续竞价队列(状态, 订单) {
  从集合待处理移除(状态, 订单);
  订单.原委托市场 = '集合竞价';
  订单.当前委托市场 = '连续竞价';
  订单.转入连续竞价时间 = 状态.系统.虚拟时间;
  if (订单.剩余数量 > 0) {
    订单.状态 = 订单.已成交数量 > 0 ? '部分成交' : '待撮合';
    状态.结构.玩家委托.待处理.push(订单);
  }
}

function 完成集合订单(状态, 订单, 原因 = '全部成交') {
  if (订单.方向 === '买入' && 订单.冻结现金 > 0) {
    解冻玩家资产(状态, '现金', 订单.冻结现金);
    订单.冻结现金 = 0;
  }
  if (订单.方向 === '卖出' && 订单.冻结份额 > 0) {
    解冻玩家资产(状态, '份额', 订单.冻结份额);
    订单.冻结份额 = 0;
  }
  订单.状态 = 原因;
  订单.完成时间 = 状态.系统.虚拟时间;
  转入集合历史(状态, 订单);
}

function 按价格时间排序(订单列表, 方向) {
  return [...订单列表].sort((左, 右) => {
    if (左.价格 !== 右.价格) return 方向 === '买入' ? 右.价格 - 左.价格 : 左.价格 - 右.价格;
    return 左.委托序号 - 右.委托序号;
  });
}

function 读取背景订单(状态) {
  const 买单 = (状态.订单簿?.买方 || []).map((档位, 索引) => ({
    来源: '做市背景',
    方向: '买入',
    价格: Number(档位.价格),
    数量: Math.max(0, Number(档位.数量 || 0)),
    序号: 100000 + 索引,
    档位引用: 档位,
  }));
  const 卖单 = (状态.订单簿?.卖方 || []).map((档位, 索引) => ({
    来源: '做市背景',
    方向: '卖出',
    价格: Number(档位.价格),
    数量: Math.max(0, Number(档位.数量 || 0)),
    序号: 200000 + 索引,
    档位引用: 档位,
  }));
  return { 买单, 卖单 };
}

function 选择开盘价(状态, 玩家订单, 背景订单) {
  const 全部订单 = [
    ...玩家订单.map((订单) => ({ ...订单, 来源: '玩家' })),
    ...背景订单.买单,
    ...背景订单.卖单,
  ].filter((订单) => 订单.数量 > 0);
  const 候选价格 = [...new Set(全部订单.map((订单) => 价格取整(订单.价格)))].sort((左, 右) => 左 - 右);
  const 前收盘价 = Number(状态.结构.交易制度.上一交易日收盘价);

  if (!候选价格.length) return null;

  const 结果 = 候选价格.map((价格) => {
    const 买入量 = 全部订单
      .filter((订单) => 订单.方向 === '买入' && 订单.价格 >= 价格)
      .reduce((合计, 订单) => 合计 + 订单.数量, 0);
    const 卖出量 = 全部订单
      .filter((订单) => 订单.方向 === '卖出' && 订单.价格 <= 价格)
      .reduce((合计, 订单) => 合计 + 订单.数量, 0);
    return {
      价格,
      可成交量: Math.min(买入量, 卖出量),
      未成交量: Math.abs(买入量 - 卖出量),
      与前收盘偏离: Math.abs(价格 - 前收盘价),
      买入量,
      卖出量,
    };
  });

  结果.sort((左, 右) => {
    if (右.可成交量 !== 左.可成交量) return 右.可成交量 - 左.可成交量;
    if (左.未成交量 !== 右.未成交量) return 左.未成交量 - 右.未成交量;
    if (左.与前收盘偏离 !== 右.与前收盘偏离) return 左.与前收盘偏离 - 右.与前收盘偏离;
    return 左.价格 - 右.价格;
  });

  return 结果[0];
}

function 可用背景对手量(背景订单, 方向, 开盘价) {
  const 条件 = 方向 === '买入'
    ? (订单) => 订单.方向 === '卖出' && 订单.价格 <= 开盘价
    : (订单) => 订单.方向 === '买入' && 订单.价格 >= 开盘价;
  return 背景订单.filter(条件).reduce((合计, 订单) => 合计 + 订单.数量, 0);
}

function 消耗背景对手(背景订单, 方向, 开盘价, 请求数量) {
  const 对手方向 = 方向 === '买入' ? '卖出' : '买入';
  const 候选 = 背景订单
    .filter((订单) => 订单.方向 === 对手方向 && (方向 === '买入' ? 订单.价格 <= 开盘价 : 订单.价格 >= 开盘价))
    .sort((左, 右) => (方向 === '买入' ? 左.价格 - 右.价格 : 右.价格 - 左.价格));
  let 剩余 = 请求数量;
  for (const 订单 of 候选) {
    if (剩余 <= 0) break;
    const 使用 = Math.min(剩余, 订单.数量);
    订单.数量 -= 使用;
    if (订单.档位引用) 订单.档位引用.数量 = Math.max(0, Number(订单.档位引用.数量 || 0) - 使用);
    剩余 -= 使用;
  }
  return 请求数量 - 剩余;
}

function 更新玩家成交(状态, 订单, 开盘价, 成交数量) {
  if (!成交数量) return 0;
  const 变量 = 状态.变量;
  const 做市商 = 状态.交易者.做市商;
  const 成交金额 = 保留数值(开盘价 * 成交数量, 2);
  const 原持仓 = 变量.玩家持仓;
  const 原成本 = 变量.玩家持仓成本;

  if (订单.方向 === '买入') {
    变量.玩家现金 = 保留数值(变量.玩家现金 - 成交金额, 2);
    变量.玩家持仓 += 成交数量;
    变量.玩家持仓成本 = 保留数值(
      (原成本 * 原持仓 + 成交金额) / Math.max(变量.玩家持仓, 1),
      6,
    );
    做市商.现金 = 保留数值(做市商.现金 + 成交金额, 2);
    做市商.持仓 -= 成交数量;
    变量.主动买入量 += 成交数量;
    const 释放金额 = 保留数值(订单.价格 * 成交数量, 2);
    订单.冻结现金 = 保留数值(Math.max(0, 订单.冻结现金 - 释放金额), 2);
    解冻玩家资产(状态, '现金', 释放金额);
  } else {
    变量.玩家现金 = 保留数值(变量.玩家现金 + 成交金额, 2);
    变量.玩家持仓 -= 成交数量;
    const 已实现 = (开盘价 - 原成本) * 成交数量;
    变量.玩家累计已实现盈亏 = 保留数值(Number(变量.玩家累计已实现盈亏 || 0) + 已实现, 2);
    if (!变量.玩家持仓) 变量.玩家持仓成本 = 0;
    做市商.现金 = 保留数值(做市商.现金 - 成交金额, 2);
    做市商.持仓 += 成交数量;
    变量.主动卖出量 += 成交数量;
    订单.冻结份额 = Math.max(0, Number(订单.冻结份额 || 0) - 成交数量);
    解冻玩家资产(状态, '份额', 成交数量);
  }

  订单.已成交数量 += 成交数量;
  订单.剩余数量 -= 成交数量;
  订单.成交金额 = 保留数值(订单.成交金额 + 成交金额, 2);
  订单.平均成交价 = 保留数值(订单.成交金额 / Math.max(订单.已成交数量, 1), 6);
  变量.成交量 += 成交数量;
  变量.成交额 = 保留数值(变量.成交额 + 成交金额, 2);
  变量.成交笔数 += 1;
  记录当日成交(状态, 开盘价, 成交数量, 成交金额);
  return 成交金额;
}

function 更新开盘价格与盘口(状态, 开盘价, 成交量, 成交额) {
  const 变量 = 状态.变量;
  const 原价格 = 变量.基金交易价格;
  const 价格 = 限制在当日价格边界(状态, 开盘价);
  变量.基金交易价格 = 价格;
  变量.基金交易价格收益 = 保留数值(Math.log(价格 / Math.max(原价格, Number.EPSILON)), 8);
  变量.基金交易溢价折价 = 保留数值(
    (价格 - 变量.基金理论净值) / Math.max(变量.基金理论净值, Number.EPSILON),
    8,
  );
  变量.订单失衡 = 保留数值(
    (变量.主动买入量 - 变量.主动卖出量) / Math.max(变量.主动买入量 + 变量.主动卖出量, 1),
    8,
  );
  变量.成交价格冲击 = 保留数值((价格 - 原价格) / Math.max(原价格, Number.EPSILON), 8);
  变量.玩家持仓市值 = 保留数值(变量.玩家持仓 * 价格, 2);
  变量.玩家浮动盈亏 = 保留数值((价格 - 变量.玩家持仓成本) * 变量.玩家持仓, 2);
  变量.玩家总资产 = 保留数值(变量.玩家现金 + 变量.玩家持仓市值, 2);
  变量.做市库存 = 状态.交易者.做市商.持仓 - 状态.交易者.做市商.目标仓位;
  状态.订单簿.中间价 = 价格;
  状态.订单簿.上次成交价 = 价格;
  变量.买方挂单深度 = 状态.订单簿.买方.reduce((合计, 档位) => 合计 + Math.max(0, Number(档位.数量 || 0)), 0);
  变量.卖方挂单深度 = 状态.订单簿.卖方.reduce((合计, 档位) => 合计 + Math.max(0, Number(档位.数量 || 0)), 0);

  const 制度 = 状态.结构.交易制度;
  制度.当日开盘价 = 价格;
  制度.当日最高价 = Math.max(制度.当日最高价, 价格);
  制度.当日最低价 = Math.min(制度.当日最低价, 价格);
  制度.当日收盘候选价 = 价格;

  const 最新价格记录 = 状态.历史.价格序列.at(-1);
  if (最新价格记录) {
    最新价格记录.开盘 = 价格;
    最新价格记录.最高 = Math.max(最新价格记录.最高, 价格);
    最新价格记录.最低 = Math.min(最新价格记录.最低, 价格);
    最新价格记录.收盘 = 价格;
    最新价格记录.成交量 += 成交量;
    最新价格记录.成交额 = 保留数值(最新价格记录.成交额 + 成交额, 2);
    最新价格记录.溢价折价 = 变量.基金交易溢价折价;
  }
}

export function 提交玩家集合竞价委托(状态, 方向, 请求数量, 请求价格) {
  初始化交易制度状态(状态);
  const 数量 = 取整数量(请求数量);
  const 价格 = Number(请求价格);
  const 校验 = 验证玩家二级市场订单(状态, 方向, 数量, 价格, { 订单类型: '限价' });
  if (!校验.有效 || 校验.委托市场 !== '集合竞价') {
    return Object.freeze({ 成功: false, 原因: 校验.原因 || '当前不处于集合竞价申报时段。' });
  }

  const 买入 = 方向 === '买入';
  const 冻结金额 = 保留数值(校验.价格 * 数量, 2);
  if (买入 && 可用玩家现金(状态) + 0.0001 < 冻结金额) {
    return Object.freeze({ 成功: false, 原因: '可用现金不足，部分现金可能已被冻结在其他委托中。' });
  }
  if (!买入 && 可用玩家持仓(状态) < 数量) {
    return Object.freeze({ 成功: false, 原因: '可卖持仓不足，部分份额可能已被冻结在其他委托中。' });
  }

  const 容器 = 读取集合容器(状态);
  const 编号 = 状态.结构.玩家委托.下次编号;
  状态.结构.玩家委托.下次编号 += 1;
  const 订单 = {
    编号: `玩家委托-${编号}`,
    委托序号: 编号,
    类型: '限价单',
    原委托市场: '集合竞价',
    当前委托市场: '集合竞价',
    方向,
    价格: 校验.价格,
    数量,
    已成交数量: 0,
    剩余数量: 数量,
    成交金额: 0,
    平均成交价: 0,
    状态: '待撮合',
    委托时间: 状态.系统.虚拟时间,
    交易日: 取北京时间日期键(状态.系统.虚拟时间),
    有效期: '当日有效',
    冻结现金: 买入 ? 冻结金额 : 0,
    冻结份额: 买入 ? 0 : 数量,
    上次成交前价格: 状态.变量.基金交易价格,
  };

  容器.待处理.push(订单);
  if (买入) 冻结玩家资产(状态, '现金', 冻结金额);
  else 冻结玩家资产(状态, '份额', 数量);

  写入日志(状态, {
    动作: '集合竞价提交',
    编号: 订单.编号,
    方向,
    价格: 订单.价格,
    数量,
    剩余数量: 数量,
    状态: 订单.状态,
    委托市场: '集合竞价',
    说明: `${方向}${数量}份中国黄金集合竞价限价单已提交，委托价 ${订单.价格}，将在 09:30 统一撮合。`,
  });
  写入上一刻观测(状态);
  断言交易制度一致性(状态);
  return Object.freeze({ 成功: true, 订单: { ...订单 }, 撮合结果: null });
}

export function 撤销玩家集合竞价委托(状态, 编号) {
  初始化交易制度状态(状态);
  const 容器 = 读取集合容器(状态);
  const 订单 = 容器.待处理.find((项) => 项.编号 === 编号);
  if (!订单) return Object.freeze({ 成功: false, 原因: '未找到可撤销的集合竞价委托。' });

  const 撤单校验 = 是否允许撤销玩家委托(状态, '集合竞价');
  if (!撤单校验.有效) return Object.freeze({ 成功: false, 原因: 撤单校验.原因 });

  const 原剩余 = 订单.剩余数量;
  完成集合订单(状态, 订单, '已撤销');
  写入日志(状态, {
    动作: '集合竞价撤销',
    编号: 订单.编号,
    方向: 订单.方向,
    价格: 订单.价格,
    数量: 原剩余,
    剩余数量: 0,
    状态: '已撤销',
    委托市场: '集合竞价',
    说明: `${订单.编号}已在集合竞价撤单窗口内撤销，冻结资产已释放。`,
  });
  写入上一刻观测(状态);
  断言交易制度一致性(状态);
  return Object.freeze({ 成功: true, 订单: { ...订单 } });
}

export function 执行开盘集合竞价(状态) {
  初始化交易制度状态(状态);
  const 容器 = 读取集合容器(状态);
  const 正在切换开盘 = 状态.系统.上一盘中阶段 === '集合竞价' && 状态.系统.盘中阶段 === '上午交易';
  if (!正在切换开盘 || 容器.当前交易日 === `已结算-${状态.结构.交易制度.当前交易日}`) {
    return Object.freeze({ 已执行: false, 原因: '当前不是集合竞价开盘撮合时点。', 成交数量: 0, 成交金额: 0 });
  }

  const 玩家订单 = [...容器.待处理];
  const 背景订单 = 读取背景订单(状态);
  const 价格结果 = 选择开盘价(状态, 玩家订单, 背景订单);
  if (!价格结果) {
    容器.当前交易日 = `已结算-${状态.结构.交易制度.当前交易日}`;
    return Object.freeze({ 已执行: true, 原因: '集合竞价无可用订单。', 成交数量: 0, 成交金额: 0 });
  }

  const 开盘价 = 限制在当日价格边界(状态, 价格结果.价格);
  let 总成交数量 = 0;
  let 总成交金额 = 0;
  const 订单结果 = [];

  for (const [方向, 队列] of [
    ['买入', 按价格时间排序(玩家订单.filter((订单) => 订单.方向 === '买入' && 订单.价格 >= 开盘价), '买入')],
    ['卖出', 按价格时间排序(玩家订单.filter((订单) => 订单.方向 === '卖出' && 订单.价格 <= 开盘价), '卖出')],
  ]) {
    for (const 订单 of 队列) {
      if (!容器.待处理.some((项) => 项.编号 === 订单.编号)) continue;
      const 可用对手量 = 可用背景对手量([...背景订单.买单, ...背景订单.卖单], 方向, 开盘价);
      const 成交数量 = Math.min(订单.剩余数量, 可用对手量);
      if (!成交数量) continue;
      const 实际成交量 = 消耗背景对手([...背景订单.买单, ...背景订单.卖单], 方向, 开盘价, 成交数量);
      if (!实际成交量) continue;
      const 成交金额 = 更新玩家成交(状态, 订单, 开盘价, 实际成交量);
      总成交数量 += 实际成交量;
      总成交金额 += 成交金额;

      if (订单.剩余数量 === 0) {
        完成集合订单(状态, 订单, '全部成交');
      } else {
        转入连续竞价队列(状态, 订单);
      }
      写入日志(状态, {
        动作: '集合竞价成交',
        编号: 订单.编号,
        方向: 订单.方向,
        价格: 订单.价格,
        数量: 实际成交量,
        成交价: 开盘价,
        成交金额,
        剩余数量: 订单.剩余数量,
        状态: 订单.状态,
        委托市场: '集合竞价',
        说明: `${订单.编号}在集合竞价以统一开盘价 ${开盘价} 成交 ${实际成交量}份，剩余 ${订单.剩余数量}份。`,
      });
      订单结果.push({ 编号: 订单.编号, 成交数量: 实际成交量, 成交金额, 剩余数量: 订单.剩余数量, 状态: 订单.状态 });
    }
  }

  for (const 订单 of [...容器.待处理]) {
    转入连续竞价队列(状态, 订单);
    写入日志(状态, {
      动作: '转入连续竞价',
      编号: 订单.编号,
      方向: 订单.方向,
      价格: 订单.价格,
      数量: 0,
      剩余数量: 订单.剩余数量,
      状态: 订单.状态,
      委托市场: '连续竞价',
      说明: `${订单.编号}未在集合竞价完全成交，已转入连续竞价队列。`,
    });
  }

  更新开盘价格与盘口(状态, 开盘价, 总成交数量, 总成交金额);
  容器.最近开盘价 = 开盘价;
  容器.最近成交量 = 总成交数量;
  容器.最近成交额 = 保留数值(总成交金额, 2);
  容器.最近结果 = {
    时间: 状态.系统.虚拟时间,
    开盘价,
    候选成交量: 价格结果.可成交量,
    玩家成交量: 总成交数量,
    玩家成交额: 保留数值(总成交金额, 2),
    未成交转入连续竞价: 状态.结构.玩家委托.待处理.filter((订单) => 订单.原委托市场 === '集合竞价').length,
  };
  容器.当前交易日 = `已结算-${状态.结构.交易制度.当前交易日}`;

  写入上一刻观测(状态);
  断言市场一致性(状态);
  断言交易制度一致性(状态);
  return Object.freeze({
    已执行: true,
    开盘价,
    候选成交量: 价格结果.可成交量,
    成交数量: 总成交数量,
    成交金额: 保留数值(总成交金额, 2),
    订单结果,
  });
}

export function 使跨日集合竞价委托失效(状态, 当前日期键 = null) {
  初始化交易制度状态(状态);
  const 容器 = 读取集合容器(状态);
  const 日期键 = 当前日期键 || 取北京时间日期键(状态.系统.虚拟时间);
  const 待失效 = 容器.待处理.filter((订单) => 订单.交易日 !== 日期键);
  for (const 订单 of 待失效) {
    const 原剩余 = 订单.剩余数量;
    完成集合订单(状态, 订单, '已过期');
    写入日志(状态, {
      动作: '集合竞价过期',
      编号: 订单.编号,
      方向: 订单.方向,
      价格: 订单.价格,
      数量: 原剩余,
      剩余数量: 0,
      状态: '已过期',
      委托市场: '集合竞价',
      说明: `${订单.编号}跨交易日自动失效，冻结资产已释放。`,
    });
  }
  if (待失效.length) {
    写入上一刻观测(状态);
    断言交易制度一致性(状态);
  }
  return Object.freeze({ 失效数量: 待失效.length, 编号列表: 待失效.map((订单) => 订单.编号) });
}

export function 读取玩家集合竞价委托(状态) {
  const 容器 = 读取集合容器(状态);
  return Object.freeze({
    待处理: 容器.待处理.map((订单) => ({ ...订单 })),
    历史: 容器.历史.slice(-100).map((订单) => ({ ...订单 })),
    最近结果: 容器.最近结果 ? { ...容器.最近结果 } : null,
  });
}
