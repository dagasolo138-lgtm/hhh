import { 变量字典 } from '../变量字典.js';
import { 同刻因果关系, 跨期反馈关系, 上一刻观测字段 } from './时序因果图.js';

const 派生节点 = new Set([
  '国内黄金溢价变化',
  '集合竞价订单失衡',
  '开盘前流动性',
  '本地流动性状态',
  '双向主动成交量',
  '订单失衡反复程度',
  '换手率',
  '市场状态摘要',
  '事件流',
]);

export function 检查执行时序() {
  const 错误 = [];
  const 节点 = new Set([
    ...Object.keys(变量字典),
    ...上一刻观测字段,
    ...派生节点,
  ]);

  for (const 关系 of [...同刻因果关系, ...跨期反馈关系]) {
    if (!节点.has(关系.来源)) 错误.push(`时序图未登记来源节点：${关系.来源}`);
    if (!节点.has(关系.去向)) 错误.push(`时序图未登记去向节点：${关系.去向}`);
    if (关系.时序 === '同一刻' && 关系.来源位置 > 关系.去向位置) {
      错误.push(`同一刻逆向关系：${关系.来源} → ${关系.去向}`);
    }
  }

  return Object.freeze({
    有效: 错误.length === 0,
    同刻关系数量: 同刻因果关系.length,
    跨期关系数量: 跨期反馈关系.length,
    错误,
  });
}
