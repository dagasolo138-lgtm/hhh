const 自动交易者标识 = Object.freeze([
  '价值套利',
  '趋势资金',
  '散户群体',
  '风险控制资金',
  '信息优势资金',
]);

const 自动订单名称 = Object.freeze({
  价值套利: '价值套利',
  趋势资金: '趋势资金',
  散户群体: '散户群体',
  风险控制资金: '风险控制资金',
  信息优势资金: '先行信号资金',
});

const 立即执行比例 = Object.freeze({
  价值套利: 0.68,
  趋势资金: 0.76,
  散户群体: 0.52,
  风险控制资金: 0.9,
  信息优势资金: 0.86,
});

function 数值(输入, 默认值 = 0) {
  const 结果 = Number(输入);
  return Number.isFinite(结果) ? 结果 : 默认值;
}

function 整数(输入) {
  return Math.max(0, Math.floor(数值(输入)));
}

function 价格取整(状态, 价格) {
  const 最小变动 = Number(状态.资产?.价格最小变动 || 0.001);
  const 对齐后 = Math.round(数值(价格) / 最小变动) * 最小变动;
  return 限制在当日价格边界(状态, Math.max(最小变动, 对齐后));
}

function 读取中心状态(状态) {
  状态.结构 ??= {};
  状态.结构.中心订单簿 ??= {
    下次委托序号: 1,
    本刻订单: [],
    本刻成交: [],
    订单历史: [],
    成交历史: [],
    盘口快照: { 买方: [], 卖方: [], 中间价: 0, 上次成交价: 0, 版本: 0 },
    版本: 0,
    最近更新: null,
  };
  const 中心 = 状态.结构.中心订单簿;
  中心.下次委托序号 ??= 1;
  中心.本刻订单 ??= [];
  中心.本刻成交 ??= [];
  中心.订单历史 ??= [];
  中心.成交历史 ??= [];
  中心.盘口快照 ??= { 买方: [], 卖方: [], 中间价: 0, 上次成交价: 0, 版本: 0 };
  中心.版本 ??= 0;
  return 中心;
}

function 下一序号(状态) {
  const 中心 = 读取中心状态(状态);
  const 序号 = 中心.下次委托序号;
  中心.下次委托序号 += 1;
  return 序号;
}

function 读取参与者(状态, 标识) {
  const 参与者 = 状态.交易者?.[标识];
  if (!参与者) throw new Error(`中心订单簿找不到参与者：${标识}`);
  参与者.平均成本 ??= 状态.变量.基金交易价格;
  参与者.已实现盈亏 ??= 0;
  return 参与者;
}

function 可买数量(参与者, 限价) {
  return Math.max(0, Math.floor(数值(参与者.现金) / Math.max(限价, 0.001)));
}

function 可卖数量(参与者) {
  return 整数(参与者.持仓);
}

function 创建订单(状态, 选项) {
  const 序号 = 下一序号(状态);
  const 数量 = 整数(选项.数量);
  const 价格 = 价格取整(状态, 选项.价格);
  return {
    编号: `中心委托-${序号}`,
    委托序号: 序号,
    来源: 选项.来源,
    参与者标识: 选项.参与者标识,
    订单类别: 选项.订单类别,
    方向: 选项.方向,
    价格,
    数量,
    已成交数量: 0,
    剩余数量: 数量,
    状态: '待撮合',
    委托时间: 状态.系统.虚拟时间,
    有效期: 选项.有效期 || '本刻',
    做市报价: Boolean(选项.做市报价),
    自动生成: Boolean(选项.自动生成),
  };
}

function 复制做市报价进入中心(状态) {
  const 订单簿 = 状态.订单簿 || { 买方: [], 卖方: [] };
  const 做市商 = 读取参与者(状态, '做市商');
  const 订单 = [];
  let 剩余可卖 = 可卖数量(做市商);
  let 剩余可买现金 = 数值(做市商.现金);

  for (const 档位 of [...(订单簿.卖方 || [])].sort((左, 右) => 左.价格 - 右.价格)) {
    const 数量 = Math.min(整数(档位.数量), 剩余可卖);
    if (!数量) continue;
    订单.push(创建订单(状态, {
      来源: '做市商',
      参与者标识: '做市商',
      订单类别: '双边报价',
      方向: '卖出',
      价格: 档位.价格,
      数量,
      有效期: '本刻',
      做市报价: true,
    }));
    剩余可卖 -= 数量;
  }

  for (const 档位 of [...(订单簿.买方 || [])].sort((左, 右) => 右.价格 - 左.价格)) {
    const 价格 = 价格取整(状态, 档位.价格);
    const 数量 = Math.min(整数(档位.数量), Math.floor(剩余可买现金 / Math.max(价格, 0.001)));
    if (!数量) continue;
    订单.push(创建订单(状态, {
      来源: '做市商',
      参与者标识: '做市商',
      订单类别: '双边报价',
      方向: '买入',
      价格,
      数量,
      有效期: '本刻',
      做市报价: true,
    }));
    剩余可买现金 -= 数量 * 价格;
  }

  return 订单;
}

function 读取同侧最优价(状态, 方向) {
  const 档位 = 方向 === '买入' ? 状态.订单簿?.买方 : 状态.订单簿?.卖方;
  return 数值(档位?.[0]?.价格, 状态.变量.基金交易价格);
}

function 读取对侧层级价格(状态, 方向, 预计数量) {
  const 对侧 = directionOpposite(方向) === '卖出' ? 状态.订单簿?.卖方 : 状态.订单簿?.买方;
  const 排序后 = [...(对侧 || [])].sort((左, 右) => directionOpposite(方向) === '卖出' ? 左.价格 - 右.价格 : 右.价格 - 左.价格);
  let 累计 = 0;
  for (const 档位 of 排序后) {
    累计 += 整数(档位.数量);
    if (累计 >= 预计数量) return 数值(档位.价格, 读取同侧最优价(状态, 方向));
  }
  return 数值(排序后.at(-1)?.价格, 读取同侧最优价(状态, 方向));
}

function directionOpposite(方向) {
  return 方向 === '买入' ? '卖出' : '买入';
}

function 自动订单意图(状态, 最大单刻调整) {
  const 意图 = {};
  for (const 标识 of 自动交易者标识) {
    const 参与者 = 读取参与者(状态, 标识);
    const 最大调整 = 整数(最大单刻调整?.[标识] ?? 参与者.最大单刻调整);
    const 差额 = Math.trunc(数值(参与者.目标仓位) - 数值(参与者.持仓));
    let 数量 = Math.min(Math.abs(差额), 最大调整);
    const 方向 = 差额 >= 0 ? '买入' : '卖出';
    const 参考价 = 读取同侧最优价(状态, 方向);
    if (方向 === '买入') 数量 = Math.min(数量, 可买数量(参与者, 参考价));
    else 数量 = Math.min(数量, 可卖数量(参与者));
    意图[标识] = 方向 === '买入' ? 数量 : -数量;
  }
  return 意图;
}

function 复制自动意图进入中心(状态, 意图) {
  const 订单 = [];
  for (const [标识, 有符号数量] of Object.entries(意图)) {
    const 总数量 = Math.abs(Math.trunc(数值(有符号数量)));
    if (!总数量 || !自动订单名称[标识]) continue;
    const 方向 = 有符号数量 > 0 ? '买入' : '卖出';
    const 立即比例 = 立即执行比例[标识] || 0.6;
    const 立即数量 = Math.max(0, Math.min(总数量, Math.round(总数量 * 立即比例)));
    const 被动数量 = Math.max(0, 总数量 - 立即数量);
    const 激进限价 = 读取对侧层级价格(状态, 方向, Math.max(1, 立即数量));
    const 被动限价 = 读取同侧最优价(状态, 方向);

    if (立即数量) {
      订单.push(创建订单(状态, {
        来源: 自动订单名称[标识],
        参与者标识: 标识,
        订单类别: '自动主动限价单',
        方向,
        价格: 激进限价,
        数量: 立即数量,
        有效期: '本刻',
        自动生成: true,
      }));
    }
    if (被动数量) {
      订单.push(创建订单(状态, {
        来源: 自动订单名称[标识],
        参与者标识: 标识,
        订单类别: '自动被动限价单',
        方向,
        价格: 被动限价,
        数量: 被动数量,
        有效期: '本刻',
        自动生成: true,
      }));
    }
  }
  return 订单;
}

function 买方排序(订单) {
  return [...订单]
    .filter((项) => 项.方向 === '买入' && 项.剩余数量 > 0)
    .sort((左, 右) => 右.价格 - 左.价格 || 左.委托序号 - 右.委托序号);
}

function 卖方排序(订单) {
  return [...订单]
    .filter((项) => 项.方向 === '卖出' && 项.剩余数量 > 0)
    .sort((左, 右) => 左.价格 - 右.价格 || 左.委托序号 - 右.委托序号);
}

function 更新买方账户(参与者, 数量, 金额, 成交价) {
  const 原持仓 = 数值(参与者.持仓);
  const 原成本 = 数值(参与者.平均成本, 成交价);
  参与者.现金 = 保留数值(数值(参与者.现金) - 金额, 2);
  参与者.持仓 = 原持仓 + 数量;
  参与者.平均成本 = 保留数值((原成本 * 原持仓 + 金额) / Math.max(参与者.持仓, 1), 6);
}

function 更新卖方账户(参与者, 数量, 金额, 成交价) {
  const 原成本 = 数值(参与者.平均成本, 成交价);
  参与者.现金 = 保留数值(数值(参与者.现金) + 金额, 2);
  参与者.持仓 = Math.max(0, 数值(参与者.持仓) - 数量);
  参与者.已实现盈亏 = 保留数值(数值(参与者.已实现盈亏) + (成交价 - 原成本) * 数量, 2);
  if (!参与者.持仓) 参与者.平均成本 = 0;
}

function 执行成交(状态, 买单, 卖单, 成交价, 成交数量) {
  const 买方 = 读取参与者(状态, 买单.参与者标识);
  const 卖方 = 读取参与者(状态, 卖单.参与者标识);
  const 金额 = 保留数值(成交价 * 成交数量, 2);

  更新买方账户(买方, 成交数量, 金额, 成交价);
  更新卖方账户(卖方, 成交数量, 金额, 成交价);
  买单.已成交数量 += 成交数量;
  卖单.已成交数量 += 成交数量;
  买单.剩余数量 -= 成交数量;
  卖单.剩余数量 -= 成交数量;
  买单.状态 = 买单.剩余数量 ? '部分成交' : '全部成交';
  卖单.状态 = 卖单.剩余数量 ? '部分成交' : '全部成交';

  return {
    编号: `中心成交-${状态.系统.演算序号}-${买单.委托序号}-${卖单.委托序号}-${买单.已成交数量}`,
    时间: 状态.系统.虚拟时间,
    买方订单: 买单.编号,
    卖方订单: 卖单.编号,
    买方来源: 买单.来源,
    卖方来源: 卖单.来源,
    买方标识: 买单.参与者标识,
    卖方标识: 卖单.参与者标识,
    价格: 成交价,
    数量: 成交数量,
    金额,
  };
}

function 按价格时间撮合(状态, 订单) {
  const 成交 = [];
  while (true) {
    const 买方 = 买方排序(订单);
    const 卖方 = 卖方排序(订单);
    const 最优买 = 买方[0];
    const 最优卖 = 卖方[0];
    if (!最优买 || !最优卖 || 最优买.价格 < 最优卖.价格) break;

    if (最优买.参与者标识 === 最优卖.参与者标识) {
      const 后提交 = 最优买.委托序号 > 最优卖.委托序号 ? 最优买 : 最优卖;
      后提交.状态 = '已拒绝';
      后提交.拒绝原因 = '同一参与者自成交保护';
      后提交.剩余数量 = 0;
      continue;
    }

    const 买方账户 = 读取参与者(状态, 最优买.参与者标识);
    const 卖方账户 = 读取参与者(状态, 最优卖.参与者标识);
    const 成交价 = 价格取整(状态, 最优买.委托序号 < 最优卖.委托序号 ? 最优买.价格 : 最优卖.价格);
    const 现金可买 = Math.floor(数值(买方账户.现金) / Math.max(成交价, 0.001));
    const 可卖 = 可卖数量(卖方账户);
    const 成交数量 = Math.min(最优买.剩余数量, 最优卖.剩余数量, 现金可买, 可卖);
    if (!成交数量) {
      if (!现金可买) {
        最优买.状态 = '已拒绝';
        最优买.拒绝原因 = '可用现金不足';
        最优买.剩余数量 = 0;
      }
      if (!可卖) {
        最优卖.状态 = '已拒绝';
        最优卖.拒绝原因 = '可用持仓不足';
        最优卖.剩余数量 = 0;
      }
      continue;
    }
    成交.push(执行成交(状态, 最优买, 最优卖, 成交价, 成交数量));
  }
  return 成交;
}

function 形成盘口快照(状态, 订单, 成交) {
  const 分层 = (方向) => {
    const 聚合 = new Map();
    for (const 项 of 订单.filter((订单项) => 订单项.方向 === 方向 && 订单项.剩余数量 > 0)) {
      const 键 = 项.价格.toFixed(3);
      const 层 = 聚合.get(键) || {
        价格: 项.价格,
        做市数量: 0,
        自动挂单数量: 0,
        玩家挂单数量: 0,
        来源: [],
      };
      if (项.做市报价) 层.做市数量 += 项.剩余数量;
      else 层.自动挂单数量 += 项.剩余数量;
      if (!层.来源.includes(项.来源)) 层.来源.push(项.来源);
      聚合.set(键, 层);
    }
    return [...聚合.values()]
      .sort((左, 右) => 方向 === '买入' ? 右.价格 - 左.价格 : 左.价格 - 右.价格)
      .slice(0, 10)
      .map((层, 索引) => ({
        档位: 索引 + 1,
        ...层,
        数量: 层.做市数量 + 层.自动挂单数量,
        可成交数量: 层.做市数量 + 层.自动挂单数量,
      }));
  };
  const 最后成交价 = 成交.at(-1)?.价格 ?? 数值(状态.变量.基金交易价格);
  return {
    买方: 分层('买入'),
    卖方: 分层('卖出'),
    中间价: 最后成交价,
    上次成交价: 最后成交价,
    版本: Number(状态.结构.中心订单簿?.版本 || 0) + 1,
  };
}

function 更新成交统计(状态, 成交, 自动意图, 原价格) {
  const 变量 = 状态.变量;
  const 买入成交量 = 成交
    .filter((项) => 项.买方标识 !== '做市商')
    .reduce((合计, 项) => 合计 + 项.数量, 0);
  const 卖出成交量 = 成交
    .filter((项) => 项.卖方标识 !== '做市商')
    .reduce((合计, 项) => 合计 + 项.数量, 0);
  const 总成交量 = 成交.reduce((合计, 项) => 合计 + 项.数量, 0);
  const 总成交额 = 成交.reduce((合计, 项) => 合计 + 项.金额, 0);
  const 最后成交价 = 成交.at(-1)?.价格 ?? 原价格;

  变量.主动买入量 = 买入成交量;
  变量.主动卖出量 = 卖出成交量;
  变量.成交量 = 总成交量;
  变量.成交额 = 保留数值(总成交额, 2);
  变量.成交笔数 = 成交.length;
  变量.订单失衡 = 限制数值(
    (买入成交量 - 卖出成交量) / Math.max(买入成交量 + 卖出成交量, 1),
    -1,
    1,
  );
  变量.成交价格冲击 = 保留数值(
    Math.log(Math.max(最后成交价, 0.001) / Math.max(原价格, 0.001)),
    8,
  );
  变量.基金交易价格 = 限制在当日价格边界(状态, 最后成交价);
  变量.基金交易价格收益 = 保留数值(
    Math.log(变量.基金交易价格 / Math.max(原价格, 0.001)),
    8,
  );
  变量.基金交易溢价折价 = 保留数值(
    (变量.基金交易价格 - 变量.基金理论净值) / Math.max(变量.基金理论净值, Number.EPSILON),
    8,
  );
  变量.短期实现波动率 = Math.sqrt(变量.短期实现波动率 ** 2 * 0.92 + 变量.基金交易价格收益 ** 2 * 0.08);
  变量.近期高点 = Math.max(变量.近期高点, 变量.基金交易价格);
  变量.回撤幅度 = 限制数值(
    (变量.近期高点 - 变量.基金交易价格) / Math.max(变量.近期高点, Number.EPSILON),
    0,
    1,
  );
  变量.历史平均成交量 = Math.max(1, 变量.历史平均成交量 * 0.97 + Math.max(变量.成交量, 1) * 0.03);
  变量.异常成交量 = 变量.成交量 / Math.max(变量.历史平均成交量, 1);
  变量.流动性冲击指标 = Math.abs(变量.基金交易价格收益) / Math.max(变量.成交额, 1);
  变量.做市库存 = 读取参与者(状态, '做市商').持仓 - 读取参与者(状态, '做市商').目标仓位;
  变量.价值套利订单量 = 自动意图.价值套利 || 0;
  变量.趋势资金订单量 = 自动意图.趋势资金 || 0;
  变量.风险控制订单量 = Math.min(0, 自动意图.风险控制资金 || 0);
  变量.信息优势订单量 = 自动意图.信息优势资金 || 0;
  状态.派生.双向主动成交量 = Math.min(买入成交量, 卖出成交量);

  const 失衡历史 = 状态.结构.最近订单失衡;
  失衡历史.push(变量.订单失衡);
  if (失衡历史.length > 24) 失衡历史.shift();
  const 变号次数 = 失衡历史.slice(1).reduce((合计, 当前, 索引) => (
    当前 * 失衡历史[索引] < 0 ? 合计 + 1 : 合计
  ), 0);
  状态.派生.订单失衡反复程度 = 失衡历史.length > 1 ? 变号次数 / (失衡历史.length - 1) : 0;

  if (总成交量) 记录当日成交(状态, 变量.基金交易价格, 总成交量, 总成交额);
}

function 更新历史记录(状态, 市场日志, 成交, 自动意图) {
  const 变量 = 状态.变量;
  const 最新价格记录 = 状态.历史.价格序列.at(-1);
  if (最新价格记录) {
    最新价格记录.最高 = Math.max(最新价格记录.最高, 变量.基金交易价格);
    最新价格记录.最低 = Math.min(最新价格记录.最低, 变量.基金交易价格);
    最新价格记录.收盘 = 变量.基金交易价格;
    最新价格记录.成交量 = 变量.成交量;
    最新价格记录.成交额 = 变量.成交额;
    最新价格记录.溢价折价 = 变量.基金交易溢价折价;
  }

  const 成本分布 = Object.values(状态.交易者).map((参与者) => ({
    来源: 参与者.名称,
    价格: 保留数值(参与者.平均成本 ?? 变量.基金交易价格, 6),
    份额: Math.max(0, Math.round(参与者.持仓)),
    时间: 状态.系统.虚拟时间,
  }));
  状态.结构.持仓成本分布 = 成本分布;
  变量.持仓成本分布 = 成本分布;

  if (市场日志) {
    市场日志.交易价格 = 变量.基金交易价格;
    市场日志.价格收益 = 保留数值(变量.基金交易价格收益 * 100, 4);
    市场日志.成交量 = 变量.成交量;
    市场日志.订单失衡 = 保留数值(变量.订单失衡, 4);
    市场日志.订单 = { ...自动意图 };
    市场日志.中心撮合 = {
      成交笔数: 成交.length,
      成交量: 变量.成交量,
      成交额: 变量.成交额,
      最后成交价: 变量.基金交易价格,
    };
  }
}

export function 执行自动中心订单簿(状态, 最大单刻调整 = {}, 市场日志 = null) {
  const 中心 = 读取中心状态(状态);
  const 原价格 = 数值(状态.变量.基金交易价格);
  const 自动意图 = 自动订单意图(状态, 最大单刻调整);
  const 做市订单 = 复制做市报价进入中心(状态);
  const 自动订单 = 复制自动意图进入中心(状态, 自动意图);
  const 本刻订单 = [...做市订单, ...自动订单];
  const 成交 = 按价格时间撮合(状态, 本刻订单);

  更新成交统计(状态, 成交, 自动意图, 原价格);
  const 盘口快照 = 形成盘口快照(状态, 本刻订单, 成交);
  中心.本刻订单 = 本刻订单;
  中心.本刻成交 = 成交;
  中心.订单历史.push(...本刻订单.filter((订单) => 订单.状态 !== '待撮合' && 订单.状态 !== '部分成交').map((订单) => ({ ...订单 })));
  中心.成交历史.push(...成交.map((记录) => ({ ...记录 })));
  if (中心.订单历史.length > 6000) 中心.订单历史.splice(0, 中心.订单历史.length - 6000);
  if (中心.成交历史.length > 6000) 中心.成交历史.splice(0, 中心.成交历史.length - 6000);
  中心.盘口快照 = 盘口快照;
  中心.版本 += 1;
  中心.最近更新 = 状态.系统.虚拟时间;

  更新历史记录(状态, 市场日志, 成交, 自动意图);
  return Object.freeze({
    自动意图,
    本刻订单: 本刻订单.map((订单) => ({ ...订单 })),
    成交: 成交.map((记录) => ({ ...记录 })),
    盘口快照: { ...盘口快照, 买方: [...盘口快照.买方], 卖方: [...盘口快照.卖方] },
    买入: {
      成交数量: 成交.filter((项) => 项.买方标识 !== '做市商').reduce((合计, 项) => 合计 + 项.数量, 0),
      成交金额: 成交.filter((项) => 项.买方标识 !== '做市商').reduce((合计, 项) => 合计 + 项.金额, 0),
    },
    卖出: {
      成交数量: 成交.filter((项) => 项.卖方标识 !== '做市商').reduce((合计, 项) => 合计 + 项.数量, 0),
      成交金额: 成交.filter((项) => 项.卖方标识 !== '做市商').reduce((合计, 项) => 合计 + 项.金额, 0),
    },
  });
}

export function 读取中心订单簿(状态) {
  const 中心 = 读取中心状态(状态);
  return Object.freeze({
    本刻订单: 中心.本刻订单.map((订单) => ({ ...订单 })),
    本刻成交: 中心.本刻成交.map((成交) => ({ ...成交 })),
    盘口快照: {
      ...中心.盘口快照,
      买方: [...(中心.盘口快照.买方 || [])],
      卖方: [...(中心.盘口快照.卖方 || [])],
    },
    版本: 中心.版本,
  });
}
