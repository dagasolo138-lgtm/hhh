function 断言(条件, 信息) {
  if (!条件) throw new Error(信息);
}

function 接近零(数值) {
  return Math.abs(Number(数值 || 0)) < 0.01;
}

function 指纹(状态) {
  return JSON.stringify({
    系统: { 随机种子: 状态.系统.随机种子, 演算序号: 状态.系统.演算序号, 虚拟时间: 状态.系统.虚拟时间 },
    变量: 状态.变量,
    制度: 状态.结构.交易制度,
    玩家委托: 状态.结构.玩家委托,
    集合竞价: 状态.结构.集合竞价,
    中心订单簿: {
      本刻订单: 状态.结构.中心订单簿?.本刻订单,
      本刻成交: 状态.结构.中心订单簿?.本刻成交,
      盘口快照: 状态.结构.中心订单簿?.盘口快照,
    },
    交易者: Object.fromEntries(Object.entries(状态.交易者).map(([标识, 参与者]) => [标识, {
      现金: Number(参与者.现金.toFixed(8)), 持仓: 参与者.持仓, 平均成本: Number((参与者.平均成本 ?? 0).toFixed(8)),
    }])),
  });
}

function 断言当日价格边界(状态) {
  const { 跌停价, 涨停价 } = 读取当日价格边界(状态);
  断言(状态.变量.基金交易价格 >= 跌停价, '交易价格跌破当日跌停价。');
  断言(状态.变量.基金交易价格 <= 涨停价, '交易价格突破当日涨停价。');
  for (const 档位 of [...状态.订单簿.买方, ...状态.订单簿.卖方]) {
    断言(档位.价格 >= 跌停价 && 档位.价格 <= 涨停价, '基础做市报价突破当日价格边界。');
  }
}

function 测试集合竞价(状态) {
  const { 涨停价 } = 读取当日价格边界(状态);
  const 可撤单 = 提交玩家限价订单(状态, '买入', 100, 涨停价);
  断言(可撤单.成功, '集合竞价应允许提交限价单。');
  断言(状态.结构.集合竞价.待处理.length === 1, '集合竞价委托应进入独立订单池。');
  断言(!接近零(状态.结构.玩家冻结资产.现金), '集合竞价买单应冻结现金。');
  断言(撤销玩家限价订单(状态, 可撤单.订单.编号).成功, '09:20前集合竞价委托应允许撤销。');
  断言(接近零(状态.结构.玩家冻结资产.现金), '集合竞价撤单后冻结现金应归零。');

  const 待开盘 = 提交玩家限价订单(状态, '买入', 100, 涨停价);
  断言(待开盘.成功, '集合竞价锁单测试委托提交失败。');
  连续推进时序中国黄金市场(状态, 1);
  断言(状态.结构.交易制度.当前时分 === '09:20', '测试应进入09:20撤单锁定时段。');
  断言(!撤销玩家限价订单(状态, 待开盘.订单.编号).成功, '09:20后集合竞价委托不应允许撤销。');

  连续推进时序中国黄金市场(状态, 2);
  const 竞价结果 = 状态.结构.集合竞价.最近结果;
  断言(竞价结果?.开盘价 > 0, '09:30应产生单一集合竞价开盘价。');
  const 所有订单 = 读取玩家全部限价委托(状态);
  const 原集合订单 = [...所有订单.待处理, ...所有订单.历史].find((订单) => 订单.编号 === 待开盘.订单.编号);
  断言(原集合订单?.原委托市场 === '集合竞价', '集合竞价委托应保留来源标识。');
}

function 测试玩家中心订单(状态) {
  连续推进时序中国黄金市场(状态, 1);
  const 市价买入 = 玩家市价成交(状态, '买入', 100);
  断言(市价买入.成功, '玩家市价买单必须通过中心订单簿成交。');
  const 中心 = 读取中心订单簿(状态);
  断言(中心.本刻成交.some((成交) => 成交.买方标识 === '玩家'), '中心成交记录必须包含玩家买方。');
  断言(中心.本刻成交.every((成交) => 成交.买方标识 !== 成交.卖方标识), '中心订单簿不得发生自成交。');

  const { 跌停价 } = 读取当日价格边界(状态);
  const 远离成交的买价 = Math.max(跌停价 + 0.003, Number((状态.变量.基金交易价格 * 0.96).toFixed(3)));
  const 限价买单 = 提交玩家限价委托(状态, '买入', 100, 远离成交的买价);
  断言(限价买单.成功, '玩家限价单应进入中心订单簿。');
  const 初始待处理 = 读取玩家限价委托(状态).待处理.find((订单) => 订单.编号 === 限价买单.订单.编号);
  断言(初始待处理, '不应立即成交的远离市价限价单必须保留。');
  断言(状态.结构.玩家冻结资产.现金 > 0, '待处理限价买单必须冻结现金。');

  连续推进时序中国黄金市场(状态, 1);
  const 跨刻订单 = 读取玩家限价委托(状态).待处理.find((订单) => 订单.编号 === 限价买单.订单.编号)
    || 读取玩家限价委托(状态).历史.find((订单) => 订单.编号 === 限价买单.订单.编号);
  断言(跨刻订单, '玩家当日有效限价单跨刻不得无故丢失。');

  const 可撤订单 = 读取玩家限价委托(状态).待处理.find((订单) => 订单.编号 === 限价买单.订单.编号);
  if (可撤订单) {
    断言(撤销玩家限价委托(状态, 可撤订单.编号).成功, '玩家中心限价单应可撤销。');
    断言(接近零(状态.结构.玩家冻结资产.现金), '撤单后冻结现金应归零。');
  }

  const 可卖数量 = 状态.变量.玩家持仓 - 状态.结构.玩家冻结资产.份额;
  const 市价卖出 = 玩家市价成交(状态, '卖出', 可卖数量);
  断言(市价卖出.成功, '玩家市价卖单必须通过中心订单簿成交。');
  断言(状态.变量.玩家持仓 === 0, '玩家市价买入后卖出应恢复为零持仓。');
}

function 运行一次(种子) {
  const 状态 = 初始化时序市场(创建中国黄金市场状态({
    随机种子: 种子,
    初始时间: '2026-06-22T01:15:00.000Z',
  }));
  断言(!玩家市价成交(状态, '买入', 100).成功, '集合竞价阶段不得发生即时市价成交。');
  测试集合竞价(状态);
  断言(!玩家市价成交(状态, '买入', 50).成功, '非100份整数倍买入必须被拒绝。');
  测试玩家中心订单(状态);

  连续推进时序中国黄金市场(状态, 520);
  const 市场校验 = 检查市场一致性(状态);
  const 制度校验 = 检查交易制度一致性(状态);
  断言(市场校验.有效, `市场一致性失败：${市场校验.错误.join('；')}`);
  断言(制度校验.有效, `交易制度一致性失败：${制度校验.错误.join('；')}`);
  断言当日价格边界(状态);
  断言(状态.历史.日志.length >= 530, '市场日志数量不足。');
  断言(状态.变量.基金交易价格 > 0, '交易价格必须大于零。');
  断言(状态.变量.基金理论净值 > 0, '理论净值必须大于零。');
  断言(接近零(状态.结构.玩家冻结资产.现金), '最终冻结现金应归零。');
  断言(接近零(状态.结构.玩家冻结资产.份额), '最终冻结份额应归零。');
  return 状态;
}

