import { 创建中国黄金市场状态 } from './状态工厂.js';
import { 连续推进中国黄金市场 } from './演算器.js';
import { 检查市场一致性 } from './一致性检查.js';

function 断言(条件, 信息) {
  if (!条件) throw new Error(信息);
}

function 指纹(状态) {
  return JSON.stringify({
    系统: {
      随机种子: 状态.系统.随机种子,
      演算序号: 状态.系统.演算序号,
      虚拟时间: 状态.系统.虚拟时间,
    },
    变量: 状态.变量,
    交易者: Object.fromEntries(
      Object.entries(状态.交易者).map(([标识, 参与者]) => [标识, {
        现金: Number(参与者.现金.toFixed(8)),
        持仓: 参与者.持仓,
        平均成本: Number((参与者.平均成本 ?? 0).toFixed(8)),
      }]),
    ),
    最后一条日志: 状态.历史.日志.at(-1),
  });
}

function 运行一次(种子) {
  const 状态 = 创建中国黄金市场状态({
    随机种子: 种子,
    初始时间: '2026-06-22T01:15:00.000Z',
  });

  连续推进中国黄金市场(状态, 520);
  const 校验 = 检查市场一致性(状态);
  断言(校验.有效, `一致性失败：${校验.错误.join('；')}`);
  断言(状态.历史.日志.length === 520 || 状态.历史.日志.length > 520, '市场日志数量不足。');
  断言(状态.变量.基金交易价格 > 0, '交易价格必须大于零。');
  断言(状态.变量.基金理论净值 > 0, '理论净值必须大于零。');
  断言(状态.订单簿.买方[0].价格 < 状态.订单簿.卖方[0].价格, '买一价必须低于卖一价。');
  return 状态;
}

const 第一条世界线 = 运行一次(20260622);
const 第二条世界线 = 运行一次(20260622);
const 第三条世界线 = 运行一次(20260623);

断言(指纹(第一条世界线) === 指纹(第二条世界线), '同种子世界线无法复现。');
断言(指纹(第一条世界线) !== 指纹(第三条世界线), '不同种子世界线不应完全相同。');

console.log(JSON.stringify({
  结果: '通过',
  演算刻数: 第一条世界线.系统.演算序号,
  最终时间: 第一条世界线.系统.虚拟时间,
  最终交易价格: 第一条世界线.变量.基金交易价格,
  最终理论净值: 第一条世界线.变量.基金理论净值,
  最终溢价折价: 第一条世界线.变量.基金交易溢价折价,
  最终日志数量: 第一条世界线.历史.日志.length,
}, null, 2));
