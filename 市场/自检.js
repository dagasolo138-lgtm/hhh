function 断言(条件, 信息) {
  if (!条件) throw new Error(信息);
}

function stateZero(数值) {
  return Math.abs(Number(数值 || 0)) < 0.01;
}

function 指纹(状态) {
  return JSON.stringify({
    系统: {
      随机种子: 状态.系统.随机种子,
      演算序号: 状态.系统.演算序号,
      虚拟时间: 状态.系统.虚拟时间,
    },
    变量: 状态.变量,
    制度: 状态.结构.交易制度,
    玩家委托: 状态.结构.玩家委托,
    集合竞价: 状态.结构.集合竞价,
    交易者: Object.fromEntries(
      Object.entries(状态.交易者).map(([标识, 参与者]) => [标识, {
        现金: Number(参与者.现金.toFixed(8)),
        持仓: 参与者.持仓,
        平均成本: Number((参与者.平均成本 ?? 0).toFixed(8)),
      }]),
    ),
    最后一条日志: 状态.历史.日志.at(-1),
  });
}

function 断言当日价格边界(状态) {
  const { 跌停价, 涨停价 } = 读取当日价格边界(状态);
  断言(状态.变量.基金交易价格 >= 跌停价, '交易价格跌破当日跌停价。');
  断言(状态.变量.基金交易价格 <= 涨停价, '交易价格突破当日涨停价。');
  for (const 档位 of [...状态.订单簿.买方, ...状态.订单簿.卖方]) {
    断言(档位.价格 >= 跌停价 && 档位.价格 <= 涨停价, '订单簿报价突破当日价格边界。');
  }
}

function 测试集合竞价(状态) {
  const { 涨停价 } = 读取当日价格边界(状态);
  const 可撤单 = 提交玩家限价订单(状态, '买入', 100, 涨停价);
  断言(可撤单.成功, '集合竞价应允许提交限价单。');
  断言(状态.结构.集合竞价.待处理.length === 1, '集合竞价委托应进入独立订单池。');
  断言(!stateZero(状态.结构.玩家冻结资产.现金), '集合竞价买单应冻结现金。');

  const 撤销成功 = 撤销玩家限价订单(状态, 可撤单.订单.编号);
  断言(撤销成功.成功, '09:20 前集合竞价委托应允许撤销。');
  断言(stateZero(状态.结构.玩家冻结资产.现金), '集合竞价撤单后冻结现金应归零。');

  const 待开盘 = 提交玩家限价订单(状态, '买入', 100, 涨停价);
  断言(待开盘.成功, '集合竞价锁单测试委托提交失败。');
  连续推进时序中国黄金市场(状态, 1);
  断言(状态.结构.交易制度.当前时分 === '09:20', '测试应进入09:20撤单锁定时段。');
  const 锁定撤单 = 撤销玩家限价订单(状态, 待开盘.订单.编号);
  断言(!锁定撤单.成功, '09:20后集合竞价委托不应允许撤销。');

  连续推进时序中国黄金市场(状态, 2);
  const 竞价结果 = 状态.结构.集合竞价.最近结果;
  断言(竞价结果?.开盘价 > 0, '09:30应产生单一集合竞价开盘价。');
  断言(状态.结构.集合竞价.待处理.length === 0, '09:30后集合竞价订单池不应保留待处理订单。');
  const 所有订单 = 读取玩家全部限价委托(状态);
  const 原集合订单 = [...所有订单.待处理, ...所有订单.历史].find((订单) => 订单.编号 === 待开盘.订单.编号);
  断言(原集合订单, '集合竞价委托在开盘后不应丢失。');
  断言(原集合订单.原委托市场 === '集合竞价', '集合竞价委托应保留来源标识。');
  断言(状态.变量.玩家持仓 >= 0, '集合竞价后玩家持仓不合法。');
}

function 测试连续竞价队列(状态) {
  const { 跌停价 } = 读取当日价格边界(状态);
  const 限价 = Math.max(跌停价 + 0.003, Number((状态.订单簿.买方[0].价格 - 0.006).toFixed(3)));
  const 原现金 = 状态.变量.玩家现金;

  const 第一笔 = 提交玩家限价委托(状态, '买入', 100, 限价);
  const 第二笔 = 提交玩家限价委托(状态, '买入', 100, 限价);
  断言(第一笔.成功 && 第二笔.成功, '同价限价买单应可进入队列。');
  断言(状态.结构.玩家委托.待处理.length === 2, '两笔不可成交同价买单应保留在队列中。');
  断言(Math.abs(状态.结构.玩家冻结资产.现金 - 限价 * 200) < 0.01, '买单冻结现金不正确。');

  状态.订单簿.买方 = 状态.订单簿.买方.map((档位, 索引) => ({
    ...档位,
    价格: Number((限价 - (索引 + 1) * 0.001).toFixed(3)),
  }));
  状态.订单簿.卖方 = 状态.订单簿.卖方.map((档位, 索引) => ({
    ...档位,
    价格: Number((限价 + (索引 === 0 ? 0 : (索引 + 1) * 0.001)).toFixed(3)),
    数量: 索引 === 0 ? 100 : 0,
  }));

  const 撮合 = 撮合玩家限价委托(状态);
  断言(撮合.成交订单数 === 1, '只有一档100份对手深度时应只成交一笔同价委托。');
  const 历史第一笔 = 状态.结构.玩家委托.历史.find((订单) => 订单.编号 === 第一笔.订单.编号);
  const 待处理第二笔 = 状态.结构.玩家委托.待处理.find((订单) => 订单.编号 === 第二笔.订单.编号);
  断言(历史第一笔?.状态 === '全部成交', '同价队列中先提交的订单应优先成交。');
  断言(待处理第二笔?.状态 === '待撮合', '同价队列中后提交的订单应继续等待。');
  断言(Math.abs(状态.结构.玩家冻结资产.现金 - 限价 * 100) < 0.01, '部分队列成交后冻结现金不正确。');

  const 撤销 = 撤销玩家限价委托(状态, 第二笔.订单.编号);
  断言(撤销.成功, '待处理限价单应可以撤销。');
  断言(stateZero(状态.结构.玩家冻结资产.现金), '撤单后冻结现金应归零。');
  断言(状态.变量.玩家现金 < 原现金, '首笔成交后玩家现金应减少。');
}

function 运行一次(种子) {
  const 状态 = 初始化时序市场(创建中国黄金市场状态({
    随机种子: 种子,
    初始时间: '2026-06-22T01:15:00.000Z',
  }));

  const 集合竞价市价单 = 玩家市价成交(状态, '买入', 100);
  断言(!集合竞价市价单.成功, '集合竞价阶段不得发生即时市价成交。');
  测试集合竞价(状态);

  const 非法买入 = 玩家市价成交(状态, '买入', 50);
  断言(!非法买入.成功, '非100份整数倍买入必须被拒绝。');
  测试连续竞价队列(状态);
  连续推进时序中国黄金市场(状态, 1);

  const 买入结果 = 玩家市价成交(状态, '买入', 100);
  断言(买入结果.成功, `玩家买入失败：${买入结果.原因 || ''}`);
  const 待卖数量 = 状态.变量.玩家持仓;
  const 卖出结果 = 玩家市价成交(状态, '卖出', 待卖数量);
  断言(卖出结果.成功, `玩家卖出失败：${卖出结果.原因 || ''}`);

  连续推进时序中国黄金市场(状态, 520);
  const 市场校验 = 检查市场一致性(状态);
  const 制度校验 = 检查交易制度一致性(状态);
  断言(市场校验.有效, `市场一致性失败：${市场校验.错误.join('；')}`);
  断言(制度校验.有效, `交易制度一致性失败：${制度校验.错误.join('；')}`);
  断言当日价格边界(状态);
  断言(状态.历史.日志.length >= 530, '市场日志数量不足。');
  断言(状态.变量.基金交易价格 > 0, '交易价格必须大于零。');
  断言(状态.变量.基金理论净值 > 0, '理论净值必须大于零。');
  断言(状态.订单簿.买方[0].价格 < 状态.订单簿.卖方[0].价格, '买一价必须低于卖一价。');
  断言(状态.变量.玩家持仓 === 0, '玩家买入后卖出应恢复为零持仓。');
  断言(stateZero(状态.结构.玩家冻结资产.现金), '冻结现金应归零。');
  断言(stateZero(状态.结构.玩家冻结资产.份额), '冻结份额应归零。');
  return 状态;
}

