import { 保留数值 } from './状态工厂.js';

/**
 * 09:30 同一演算刻可能先完成集合竞价，再撮合玩家剩余限价单。
 * 后一段中心撮合会写入本刻统计，因此这里把两段成交重新合并。
 */
export function 合并开盘与连续成交统计(状态, 集合竞价结果) {
  if (!集合竞价结果?.已执行 || !(集合竞价结果.成交数量 > 0)) return false;
  const 中心 = 状态.结构?.中心订单簿;
  if (!中心 || 中心.最近更新 !== 状态.系统?.虚拟时间) return false;

  const 开盘成交量 = Number(集合竞价结果.成交数量 || 0);
  const 开盘成交额 = Number(集合竞价结果.成交金额 || 0);
  const 连续成交量 = Number(状态.变量.成交量 || 0);
  const 连续成交额 = Number(状态.变量.成交额 || 0);
  状态.变量.成交量 = 开盘成交量 + 连续成交量;
  状态.变量.成交额 = 保留数值(开盘成交额 + 连续成交额, 2);

  const 最新价格记录 = 状态.历史?.价格序列?.at(-1);
  if (最新价格记录) {
    最新价格记录.成交量 = 开盘成交量 + Number(最新价格记录.成交量 || 0);
    最新价格记录.成交额 = 保留数值(开盘成交额 + Number(最新价格记录.成交额 || 0), 2);
  }
  return true;
}
