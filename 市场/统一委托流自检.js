import { 创建中国黄金市场状态, 同步系统变量 } from './状态工厂.js';
import { 初始化交易制度状态 } from './制度执行器.js';
import { 执行自动中心订单簿 } from './中心订单簿.js';
import { 构建可见十档盘口, 读取统一委托流 } from './统一委托流.js';

function 断言(条件, 信息) {
  if (!条件) throw new Error(信息);
}

const 状态 = 创建中国黄金市场状态({
  随机种子: 2026062201,
  初始时间: '2026-06-22T01:30:00.000Z',
});
同步系统变量(状态);
初始化交易制度状态(状态);
状态.变量.基金交易价格 = 7;
状态.变量.基金理论净值 = 7;
状态.订单簿 = {
  买方: [
    { 档位: 1, 价格: 6.99, 数量: 1000 },
    { 档位: 2, 价格: 6.98, 数量: 800 },
  ],
  卖方: [
    { 档位: 1, 价格: 7.01, 数量: 1100 },
    { 档位: 2, 价格: 7.02, 数量: 900 },
  ],
  中间价: 7,
  上次成交价: 7,
  版本: 1,
};
状态.结构.玩家委托.待处理.push({
  编号: '玩家委托-测试',
  委托序号: 1,
  当前委托市场: '连续竞价',
  方向: '买入',
  价格: 6.98,
  数量: 200,
  已成交数量: 0,
  剩余数量: 200,
  状态: '待撮合',
});
状态.交易者.趋势资金.目标仓位 += 300;
状态.交易者.价值套利.目标仓位 -= 300;

const 原买一深度 = 状态.订单簿.买方[0].数量;
执行自动中心订单簿(状态, {
  价值套利: 300,
  趋势资金: 300,
  散户群体: 0,
  风险控制资金: 0,
  信息优势资金: 0,
});

const 流第一次 = 读取统一委托流(状态);
const 历史数量 = 流第一次.自动委托历史.length;
const 流第二次 = 读取统一委托流(状态);
断言(流第一次.自动委托.length >= 2, '中心订单簿应提供自动委托记录。');
断言(流第一次.自动委托.every((订单) => ['部分成交', '全部成交', '待撮合', '已拒绝'].includes(订单.状态)), '自动委托状态不合法。');
断言(流第二次.自动委托历史.length === 历史数量, '重复读取页面模型不得重复写入自动委托历史。');

const 可见盘口 = 构建可见十档盘口(状态);
const 玩家买二 = 可见盘口.买方.find((档位) => Math.abs(档位.价格 - 6.98) < 0.000001);
断言(玩家买二?.玩家挂单数量 === 200, '玩家挂单应叠加到可见买方深度。');
断言(玩家买二?.可成交数量 === 玩家买二?.做市数量 + 玩家买二?.自动挂单数量, '玩家自身挂单不得进入自身可成交数量。');
断言(可见盘口.买方[0].价格 < 可见盘口.卖方[0].价格, '可见买一必须低于可见卖一。');
断言(状态.订单簿.买方[0].数量 === 原买一深度, '可见深度层不得改写原始做市盘口。');

console.log(JSON.stringify({
  结果: '通过',
  自动委托数量: 流第一次.自动委托.length,
  可见买一: 可见盘口.买方[0],
  可见卖一: 可见盘口.卖方[0],
  玩家挂单数量: 可见盘口.汇总.玩家挂单数量,
}, null, 2));
