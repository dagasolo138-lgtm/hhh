import {
  初始化交易制度状态,
  刷新交易制度时钟,
  限制在当日价格边界,
  读取当日价格边界,
  记录当日成交,
} from './制度执行器.js';
import { 上交所黄金ETF交易制度 } from './交易制度参数.js';

function 保留(数值, 位数 = 6) {
  const 倍数 = 10 ** 位数;
  return Math.round(Number(数值 || 0) * 倍数) / 倍数;
}

function 取北京时间日期键(时间) {
  const 日期 = new Date(new Date(时间).getTime() + 8 * 60 * 60 * 1000);
  return `${日期.getUTCFullYear()}-${String(日期.getUTCMonth() + 1).padStart(2, '0')}-${String(日期.getUTCDate()).padStart(2, '0')}`;
}

function 对齐价格最小变动(价格) {
  const 最小变动 = 上交所黄金ETF交易制度.二级市场.价格最小变动;
  return 保留(Math.round(Number(价格) / 最小变动) * 最小变动, 3);
}

function 规范订单簿价格(状态) {
  const 订单簿 = 状态.订单簿;
  if (!订单簿?.买方?.length || !订单簿?.卖方?.length) return;

  const { 跌停价, 涨停价 } = 读取当日价格边界(状态);
  const 最小变动 = 上交所黄金ETF交易制度.二级市场.价格最小变动;
  const 中间价 = 限制在当日价格边界(状态, 状态.变量.基金交易价格);

  订单簿.买方 = 订单簿.买方.map((档位, 索引) => ({
    ...档位,
    价格: 对齐价格最小变动(Math.max(跌停价, Math.min(中间价 - 最小变动 * Math.max(1, 索引 + 1), 档位.价格))),
  })).sort((左, 右) => 右.价格 - 左.价格);

  订单簿.卖方 = 订单簿.卖方.map((档位, 索引) => ({
    ...档位,
    价格: 对齐价格最小变动(Math.min(涨停价, Math.max(中间价 + 最小变动 * Math.max(1, 索引 + 1), 档位.价格))),
  })).sort((左, 右) => 左.价格 - 右.价格);

  订单簿.中间价 = 中间价;
  订单簿.上次成交价 = 中间价;
  状态.变量.买方挂单深度 = 订单簿.买方.reduce((合计, 档位) => 合计 + Number(档位.数量 || 0), 0);
  状态.变量.卖方挂单深度 = 订单簿.卖方.reduce((合计, 档位) => 合计 + Number(档位.数量 || 0), 0);
}

function 修正最新市场记录(状态, 日志, 原价格, 修正后价格) {
  if (!日志 || 日志.类型 !== '市场推进') return;
  日志.交易价格 = 修正后价格;
  日志.价格收益 = 保留(Math.log(修正后价格 / Math.max(原价格, Number.EPSILON)) * 100, 4);
  日志.溢价折价 = 保留(
    (修正后价格 - 状态.变量.基金理论净值) / Math.max(状态.变量.基金理论净值, Number.EPSILON) * 100,
    4,
  );
  日志.摘要 = {
    ...(日志.摘要 || {}),
    交易价格: 修正后价格,
    溢价折价: 状态.变量.基金交易溢价折价,
  };
}

export function 进入市场推进前(状态) {
  初始化交易制度状态(状态);
  return 取北京时间日期键(状态.系统.虚拟时间);
}

export function 完成市场推进后(状态, 原日期键, 日志) {
  const 原价格 = 状态.历史?.价格序列?.at(-2)?.收盘 ?? 状态.变量.基金交易价格;
  刷新交易制度时钟(状态, 原日期键);

  const 修正前价格 = 状态.变量.基金交易价格;
  const 修正后价格 = 限制在当日价格边界(状态, 修正前价格);
  状态.变量.基金交易价格 = 修正后价格;
  状态.变量.基金交易价格收益 = 保留(
    Math.log(修正后价格 / Math.max(原价格, Number.EPSILON)),
    8,
  );
  状态.变量.基金交易溢价折价 = 保留(
    (修正后价格 - 状态.变量.基金理论净值) / Math.max(状态.变量.基金理论净值, Number.EPSILON),
    8,
  );

  const 最新价格记录 = 状态.历史?.价格序列?.at(-1);
  if (最新价格记录) {
    最新价格记录.收盘 = 修正后价格;
    最新价格记录.最高 = Math.max(最新价格记录.最高, 修正后价格);
    最新价格记录.最低 = Math.min(最新价格记录.最低, 修正后价格);
    最新价格记录.溢价折价 = 状态.变量.基金交易溢价折价;
  }

  规范订单簿价格(状态);
  记录当日成交(状态, 修正后价格, 状态.变量.成交量, 状态.变量.成交额);
  修正最新市场记录(状态, 日志, 原价格, 修正后价格);

  if (修正前价格 !== 修正后价格 && 日志) {
    日志.制度修正 = `交易价格触及当日价格边界，已限制为 ${修正后价格}。`;
  }

  return {
    前收盘价: 状态.结构.交易制度.上一交易日收盘价,
    涨停价: 状态.结构.交易制度.当日涨停价,
    跌停价: 状态.结构.交易制度.当日跌停价,
    连续竞价中: 状态.结构.交易制度.连续竞价中,
  };
}
