function 读取北京时间字段(时间) {
  const 日期 = new Date(new Date(时间).getTime() + 8 * 60 * 60 * 1000);
  return {
    年: 日期.getUTCFullYear(),
    月: 日期.getUTCMonth() + 1,
    日: 日期.getUTCDate(),
    星期: 日期.getUTCDay(),
    小时: 日期.getUTCHours(),
    分钟: 日期.getUTCMinutes(),
  };
}

export function 判定中国交易阶段(时间) {
  const 字段 = 读取北京时间字段(时间);
  const 周末 = 字段.星期 === 0 || 字段.星期 === 6;
  const 分钟数 = 字段.小时 * 60 + 字段.分钟;

  if (周末) return '休市';
  if (分钟数 >= 555 && 分钟数 < 570) return '集合竞价';
  if (分钟数 >= 570 && 分钟数 < 690) return '上午交易';
  if (分钟数 >= 690 && 分钟数 < 780) return '午间休市';
  if (分钟数 >= 780 && 分钟数 < 900) return '下午交易';
  if (分钟数 >= 900 && 分钟数 < 930) return '收盘后';
  return '休市';
}

export function 判定国际市场阶段(时间) {
  const 日期 = new Date(时间);
  const 星期 = 日期.getUTCDay();
  if (星期 === 0 || 星期 === 6) return '国际休市';

  const 分钟数 = 日期.getUTCHours() * 60 + 日期.getUTCMinutes();
  if (分钟数 >= 13 * 60 && 分钟数 < 17 * 60) return '重叠时段';
  if (分钟数 >= 8 * 60 && 分钟数 < 13 * 60) return '伦敦时段';
  if (分钟数 >= 17 * 60 && 分钟数 < 22 * 60) return '纽约时段';
  return '亚洲时段';
}

export function 是否中国市场可交易(阶段) {
  return 阶段 === '集合竞价' || 阶段 === '上午交易' || 阶段 === '下午交易';
}

