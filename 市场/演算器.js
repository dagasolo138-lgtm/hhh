  变量.买卖价差 = 有界(
    0.00045 + 变量.短期实现波动率 * 1.8 + 变量.信息不对称 * 0.004 + 变量.脆弱度 * 0.005 + Math.abs(库存偏离) * 0.0025,
    0.00035,
    0.08,
  );

  const 基础深度 = Math.max(500, 50000 * 变量.做市报价意愿 * (0.55 + 变量.注意力 * 0.45));
  const 买方倾斜 = 有界(1 - Math.max(0, 库存偏离) * 0.55, 0.22, 1.45);
  const 卖方倾斜 = 有界(1 + Math.max(0, 库存偏离) * 0.45, 0.22, 1.45);
  const 买方 = [];
  const 卖方 = [];

  for (let 档位 = 0; 档位 < 10; 档位 += 1) {
    const 距离 = 变量.买卖价差 / 2 + 档位 * (0.00016 + 变量.买卖价差 * 0.16);
    const 衰减 = Math.exp(-档位 * 0.17);
    买方.push({
      档位: 档位 + 1,
      价格: 价格取整(中间价 * (1 - 距离)),
      数量: Math.max(1, Math.round(基础深度 * 衰减 * 买方倾斜)),
    });
    卖方.push({
      档位: 档位 + 1,
      价格: 价格取整(中间价 * (1 + 距离)),
      数量: Math.max(1, Math.round(基础深度 * 衰减 * 卖方倾斜)),
    });
  }

  状态.订单簿 = {
    买方,
    卖方,
    中间价: 中间价,
    上次成交价: 状态.订单簿?.上次成交价 || 中间价,
    版本: (状态.订单簿?.版本 || 0) + 1,
  };
  变量.买方挂单深度 = 总和(买方.map((档位) => 档位.数量));
  变量.卖方挂单深度 = 总和(卖方.map((档位) => 档位.数量));
  状态.派生.开盘前流动性 = 有界((变量.买方挂单深度 + 变量.卖方挂单深度) / 160000, 0, 1);
