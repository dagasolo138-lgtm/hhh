function 读取事件通道(状态) {
  const 通道 = {
    实际利率: 0,
    美元: 0,
    地缘: 0,
    通胀: 0,
    国内流动性: 0,
    政策: 0,
    实物需求: 0,
    叙事: 0,
    热度: 0,
    泄露: 0,
  };

  for (const 事件 of 状态.事件.活跃) {
    const 权重 = 事件.剩余强度;
    for (const [名称, 影响] of Object.entries(事件.通道)) {
      if (名称 in 通道) 通道[名称] += 影响 * 权重;
    }
    if (!事件.已公开) 通道.泄露 = Math.max(通道.泄露, 事件.泄露程度 * 权重);
  }

  return 通道;
}

