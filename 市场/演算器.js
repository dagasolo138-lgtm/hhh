function 指数平滑(旧值, 目标值, 速度) {
  return 旧值 + (目标值 - 旧值) * 速度;
}

function 有界(数值, 最小值, 最大值) {
  return 限制数值(数值, 最小值, 最大值);
}

function 价格取整(价格) {
  return Math.max(0.001, 保留数值(价格, 3));
}

function 平均数(序列) {
  if (!序列.length) return 0;
  return 序列.reduce((合计, 数值) => 合计 + 数值, 0) / 序列.length;
}

function 总和(序列) {
  return 序列.reduce((合计, 数值) => 合计 + 数值, 0);
}

function 计算中国日期键(时间) {
  const 日期 = new Date(new Date(时间).getTime() + 8 * 60 * 60 * 1000);
  return `${日期.getUTCFullYear()}-${String(日期.getUTCMonth() + 1).padStart(2, '0')}-${String(日期.getUTCDate()).padStart(2, '0')}`;
}

