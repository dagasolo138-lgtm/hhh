function 计算技术指标(状态) {
  const 价格 = 状态.历史.价格序列.map((记录) => 记录.收盘);
  const 当前价 = 状态.变量.基金交易价格;
  const 序列 = [...价格, 当前价].slice(-80);
  const 均线 = (窗口) => 平均数(序列.slice(-窗口));
  const 短均线 = 均线(Math.min(6, 序列.length));
  const 中均线 = 均线(Math.min(18, 序列.length));
  const 长均线 = 均线(Math.min(48, 序列.length));
  const 动量 = 序列.length > 12 ? Math.log(当前价 / Math.max(序列[序列.length - 13], Number.EPSILON)) : 0;
  const 收益 = [];
  for (let 索引 = 1; 索引 < 序列.length; 索引 += 1) 收益.push(Math.log(序列[索引] / Math.max(序列[索引 - 1], Number.EPSILON)));
  const 平均收益 = 平均数(收益);
  const 标准差 = Math.sqrt(平均数(收益.map((数值) => (数值 - 平均收益) ** 2))) || 0.00001;
  const 相对位置 = (当前价 - 中均线) / Math.max(中均线 * 标准差 * 2, 0.00001);
  const 趋势信号 = 有界((短均线 - 中均线) / Math.max(中均线, Number.EPSILON) * 65 + 动量 * 28 + 相对位置 * 0.06, -1, 1);

  return {
    短均线: 保留数值(短均线, 6),
    中均线: 保留数值(中均线, 6),
    长均线: 保留数值(长均线, 6),
    动量: 保留数值(动量, 8),
    波动带相对位置: 保留数值(相对位置, 6),
    趋势信号: 保留数值(趋势信号, 6),
  };
}

function 限制订单到资金和持仓(参与者, 目标仓位, 参考价格) {
  const 目标 = 有界(目标仓位, 0, 参与者.最大绝对仓位);
  let 订单 = 有界(目标 - 参与者.持仓, -参与者.最大单刻调整, 参与者.最大单刻调整);

  if (订单 > 0) {
    const 可买数量 = Math.floor(参与者.现金 / Math.max(参考价格, 0.001));
    订单 = Math.min(订单, 可买数量);
  } else {
    订单 = Math.max(订单, -参与者.持仓);
  }
  return Math.trunc(订单);
}

