export function 创建上一刻观测(变量 = {}, 派生 = {}) {
  const 观测 = {};
  for (const 字段 of 上一刻观测字段) {
    if (字段 in 派生) 观测[字段] = 结构化拷贝(派生[字段]);
    else 观测[字段] = 结构化拷贝(变量[字段]);
  }
  return Object.freeze(观测);
}

export function 读取上一刻观测(状态) {
  if (状态?.结构?.上一刻观测) return 状态.结构.上一刻观测;
  return 创建上一刻观测(状态?.变量 || {}, 状态?.派生 || {});
}

export function 写入上一刻观测(状态) {
  状态.结构 ??= {};
  状态.结构.上一刻观测 = 创建上一刻观测(状态.变量, {
    ...状态.派生,
    持仓成本分布: 状态.结构.持仓成本分布,
  });
  return 状态.结构.上一刻观测;
}

function 结构化拷贝(值) {
  if (值 === undefined) return null;
  if (值 === null || typeof 值 !== 'object') return 值;
  return JSON.parse(JSON.stringify(值));
}

