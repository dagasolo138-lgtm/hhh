function 保留(数值, 位数 = 6) {
  const 倍数 = 10 ** 位数;
  return Math.round(Number(数值 || 0) * 倍数) / 倍数;
}

function 读取状态名称(状态) {
  const 变量 = 状态.变量;
  if (变量.脆弱度 > 0.64 || 变量.订单簿脆弱度 > 0.7) return '紧张';
  if (变量.基金交易价格收益 > 0.0012 && 变量.乐观度 > 0.52) return '上行';
  if (变量.基金交易价格收益 < -0.0012 && 变量.脆弱度 > 0.36) return '下行';
  return '平稳';
}

function 资金摘要(记录) {
  const 订单 = 记录.订单 || {};
  return {
    价值资金: Math.round(订单.价值套利 || 0),
    趋势资金: Math.round(订单.趋势资金 || 0),
    噪声资金: Math.round(订单.散户群体 || 0),
    风控资金: Math.round(订单.风险控制资金 || 0),
    做市资金: 0,
  };
}

function 转换市场日志(状态, 记录, 上一价格) {
  const 变量 = 状态.变量;

  if (记录.类型 === '玩家成交') {
    return {
      序号: 记录.序号,
      时间: 记录.时间,
      类型: '玩家成交',
      方向: 记录.方向,
      数量: 记录.数量,
      成交价: 记录.成交价,
      成交金额: 记录.成交金额,
      价格冲击: 记录.价格冲击,
      上次价格: 记录.上次价格,
      最新价格: 记录.最新价格,
      涨跌幅: 记录.涨跌幅,
      状态: 读取状态名称(状态),
      说明: 记录.说明,
    };
  }

  if (记录.类型 === '玩家委托') {
    return {
      序号: 记录.序号,
      时间: 记录.时间,
      类型: '玩家委托',
      动作: 记录.动作,
      编号: 记录.编号,
      方向: 记录.方向,
      委托价: 记录.价格,
      数量: 记录.数量,
      成交价: 记录.成交价 ?? 0,
      成交金额: 记录.成交金额 ?? 0,
      剩余数量: 记录.剩余数量,
      委托状态: 记录.状态,
      上次价格,
      最新价格: 变量.基金交易价格,
      涨跌幅: 0,
      状态: 读取状态名称(状态),
      说明: 记录.说明,
    };
  }

  if (记录.类型 === '公开事件') {
    return {
      序号: 记录.序号 ?? 0,
      时间: 记录.时间,
      类型: '市场波动',
      上次价格,
      最新价格: 变量.基金交易价格,
      涨跌幅: 0,
      状态: 读取状态名称(状态),
      内在价值: 变量.基金理论净值,
      价值偏离: 保留(变量.基金交易溢价折价 * 100, 4),
      波动率: 保留(变量.短期实现波动率 * 100, 4),
      流动性: 保留(状态.派生.本地流动性状态, 4),
      成交量: 0,
      买卖价差: 保留(变量.买卖价差 * 100, 4),
      资金: { 价值资金: 0, 趋势资金: 0, 噪声资金: 0, 风控资金: 0, 做市资金: 0 },
      事件: { 名称: 记录.名称, 方向: 记录.方向, 强度: 记录.强度 },
      构成: { 价值回归: 0, 趋势跟随: 0, 订单冲击: 0, 随机波动: 0, 事件冲击: 0, 合计: 0 },
      说明: `公开事件：${记录.名称}。`,
    };
  }

  const 交易价格 = Number(记录.交易价格 ?? 变量.基金交易价格);
  const 涨跌幅 = Number(记录.价格收益 ?? 0);
  const 订单冲击 = Number(记录.订单失衡 ?? 0) * Number(变量.价格冲击系数 ?? 0);
  const 国际映射 = Number(记录.国际黄金收益 ?? 0) * 0.45 + Number(记录.汇率收益 ?? 0) * 0.32;
  const 净值回归 = -Number(记录.溢价折价 ?? 0) * 0.08;

  return {
    序号: 记录.序号,
    时间: 记录.时间,
    类型: '市场波动',
    上次价格,
    最新价格: 交易价格,
    涨跌幅,
    状态: 读取状态名称(状态),
    内在价值: Number(记录.理论净值 ?? 变量.基金理论净值),
    价值偏离: Number(记录.溢价折价 ?? 0),
    波动率: 保留(变量.短期实现波动率 * 100, 4),
    流动性: 保留(状态.派生.本地流动性状态, 4),
    成交量: Number(记录.成交量 ?? 0),
    买卖价差: Number(记录.买卖价差 ?? 0),
    资金: 资金摘要(记录),
    事件: 记录.事件?.length ? { 名称: 记录.事件.map((事件) => 事件.名称).join('、'), 方向: 1, 强度: 0 } : null,
    构成: {
      价值回归: 保留(净值回归 / 100, 8),
      趋势跟随: 保留((状态.变量.技术指标集合?.趋势信号 || 0) * 0.0002, 8),
      订单冲击: 保留(订单冲击, 8),
      随机波动: 保留(涨跌幅 / 100 - 国际映射 / 100 - 订单冲击, 8),
      事件冲击: 保留((状态.变量.事件叙事方向 || 0) * 0.0004, 8),
      合计: 保留(涨跌幅 / 100, 8),
    },
    说明: `国际黄金、人民币汇率、国内溢价与订单簿共同形成这一刻的交易价格。`,
  };
}

