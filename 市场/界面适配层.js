export function 读取界面模型(状态) {
  const 变量 = 状态.变量;
  const 账户 = 读取玩家账户(状态);
  const 委托 = 读取玩家限价委托(状态);
  const 价格历史 = 状态.历史.价格序列;
  const 上一价格 = 价格历史.length > 1 ? 价格历史.at(-2).收盘 : 变量.基金交易价格;
  const 买一 = 状态.订单簿?.买方?.[0]?.价格 ?? 变量.基金交易价格;
  const 卖一 = 状态.订单簿?.卖方?.[0]?.价格 ?? 变量.基金交易价格;
  const 制度 = 状态.结构?.交易制度 || {};

  const 日志 = [];
  let 日志上一价格 = 价格历史[0]?.收盘 ?? 变量.基金交易价格;
  for (const 记录 of 状态.历史.日志) {
    const 转换后 = 转换市场日志(状态, 记录, 日志上一价格);
    日志.push(转换后);
    if (转换后.最新价格) 日志上一价格 = 转换后.最新价格;
  }

  return Object.freeze({
    市场: {
      名称: '中国黄金',
      价格: 变量.基金交易价格,
      上次价格: 上一价格,
      内在价值: 变量.基金理论净值,
      状态: 读取状态名称(状态),
      瞬时波动率: 变量.短期实现波动率,
      流动性: 状态.派生.本地流动性状态,
      买卖价差: 变量.买卖价差,
      买一价: 买一,
      卖一价: 卖一,
      国内黄金现货价格: 变量.国内黄金现货价格,
      人民币换算金价: 变量.人民币换算金价,
      国际现货黄金价格: 变量.国际现货黄金价格,
      美元兑人民币: 变量.美元兑人民币,
      溢价折价: 变量.基金交易溢价折价,
      前收盘价: 制度.上一交易日收盘价 ?? null,
      涨停价: 制度.当日涨停价 ?? null,
      跌停价: 制度.当日跌停价 ?? null,
      连续竞价中: Boolean(制度.连续竞价中),
    },
    用户: {
      现金: 账户.现金,
      持仓: 账户.持仓,
      持仓成本: 账户.持仓成本,
      累计已实现盈亏: Number(变量.玩家累计已实现盈亏 || 0),
    },
    账户,
    委托,
    日志,
    原始摘要: 读取市场摘要(状态),
  });
}

export function 创建智能分析资料(状态, 最近条数 = 80) {
  const 界面模型 = 读取界面模型(状态);
  return Object.freeze({
    市场摘要: 界面模型.原始摘要,
    本地映射: {
      国际现货黄金价格: 状态.变量.国际现货黄金价格,
      美元兑人民币: 状态.变量.美元兑人民币,
      人民币换算金价: 状态.变量.人民币换算金价,
      国内黄金现货价格: 状态.变量.国内黄金现货价格,
      基金理论净值: 状态.变量.基金理论净值,
      基金交易溢价折价: 状态.变量.基金交易溢价折价,
    },
    订单簿: {
      买方前五档: stateSafeSlice(状态.订单簿?.买方, 5),
      卖方前五档: stateSafeSlice(状态.订单簿?.卖方, 5),
      买卖价差: 状态.变量.买卖价差,
      订单簿脆弱度: 状态.变量.订单簿脆弱度,
    },
    玩家账户: 界面模型.账户,
    玩家待处理委托: 界面模型.委托.待处理,
    最近波动日志: 界面模型.日志.slice(-最近条数),
    约束: '分析只能解释公开市场资料、订单状态和波动日志，不能修改市场状态、随机种子、订单簿或账户。',
  });
}

function stateSafeSlice(序列, 数量) {
  return Array.isArray(序列) ? 序列.slice(0, 数量).map((项) => ({ ...项 })) : [];
}
