import { 验证状态片段 } from '../变量字典.js';
import { 检查执行时序 } from './时序校验.js';

function 接近(左, 右, 容差 = 0.000001) {
  return Math.abs(左 - 右) <= 容差;
}

function 求和(对象, 字段) {
  return Object.values(对象).reduce((合计, 项) => 合计 + Number(项[字段] || 0), 0);
}

function 检查订单簿(订单簿, 错误) {
  if (!订单簿 || !Array.isArray(订单簿.买方) || !Array.isArray(订单簿.卖方)) {
    错误.push('订单簿结构不存在或不完整。');
    return;
  }
  if (订单簿.买方.length === 0 || 订单簿.卖方.length === 0) {
    错误.push('订单簿至少应保留一档买方和卖方报价。');
    return;
  }
  for (const 档位 of [...订单簿.买方, ...订单簿.卖方]) {
    if (!Number.isFinite(档位.价格) || 档位.价格 <= 0) 错误.push('订单簿存在非法价格。');
    if (!Number.isFinite(档位.数量) || 档位.数量 < 0) 错误.push('订单簿存在非法深度。');
  }
  for (let 索引 = 1; 索引 < 订单簿.买方.length; 索引 += 1) {
    if (订单簿.买方[索引 - 1].价格 < 订单簿.买方[索引].价格) {
      错误.push('买方订单簿价格排序错误。');
      break;
    }
  }
  for (let 索引 = 1; 索引 < 订单簿.卖方.length; 索引 += 1) {
    if (订单簿.卖方[索引 - 1].价格 > 订单簿.卖方[索引].价格) {
      错误.push('卖方订单簿价格排序错误。');
      break;
    }
  }
  if (订单簿.买方[0].价格 >= 订单簿.卖方[0].价格) {
    错误.push('订单簿买一价不得高于或等于卖一价。');
  }
}

export function 检查市场一致性(状态) {
  const 错误 = [];
  const 警告 = [];
  if (!状态 || typeof 状态 !== 'object') {
    return Object.freeze({ 有效: false, 错误: ['市场状态不存在。'], 警告: [] });
  }

  const 时序校验 = 检查执行时序();
  if (!时序校验.有效) 错误.push(...时序校验.错误);

  const 变量校验 = 验证状态片段(状态.变量 || {});
  if (!变量校验.有效) 错误.push(...变量校验.错误);
  if (!状态.系统 || !Number.isInteger(状态.系统.演算序号) || 状态.系统.演算序号 < 0) {
    错误.push('演算序号必须是非负整数。');
  }
  if (!Number.isFinite(状态?.系统?.随机种子)) 错误.push('随机种子不合法。');
  if (!Number.isFinite(Date.parse(状态?.系统?.虚拟时间))) 错误.push('虚拟时间不合法。');

  const 变量 = 状态.变量 || {};
  const 预期人民币换算金价 = 变量.国际现货黄金价格 * 变量.美元兑人民币 / 31.1034768;
  if (Number.isFinite(预期人民币换算金价) && !接近(变量.人民币换算金价, 预期人民币换算金价, 0.00002)) {
    错误.push('人民币换算金价与国际金价、汇率换算关系不一致。');
  }
  const 预期国内现货 = 变量.人民币换算金价 * (1 + 变量.国内黄金溢价);
  if (Number.isFinite(预期国内现货) && !接近(变量.国内黄金现货价格, 预期国内现货, 0.00002)) {
    错误.push('国内黄金现货价格与换算价、国内溢价关系不一致。');
  }
  const 预期净值 = 变量.国内黄金现货价格 * 变量.每份黄金含量 * (1 + 变量.跟踪误差) * (1 - 变量.管理费拖累);
  if (Number.isFinite(预期净值) && !接近(变量.基金理论净值, 预期净值, 0.00002)) {
    错误.push('基金理论净值与底层黄金换算关系不一致。');
  }
  const 预期溢价折价 = (变量.基金交易价格 - 变量.基金理论净值) / Math.max(变量.基金理论净值, Number.EPSILON);
  if (Number.isFinite(预期溢价折价) && !接近(变量.基金交易溢价折价, 预期溢价折价, 0.00002)) {
    错误.push('基金交易溢价折价计算不一致。');
  }
  if (变量.基金交易价格 <= 0 || 变量.基金理论净值 <= 0) {
    错误.push('基金交易价格与基金理论净值必须大于零。');
  }

  检查订单簿(状态.订单簿, 错误);
  if (!状态.交易者 || typeof 状态.交易者 !== 'object') {
    错误.push('交易者集合不存在。');
  } else {
    for (const [标识, 参与者] of Object.entries(状态.交易者)) {
      if (!Number.isFinite(参与者.现金)) 错误.push(`${标识} 的现金不合法。`);
      if (!Number.isFinite(参与者.持仓)) 错误.push(`${标识} 的持仓不合法。`);
      if (!Number.isFinite(参与者.目标仓位)) 错误.push(`${标识} 的目标仓位不合法。`);
      if (参与者.现金 < -0.01) 错误.push(`${标识} 的现金不得为负。`);
      if (参与者.持仓 < -0.0001) 错误.push(`${标识} 的持仓不得为负。`);
      if (Math.abs(参与者.持仓) > 参与者.最大绝对仓位 * 1.15) {
        警告.push(`${标识} 的持仓接近或超出允许范围。`);
      }
    }
  }

  const 玩家现金 = Number(变量.玩家现金 || 0);
  const 玩家持仓 = Number(变量.玩家持仓 || 0);
  if (玩家现金 < -0.01) 错误.push('玩家现金不得为负。');
  if (玩家持仓 < -0.0001) 错误.push('玩家持仓不得为负。');

  if (状态.系统?.守恒基线 && 状态.交易者) {
    const 当前现金 = 求和(状态.交易者, '现金') + 玩家现金;
    const 当前份额 = 求和(状态.交易者, '持仓') + 玩家持仓;
    if (!接近(当前现金, 状态.系统.守恒基线.现金, 0.1)) {
      错误.push('交易者与玩家现金总额未守恒。');
    }
    if (!接近(当前份额, 状态.系统.守恒基线.份额, 0.0001)) {
      错误.push('交易者与玩家基金份额总额未守恒。');
    }
  } else {
    警告.push('尚未设置现金与份额守恒基线。');
  }

  if (!Array.isArray(状态?.历史?.日志)) 错误.push('波动日志容器不存在。');
  if (!Array.isArray(状态?.历史?.价格序列)) 错误.push('价格历史容器不存在。');

  return Object.freeze({
    有效: 错误.length === 0,
    错误,
    警告,
    演算序号: 状态?.系统?.演算序号 ?? null,
  });
}

export function 断言市场一致性(状态) {
  const 结果 = 检查市场一致性(状态);
  if (!结果.有效) {
    throw new Error(`市场一致性检查失败：${结果.错误.join('；')}`);
  }
  return 结果;
}
