import {
  初始化交易制度状态,
  可用玩家持仓,
  读取当日价格边界,
  验证玩家二级市场订单,
} from './制度执行器.js';
import { 执行玩家中心市价单 } from './中心订单簿.js';
import { 刷新中心盘口快照 } from './中心盘口快照.js';

function 取整数量(数量) {
  return Math.max(0, Math.floor(Number(数量) || 0));
}

function 写入玩家成交日志(状态, 内容) {
  状态.历史.日志.push({
    时间: 状态.系统.虚拟时间,
    类型: '玩家成交',
    序号: 状态.系统.演算序号,
    ...内容,
  });
  if (状态.历史.日志.length > 状态.结构.日志上限) {
    状态.历史.日志.splice(0, 状态.历史.日志.length - 状态.结构.日志上限);
  }
}

export function 读取玩家账户(状态) {
  初始化交易制度状态(状态);
  const 变量 = 状态.变量;
  const 冻结 = 状态.结构.玩家冻结资产;
  const 市值 = 保留数值(变量.玩家持仓 * 变量.基金交易价格, 2);
  const 浮动盈亏 = 保留数值((变量.基金交易价格 - 变量.玩家持仓成本) * 变量.玩家持仓, 2);
  const 总资产 = 保留数值(变量.玩家现金 + 市值, 2);
  return Object.freeze({
    现金: 保留数值(变量.玩家现金, 2),
    可用现金: 保留数值(变量.玩家现金 - 冻结.现金, 2),
    冻结现金: 保留数值(冻结.现金, 2),
    持仓: 变量.玩家持仓,
    可用持仓: Math.max(0, 变量.玩家持仓 - 冻结.份额),
    冻结持仓: 冻结.份额,
    持仓成本: 保留数值(变量.玩家持仓成本, 6),
    持仓市值: 市值,
    浮动盈亏,
    总资产,
    总盈亏: 保留数值(总资产 - 100000, 2),
  });
}

export function 玩家市价成交(状态, 方向, 请求数量) {
  初始化交易制度状态(状态);
  const 数量 = 取整数量(请求数量);
  const 校验 = 验证玩家二级市场订单(状态, 方向, 数量);
  if (!校验.有效) return Object.freeze({ 成功: false, 原因: 校验.原因 });
  if (方向 === '卖出' && 可用玩家持仓(状态) < 数量) {
    return Object.freeze({ 成功: false, 原因: '可卖持仓不足，部分份额可能已被冻结在未成交委托中。' });
  }

  const 原价格 = 状态.变量.基金交易价格;
  const 中心结果 = 执行玩家中心市价单(状态, 方向, 数量);
  // 市价单未成交残余会从中心订单列表移除；必须同时重建快照，避免十档出现失效深度。
  刷新中心盘口快照(状态);
  if (!中心结果.成功) {
    return Object.freeze({ 成功: false, 原因: 方向 === '买入' ? '可用现金不足或中心卖方深度不足。' : '中心买方深度不足。' });
  }

  const 边界 = 读取当日价格边界(状态);
  const 结果 = Object.freeze({
    成功: true,
    方向,
    请求数量: 数量,
    成交数量: 中心结果.成交数量,
    未成交数量: 中心结果.未成交数量,
    成交价: 中心结果.平均成交价,
    成交金额: 中心结果.成交金额,
    原价格,
    最新价格: 中心结果.最新价格,
    涨停价: 边界.涨停价,
    跌停价: 边界.跌停价,
    成交笔数: 中心结果.新增成交.length,
  });

  写入玩家成交日志(状态, {
    方向,
    数量: 结果.成交数量,
    请求数量: 数量,
    未成交数量: 结果.未成交数量,
    成交价: 结果.成交价,
    成交金额: 结果.成交金额,
    成交笔数: 结果.成交笔数,
    上次价格: 原价格,
    最新价格: 结果.最新价格,
    涨跌幅: 保留数值((结果.最新价格 / Math.max(原价格, Number.EPSILON) - 1) * 100, 4),
    涨停价: 结果.涨停价,
    跌停价: 结果.跌停价,
    说明: `${方向}${结果.成交数量}份中国黄金已通过中心订单簿成交，成交均价 ${结果.成交价}。`,
  });

  写入上一刻观测(状态);
  断言市场一致性(状态);
  return 结果;
}
