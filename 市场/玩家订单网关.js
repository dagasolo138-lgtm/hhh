import {
  提交玩家限价委托,
  撤销玩家限价委托,
  读取玩家限价委托,
  使跨日玩家委托失效,
} from './玩家限价委托.js';
import {
  提交玩家集合竞价委托,
  撤销玩家集合竞价委托,
  读取玩家集合竞价委托,
  使跨日集合竞价委托失效,
} from './集合竞价.js';
import { 初始化交易制度状态, 是否允许撤销玩家委托 } from './制度执行器.js';

function 合并统计(连续, 集合) {
  const 统计 = {
    待撮合: 0,
    部分成交: 0,
    全部成交: 0,
    已撤销: 0,
    已拒绝: 0,
    已过期: 0,
  };
  for (const 来源 of [连续.状态统计 || {}, ...[集合.待处理, 集合.历史].map((订单列表) => {
    const 局部 = {};
    for (const 订单 of 订单列表 || []) 局部[订单.状态] = Number(局部[订单.状态] || 0) + 1;
    return 局部;
  })]) {
    for (const 状态 of Object.keys(统计)) 统计[状态] += Number(来源[状态] || 0);
  }
  return 统计;
}

function 标注来源(订单, 当前委托市场) {
  return { ...订单, 当前委托市场: 订单.当前委托市场 || 当前委托市场 };
}

export function 提交玩家限价订单(状态, 方向, 数量, 价格) {
  初始化交易制度状态(状态);
  if (状态.系统.盘中阶段 === '集合竞价') {
    return 提交玩家集合竞价委托(状态, 方向, 数量, 价格);
  }
  return 提交玩家限价委托(状态, 方向, 数量, 价格);
}

export function 撤销玩家限价订单(状态, 编号) {
  初始化交易制度状态(状态);
  const 集合订单 = 状态.结构.集合竞价.待处理.find((订单) => 订单.编号 === 编号);
  if (集合订单) return 撤销玩家集合竞价委托(状态, 编号);

  const 连续订单 = 状态.结构.玩家委托.待处理.find((订单) => 订单.编号 === 编号);
  if (!连续订单) return Object.freeze({ 成功: false, 原因: '未找到可撤销的待处理委托。' });
  const 校验 = 是否允许撤销玩家委托(状态, '连续竞价');
  if (!校验.有效) return Object.freeze({ 成功: false, 原因: 校验.原因 });
  return 撤销玩家限价委托(状态, 编号);
}

export function 使跨日玩家全部委托失效(状态, 当前日期键) {
  const 连续 = 使跨日玩家委托失效(状态, 当前日期键);
  const 集合 = 使跨日集合竞价委托失效(状态, 当前日期键);
  return Object.freeze({
    失效数量: 连续.失效数量 + 集合.失效数量,
    编号列表: [...连续.编号列表, ...集合.编号列表],
  });
}

export function 读取玩家全部限价委托(状态) {
  初始化交易制度状态(状态);
  const 连续 = 读取玩家限价委托(状态);
  const 集合 = 读取玩家集合竞价委托(状态);
  const 待处理 = [
    ...集合.待处理.map((订单) => 标注来源(订单, '集合竞价')),
    ...连续.待处理.map((订单) => 标注来源(订单, '连续竞价')),
  ].sort((左, 右) => leftTime(左) - leftTime(右));
  const 历史 = [
    ...集合.历史.map((订单) => 标注来源(订单, '集合竞价')),
    ...连续.历史.map((订单) => 标注来源(订单, '连续竞价')),
  ].sort((左, 右) => leftTime(右) - leftTime(左)).slice(0, 100);

  return Object.freeze({
    待处理,
    历史,
    状态统计: 合并统计(连续, 集合),
    集合竞价: { 最近结果: 集合.最近结果 },
  });
}

function leftTime(订单) {
  return new Date(订单.委托时间 || 0).getTime();
}
