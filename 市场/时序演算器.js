function 求和交易者(交易者, 字段) {
  return Object.values(交易者).reduce((合计, 参与者) => 合计 + Number(参与者[字段] || 0), 0);
}

function 确保完整守恒基线(状态) {
  if (状态.系统.守恒基线) return;
  状态.系统.守恒基线 = {
    现金: 求和交易者(状态.交易者, '现金') + Number(状态.变量.玩家现金 || 0),
    份额: 求和交易者(状态.交易者, '持仓') + Number(状态.变量.玩家持仓 || 0),
  };
}

function 确保上一刻观测(状态) {
  状态.结构 ??= {};
  状态.结构.上一刻观测 ??= 创建上一刻观测(状态.变量, {
    ...状态.派生,
    持仓成本分布: 状态.结构.持仓成本分布,
  });
}

function 同步开盘日志(状态, 日志, 集合竞价开盘) {
  if (!集合竞价开盘?.已执行 || !(集合竞价开盘.开盘价 > 0)) return;
  日志.交易价格 = 状态.变量.基金交易价格;
  日志.理论净值 = 状态.变量.基金理论净值;
  日志.溢价折价 = Math.round(状态.变量.基金交易溢价折价 * 1000000) / 10000;
  日志.成交量 = 状态.变量.成交量;
  日志.摘要 = {
    ...(日志.摘要 || {}),
    交易价格: 状态.变量.基金交易价格,
    开盘集合竞价: {
      开盘价: 集合竞价开盘.开盘价,
      成交数量: 集合竞价开盘.成交数量,
      成交金额: 集合竞价开盘.成交金额,
    },
  };
}

export function 推进时序中国黄金市场(状态) {
  确保完整守恒基线(状态);
  初始化交易制度状态(状态);
  确保上一刻观测(状态);

  const 原日期键 = 进入市场推进前(状态);
  const 读取时使用的观测 = 状态.结构.上一刻观测;
  const 日志 = 推进中心撮合市场(状态);
  const 跨日失效 = 使跨日玩家全部委托失效(状态, 状态.系统.当前中国日期键);
  const 制度摘要 = 完成市场推进后(状态, 原日期键, 日志);
  const 集合竞价开盘 = 执行开盘集合竞价(状态);
  const 玩家限价撮合 = 撮合玩家限价委托(状态);
  const 自动委托流 = 同步统一委托流(状态);

  同步开盘日志(状态, 日志, 集合竞价开盘);
  日志.上一刻观测 = 读取时使用的观测;
  日志.交易制度 = 制度摘要;
  日志.跨日失效委托 = 跨日失效;
  日志.集合竞价开盘 = 集合竞价开盘;
  日志.玩家限价撮合 = 玩家限价撮合;
  日志.自动委托流 = 自动委托流.本刻自动委托;
  写入上一刻观测(状态);
  断言交易制度一致性(状态);
  return 日志;
}

export function 连续推进时序中国黄金市场(状态, 次数 = 1) {
  const 日志列表 = [];
  for (let 序号 = 0; 序号 < 次数; 序号 += 1) {
    日志列表.push(推进时序中国黄金市场(状态));
  }
  return 日志列表;
}

