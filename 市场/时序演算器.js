import { 推进中国黄金市场 } from './演算器.js';
import { 创建上一刻观测, 写入上一刻观测 } from './时序因果图.js';
import { 进入市场推进前, 完成市场推进后 } from './制度收尾.js';
import { 初始化交易制度状态 } from './制度执行器.js';
import { 断言交易制度一致性 } from './制度一致性检查.js';
import { 撮合玩家限价委托, 使跨日玩家委托失效 } from './玩家限价委托.js';

function 求和交易者(交易者, 字段) {
  return Object.values(交易者).reduce((合计, 参与者) => 合计 + Number(参与者[字段] || 0), 0);
}

function 确保完整守恒基线(状态) {
  if (状态.系统.守恒基线) return;
  状态.系统.守恒基线 = {
    现金: 求和交易者(状态.交易者, '现金') + Number(状态.变量.玩家现金 || 0),
    份额: 求和交易者(状态.交易者, '持仓') + Number(状态.变量.玩家持仓 || 0),
  };
}

function 确保上一刻观测(状态) {
  状态.结构 ??= {};
  状态.结构.上一刻观测 ??= 创建上一刻观测(状态.变量, {
    ...状态.派生,
    持仓成本分布: 状态.结构.持仓成本分布,
  });
}

export function 推进时序中国黄金市场(状态) {
  确保完整守恒基线(状态);
  初始化交易制度状态(状态);
  确保上一刻观测(状态);

  const 原日期键 = 进入市场推进前(状态);
  const 读取时使用的观测 = 状态.结构.上一刻观测;
  const 日志 = 推进中国黄金市场(状态);
  const 制度摘要 = 完成市场推进后(状态, 原日期键, 日志);
  const 跨日失效 = 使跨日玩家委托失效(状态, 状态.结构.交易制度.当前交易日);
  const 玩家限价撮合 = 撮合玩家限价委托(状态);

  日志.上一刻观测 = 读取时使用的观测;
  日志.交易制度 = 制度摘要;
  日志.跨日失效委托 = 跨日失效;
  日志.玩家限价撮合 = 玩家限价撮合;
  写入上一刻观测(状态);
  断言交易制度一致性(状态);
  return 日志;
}

export function 连续推进时序中国黄金市场(状态, 次数 = 1) {
  const 日志列表 = [];
  for (let 序号 = 0; 序号 < 次数; 序号 += 1) {
    日志列表.push(推进时序中国黄金市场(状态));
  }
  return 日志列表;
}

export function 初始化时序市场(状态) {
  确保完整守恒基线(状态);
  初始化交易制度状态(状态);
  确保上一刻观测(状态);
  断言交易制度一致性(状态);
  return 状态;
}
