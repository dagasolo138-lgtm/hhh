import { 创建新市场, 读取界面数据, 玩家限价交易 } from '../核心.js';
import { 创建中国黄金市场状态 } from './状态工厂.js';
import { 初始化交易制度状态 } from './制度执行器.js';
import { 刷新自动开盘竞价订单 } from './自动开盘订单刷新.js';

function 断言(条件, 信息) {
  if (!条件) throw new Error(信息);
}

// 覆盖页面实际入口：新世界在09:15必须显示集合竞价，并把玩家限价单送入集合订单池。
const 页面状态 = 创建新市场(2026062291);
const 初始模型 = 读取界面数据(页面状态);
断言(初始模型.市场.集合竞价中, '新世界09:15必须显示集合竞价状态。');
const 集合结果 = 玩家限价交易(页面状态, '买入', 100, 初始模型.市场.涨停价);
断言(集合结果.成功, '09:15玩家集合竞价限价单提交失败。');
断言(页面状态.结构.集合竞价.待处理.some((订单) => 订单.编号 === 集合结果.订单.编号), '09:15订单未进入集合竞价容器。');
断言(!页面状态.结构.玩家委托.待处理.some((订单) => 订单.编号 === 集合结果.订单.编号), '09:15订单错误进入连续竞价容器。');

// 覆盖自动订单刷新：09:15可刷新，09:20按最新目标仓位锁定。
const 自动状态 = 创建中国黄金市场状态({ 初始时间: '2026-06-22T01:15:00.000Z', 随机种子: 2026062292 });
初始化交易制度状态(自动状态);
自动状态.交易者.价值套利.目标仓位 = 自动状态.交易者.价值套利.持仓 + 100;
刷新自动开盘竞价订单(自动状态);
const 初始自动订单 = 自动状态.结构.集合竞价.自动订单.find((订单) => 订单.参与者标识 === '价值套利');
断言(初始自动订单?.数量 === 100, '09:15自动开盘订单未按目标仓位生成。');
自动状态.交易者.价值套利.目标仓位 = 自动状态.交易者.价值套利.持仓 + 200;
autoSetTime(自动状态, '2026-06-22T01:20:00.000Z');
初始化交易制度状态(自动状态);
刷新自动开盘竞价订单(自动状态);
const 锁定自动订单 = 自动状态.结构.集合竞价.自动订单.find((订单) => 订单.参与者标识 === '价值套利');
断言(锁定自动订单?.数量 === 200, '09:20自动开盘订单未按最新目标仓位刷新。');
断言(自动状态.结构.集合竞价.自动订单锁定, '09:20自动开盘订单必须锁定。');

function autoSetTime(状态, 时间) {
  状态.系统.虚拟时间 = 时间;
}

console.log(JSON.stringify({
  结果: '通过',
  页面集合订单: 集合结果.订单.编号,
  自动锁定数量: 锁定自动订单.数量,
}, null, 2));
