export const 变量字典 = Object.freeze(
  Object.fromEntries(变量列表.map((变量) => [变量.标识, 变量])),
);

export function 读取变量定义(标识) {
  return 变量字典[标识] || null;
}

export function 按条件列出变量(条件 = {}) {
  return 变量列表.filter((变量) => Object.entries(条件).every(([键, 值]) => 变量[键] === 值));
}

export function 可公开给玩家的变量() {
  return 变量列表.filter((变量) => [可见性.公开, 可见性.延迟公开].includes(变量.可见性));
}

export function 可提供给智能分析的变量() {
  return 变量列表.filter((变量) => 变量.可见性 !== 可见性.仅系统 && 变量.标识 !== '随机种子');
}

export function 验证状态片段(状态片段 = {}) {
  const 错误 = [];

  for (const [标识, 值] of Object.entries(状态片段)) {
    const 定义 = 变量字典[标识];
    if (!定义) {
      错误.push(`${标识} 未在变量字典登记。`);
      continue;
    }

    if (定义.数据类型 === '数值' || 定义.数据类型 === '整数') {
      if (!Number.isFinite(值)) 错误.push(`${标识} 必须是有限数值。`);
      if (定义.数据类型 === '整数' && !Number.isInteger(值)) 错误.push(`${标识} 必须是整数。`);
      if (定义.最小值 !== null && 值 < 定义.最小值) 错误.push(`${标识} 低于最小值。`);
      if (定义.最大值 !== null && 值 > 定义.最大值) 错误.push(`${标识} 高于最大值。`);
    }

    if (定义.数据类型 === '枚举' && !定义.可选值.includes(值)) {
      错误.push(`${标识} 不在允许状态中。`);
    }
  }

  return Object.freeze({ 有效: 错误.length === 0, 错误 });
}

