function 推进时间(状态) {
  const 当前 = new Date(状态.市场时间);
  当前.setMinutes(当前.getMinutes() + 5);
  状态.市场时间 = 当前.toISOString();
}

function 生成资金行为(状态, 偏离率, 波动率) {
  const 市场 = 状态.市场;
  const 噪声 = 正态随机数(状态);
  const 库存压力 = 市场.做市库存 / 18000;

  const 价值资金 = 限制(偏离率 * 7800 + 正态随机数(状态) * 250, -2500, 2500);
  const 趋势资金 = 限制(市场.动量 * 180000 + 正态随机数(状态) * 260, -2200, 2200);
  const 噪声资金 = 限制(噪声 * 520, -1500, 1500);
  const 风控资金 = 限制(
    -(波动率 - 0.003) * 150000 * (市场.状态 === '紧张' ? 1.5 : 1) - (市场.状态 === '下行' ? 280 : 0),
    -3000,
    650,
  );
  const 做市资金 = 限制(-库存压力 * 820 - (价值资金 + 趋势资金 + 噪声资金 + 风控资金) * 0.08, -1300, 1300);

  return {
    价值资金: Math.round(价值资金),
    趋势资金: Math.round(趋势资金),
    噪声资金: Math.round(噪声资金),
    风控资金: Math.round(风控资金),
    做市资金: Math.round(做市资金),
  };
}

function 更新历史(状态, 成交量) {
  const 市场 = 状态.市场;
  const 最后 = 状态.历史[状态.历史.length - 1];
  const 当前分钟 = new Date(状态.市场时间).getMinutes();

  if (当前分钟 % 30 === 0) {
    状态.历史.push({
      时间: 状态.市场时间,
      开盘: 市场.上次价格,
      最高: 市场.价格,
      最低: 市场.价格,
      收盘: 市场.价格,
      成交量,
      状态: 市场.状态,
    });
  } else {
    最后.最高 = Math.max(最后.最高, 市场.价格);
    最后.最低 = Math.min(最后.最低, 市场.价格);
    最后.收盘 = 市场.价格;
    最后.成交量 += 成交量;
    最后.状态 = 市场.状态;
  }

  if (状态.历史.length > 900) 状态.历史.splice(0, 状态.历史.length - 900);
}

function 写入市场日志(状态, 日志) {
  状态.日志.push(日志);
  if (状态.日志.length > 2400) 状态.日志.splice(0, 状态.日志.length - 2400);
}

function 变化说明(构成, 事件) {
  const 主因 = Object.entries(构成)
    .filter(([名称]) => 名称 !== '合计')
    .sort((左, 右) => Math.abs(右[1]) - Math.abs(左[1]))[0];

  const 方向 = 构成.合计 >= 0 ? '上行' : '下行';
  const 主因名称 = {
    价值回归: '价值回归',
    趋势跟随: '趋势资金',
    订单冲击: '净买卖盘',
    随机波动: '随机波动',
    事件冲击: '突发事件',
  }[主因[0]];

  if (事件) return `市场${方向}，${事件.名称}带来显著冲击。`;
  return `市场${方向}，主要推动因素为${主因名称}。`;
}

export function 推进一步(状态) {
  const 市场 = 状态.市场;
  const 上次价格 = 市场.价格;
  const 上次状态 = 市场.状态;
  推进时间(状态);

  市场.状态 = 选择状态(状态);
  const 参数 = 状态参数[市场.状态];
  const 事件 = 生成事件(状态, 参数);

  const 基础价值漂移 = 0.00006;
  const 价值扰动 = 正态随机数(状态) * 0.0011 * 参数.波动放大;
  const 事件价值冲击 = 事件 ? 事件.方向 * 事件.强度 * 事件.对价值影响 : 0;
  const 内在价值收益 = 基础价值漂移 + 价值扰动 + 事件价值冲击;
  市场.内在价值 = 保留(市场.内在价值 * Math.exp(内在价值收益), 4);

  const 偏离率 = Math.log(市场.内在价值 / 市场.价格);
  const 上次方差 = 市场.条件方差;
  const 杠杆项 = 市场.上次残差 < 0 ? Math.abs(市场.上次残差) * 0.028 : 0;
  市场.条件方差 = 限制(
    0.00000038 + 0.13 * 市场.上次残差 ** 2 + 0.82 * 上次方差 + 杠杆项,
    0.000001,
    0.0016,
  );
  市场.瞬时波动率 = Math.sqrt(市场.条件方差) * 参数.波动放大;

  const 资金 = 生成资金行为(状态, 偏离率, 市场.瞬时波动率);
  const 净买卖量 = Object.values(资金).reduce((合计, 数值) => 合计 + 数值, 0);
  const 原始流动性 = 参数.流动性 * (1 - Math.min(0.38, 市场.瞬时波动率 * 34));
  const 流动性噪声 = 正态随机数(状态) * 0.055;
  市场.流动性 = 保留(限制(原始流动性 + 流动性噪声, 0.16, 1.45), 4);

  const 价值回归 = 限制(偏离率 * 0.085, -0.019, 0.019);
  const 趋势跟随 = 限制(市场.动量 * 0.46, -0.011, 0.011);
  const 订单冲击 = 限制((净买卖量 / (8500 * 市场.流动性)) * 0.0058, -0.026, 0.026);
  const 随机波动 = 正态随机数(状态) * 市场.瞬时波动率;
  const 事件冲击 = 事件 ? 事件.方向 * 事件.强度 : 0;
  const 状态漂移 = 参数.漂移;
  const 合计 = 限制(状态漂移 + 价值回归 + 趋势跟随 + 订单冲击 + 随机波动 + 事件冲击, -0.16, 0.16);

  市场.上次价格 = 上次价格;
  市场.价格 = 保留(Math.max(0.1, 上次价格 * Math.exp(合计)), 2);
  市场.短期收益均值 = 市场.短期收益均值 * 0.78 + 合计 * 0.22;
  市场.长期收益均值 = 市场.长期收益均值 * 0.94 + 合计 * 0.06;
  市场.动量 = 保留(市场.短期收益均值 - 市场.长期收益均值, 7);
  市场.上次残差 = 随机波动;
  市场.做市库存 = 限制(市场.做市库存 - 净买卖量 * 0.18, -30000, 30000);
  const 成交量 = Math.max(80, Math.round(Math.abs(净买卖量) * (1.1 + 随机数(状态) * 0.9)));
  市场.累计成交量 += 成交量;
  更新盘口(市场);
  更新历史(状态, 成交量);

  状态.演算序号 += 1;
  const 构成 = {
    价值回归: 保留(价值回归, 6),
    趋势跟随: 保留(趋势跟随, 6),
    订单冲击: 保留(订单冲击, 6),
    随机波动: 保留(随机波动, 6),
    事件冲击: 保留(事件冲击, 6),
    合计: 保留(合计, 6),
  };

  const 日志 = {
    序号: 状态.演算序号,
    时间: 状态.市场时间,
    类型: '市场波动',
    上次价格,
    最新价格: 市场.价格,
    涨跌幅: 保留((市场.价格 / 上次价格 - 1) * 100, 3),
    状态: 市场.状态,
    状态变化: 上次状态 === 市场.状态 ? null : `${上次状态}→${市场.状态}`,
    内在价值: 保留(市场.内在价值, 2),
    价值偏离: 保留((市场.价格 / 市场.内在价值 - 1) * 100, 3),
    波动率: 保留(市场.瞬时波动率 * 100, 3),
    流动性: 市场.流动性,
    成交量,
    买卖价差: 保留(市场.买卖价差 * 100, 3),
    资金,
    净买卖量,
    事件,
    构成,
    说明: 变化说明(构成, 事件),
  };
  写入市场日志(状态, 日志);
  return 日志;
}

export function 玩家市价交易(状态, 方向, 数量) {
  const 市场 = 状态.市场;
  const 用户 = 状态.用户;
  const 真实数量 = Math.max(0, Math.floor(Number(数量) || 0));

  if (真实数量 <= 0) {
    return { 成功: false, 原因: '数量必须大于零。' };
  }

  const 买入 = 方向 === '买入';
  const 单位冲击 = 0.000018 * Math.sqrt(真实数量 / Math.max(市场.流动性, 0.18));
  const 冲击 = 限制(单位冲击, 0, 0.08);
  const 成交价 = 保留(
    买入
      ? 市场.卖一价 * (1 + 冲击)
      : 市场.买一价 * (1 - 冲击),
    2,
  );
  const 成交金额 = 保留(成交价 * 真实数量, 2);

  if (买入 && 用户.现金 + 0.00001 < 成交金额) {
    return { 成功: false, 原因: '可用资金不足。' };
  }
  if (!买入 && 用户.持仓 < 真实数量) {
    return { 成功: false, 原因: '可卖持仓不足。' };
  }

  const 原持仓 = 用户.持仓;
  if (买入) {
    用户.现金 = 保留(用户.现金 - 成交金额, 2);
    用户.持仓 += 真实数量;
    用户.持仓成本 = 保留(
      (用户.持仓成本 * 原持仓 + 成交金额) / 用户.持仓,
      4,
    );
  } else {
    用户.现金 = 保留(用户.现金 + 成交金额, 2);
    用户.持仓 -= 真实数量;
    用户.累计已实现盈亏 = 保留(
      用户.累计已实现盈亏 + (成交价 - 用户.持仓成本) * 真实数量,
      2,
    );
    if (用户.持仓 === 0) 用户.持仓成本 = 0;
  }

  const 冲击方向 = 买入 ? 1 : -1;
  const 原价格 = 市场.价格;
  市场.上次价格 = 原价格;
  市场.价格 = 保留(原价格 * Math.exp(冲击方向 * 冲击 * 0.42), 2);
  市场.做市库存 = 限制(市场.做市库存 - 冲击方向 * 真实数量, -30000, 30000);
  更新盘口(市场);

  状态.演算序号 += 1;
  const 日志 = {
    序号: 状态.演算序号,
    时间: 状态.市场时间,
    类型: '玩家成交',
    方向,
    数量: 真实数量,
    成交价,
    成交金额,
    价格冲击: 保留(冲击 * 100, 3),
    上次价格: 原价格,
    最新价格: 市场.价格,
    涨跌幅: 保留((市场.价格 / 原价格 - 1) * 100, 3),
    状态: 市场.状态,
    说明: `${方向}${真实数量}股，成交价为${成交价}，订单造成${保留(冲击 * 100, 3)}%的即时冲击。`,
  };
  写入市场日志(状态, 日志);
  return { 成功: true, 日志 };
}

export function 读取账户概况(状态) {
  const 市场价 = 状态.市场.价格;
  const 持仓市值 = 保留(状态.用户.持仓 * 市场价, 2);
  const 总资产 = 保留(状态.用户.现金 + 持仓市值, 2);
  const 浮动盈亏 = 保留((市场价 - 状态.用户.持仓成本) * 状态.用户.持仓, 2);
  return {
    持仓市值,
    总资产,
    浮动盈亏,
    总盈亏: 保留(总资产 - 100000, 2),
  };
}

