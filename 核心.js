function 限制(数值, 最小值, 最大值) {
  return Math.max(最小值, Math.min(最大值, 数值));
}

function 保留(数值, 位数 = 4) {
  const 倍数 = 10 ** 位数;
  return Math.round(数值 * 倍数) / 倍数;
}

function 随机数(状态) {
  状态.随机种子 = (状态.随机种子 + 0x6d2b79f5) >>> 0;
  let 值 = 状态.随机种子;
  值 = Math.imul(值 ^ (值 >>> 15), 值 | 1);
  值 ^= 值 + Math.imul(值 ^ (值 >>> 7), 值 | 61);
  return ((值 ^ (值 >>> 14)) >>> 0) / 4294967296;
}

function 正态随机数(状态) {
  const 左 = Math.max(随机数(状态), 0.0000001);
  const 右 = 随机数(状态);
  return Math.sqrt(-2 * Math.log(左)) * Math.cos(2 * Math.PI * 右);
}

function 选择状态(状态) {
  const 候选 = 状态转移[状态.市场.状态];
  let 累积 = 0;
  const 抽样 = 随机数(状态);

  for (const [名称, 概率] of 候选) {
    累积 += 概率;
    if (抽样 <= 累积) return 名称;
  }
  return 候选[候选.length - 1][0];
}

function 生成事件(状态, 参数) {
  if (随机数(状态) > 参数.事件概率) return null;

  const 指数 = Math.floor(随机数(状态) * 事件库.length);
  const 原型 = 事件库[指数];
  const 强度 = 原型.强度[0] + 随机数(状态) * (原型.强度[1] - 原型.强度[0]);

  return {
    名称: 原型.名称,
    方向: 原型.方向,
    强度: 保留(强度, 5),
    对价值影响: 原型.对价值影响,
  };
}

