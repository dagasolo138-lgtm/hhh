function 更新盘口(市场) {
  const 基础价差 = 0.0007;
  const 波动价差 = 市场.瞬时波动率 * 0.75;
  const 流动性惩罚 = (1 / Math.max(市场.流动性, 0.15) - 1) * 0.0018;
  市场.买卖价差 = 保留(限制(基础价差 + 波动价差 + 流动性惩罚, 0.0008, 0.045), 5);
  市场.买一价 = 保留(市场.价格 * (1 - 市场.买卖价差 / 2), 2);
  市场.卖一价 = 保留(市场.价格 * (1 + 市场.买卖价差 / 2), 2);
}

